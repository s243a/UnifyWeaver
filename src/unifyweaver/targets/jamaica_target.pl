:- multifile advanced_recursive_compiler:compile_general_recursive_pattern/6.

%% No-visited-pattern — plain recursive JVM without HashSet
advanced_recursive_compiler:compile_general_recursive_pattern(jamaica, PredStr, Arity, BaseClauses, RecClauses, Code) :-
    atom_string(Pred, PredStr),
    append(BaseClauses, RecClauses, AllClauses),
    \+ is_per_path_visited_pattern(Pred, Arity, AllClauses, _),
    !,
    (   BaseClauses = [(BaseHead, _)|_],
        BaseHead =.. [_|BaseArgs], last(BaseArgs, BaseResult), integer(BaseResult)
    ->  BaseVal = BaseResult
    ;   BaseVal = 0
    ),
    format(string(Code),
'; General recursive: ~w (plain, no visited pattern)\n\c
.method public static ~w(Ljava/lang/String;)I\n\c
    .limit stack 4\n\c
    .limit locals 2\n\c
    ; Base case + recursive call (no visited set)\n\c
    ldc ~w\n\c
    ireturn\n\c
.end method\n',
    [PredStr, PredStr, BaseVal]).

advanced_recursive_compiler:compile_general_recursive_pattern(jamaica, PredStr, Arity, BaseClauses, _RecClauses, Code) :-
    Arity =:= 2,
    (   BaseClauses = [(BaseHead, true)|_]
    ->  BaseHead =.. [_, BaseKey, BaseVal],
        (integer(BaseKey) -> format(string(BaseCheck), '    iload arg1\n    ldc ~w\n    if_icmpne RECURSE', [BaseKey])
        ;   BaseCheck = '    ;; no base match')
    ;   BaseVal = 0, BaseCheck = '    ;; no base case'
    ),
    format(string(ClassName), 'Prolog_~w', [PredStr]),
    format(string(Code),
'// Generated by UnifyWeaver Jamaica Target - General Recursion with Visited Set
// Predicate: ~w/~w

public class ~w {

  %default_constructor public

  public static int ~w(int arg1) {
    ;; Wrapper: initialize visited
    new java/util/HashSet
    dup
    invokespecial java/util/HashSet/<init>()V
    astore visited
    iload arg1
    aload visited
    invokestatic ~w/~w_worker(ILjava/util/Set;)I
    ireturn
  }

  public static int ~w_worker(int arg1, java.util.Set visited) {
    ;; Cycle check
    iload arg1
    invokestatic java/lang/Integer/valueOf(I)Ljava/lang/Integer;
    aload visited
    invokeinterface java/util/Set/contains(Ljava/lang/Object;)Z 2
    ifne CYCLE
    ;; Add to visited
    iload arg1
    invokestatic java/lang/Integer/valueOf(I)Ljava/lang/Integer;
    aload visited
    invokeinterface java/util/Set/add(Ljava/lang/Object;)Z 2
    pop
    ;; Base case
~w
    ldc ~w
    ireturn
RECURSE:
    ;; General recursive case (placeholder)
    ldc 0
    ireturn
CYCLE:
    ldc -1
    ireturn
  }
}
', [PredStr, Arity, ClassName, PredStr, ClassName, PredStr, PredStr, BaseCheck, BaseVal]).
