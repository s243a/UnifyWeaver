%% compile_jamaica_pipeline(+Steps, +Options, -Code)
%%   Compile a multi-step pipeline into a Jamaica class.
%%   Steps: list of step(Name, Pred/Arity) or step(Name, Body)
%%   Options: class_name(Name), package(Pkg)
compile_jamaica_pipeline(Steps, Options, Code) :-
    (   option(class_name(ClassName), Options)
    ->  true
    ;   ClassName = "Pipeline"
    ),
    % Generate step methods
    maplist(jamaica_step_method, Steps, StepMethods),
    atomic_list_concat(StepMethods, '\n', StepCode),
    % Generate orchestration
    maplist(jamaica_step_call, Steps, StepCalls),
    atomic_list_concat(StepCalls, '\n', OrchCode),
    format(string(Code),
'// Generated by UnifyWeaver Jamaica Target - Pipeline
import java.io.BufferedReader;
import java.io.InputStreamReader;
import java.io.PrintStream;

public class ~w {

  %default_constructor public

~w
  // Pipeline orchestration
  public static void main(String[] args) {
    // Execute steps sequentially
~w
    return
  }
}
', [ClassName, StepCode, OrchCode]).

jamaica_step_method(step(Name, Body), Code) :-
    atom_string(Name, NameStr),
    format(string(Code),
'  public static int step_~w(int input) {
~w
  }
', [NameStr, Body]).

jamaica_step_call(step(Name, _Body), Call) :-
    atom_string(Name, NameStr),
    format(string(Call),
'    // Step: ~w
    // invokestatic step_~w(int)int', [NameStr, NameStr]).

