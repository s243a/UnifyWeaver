%% generate_jamaica_build_script(+Options, -Script)
%%   Generate a shell script to assemble and package a Jamaica project.
generate_jamaica_build_script(Options, Script) :-
    (   option(source_file(SrcFile), Options)
    ->  true
    ;   SrcFile = "Program.ja"
    ),
    (   option(main_class(MainClass), Options)
    ->  true
    ;   MainClass = "Program"
    ),
    (   option(output_jar(JarFile), Options)
    ->  true
    ;   JarFile = "output.jar"
    ),
    format(string(Script),
'#!/bin/bash
# Generated by UnifyWeaver Jamaica Target - Build Script
# Assembles Jamaica source and packages into JAR

JAMAICA_HOME="${JAMAICA_HOME:-$HOME/jamaica-tool}"
JAMAICA_CP="$JAMAICA_HOME/jamaica.jar:$JAMAICA_HOME/lib/asm-3.1.jar"

echo "Assembling ~w..."
java -cp "$JAMAICA_CP:$JAMAICA_HOME/lib/bcel-5.2.jar:$JAMAICA_HOME/lib/commons-lang-2.4.jar" \\
    com.judoscript.jamaica.Main ~w

echo "Packaging into ~w..."
echo "Main-Class: ~w" > MANIFEST.MF
echo "Created-By: UnifyWeaver Jamaica Target" >> MANIFEST.MF
jar cfm ~w MANIFEST.MF *.class
rm -f MANIFEST.MF

echo "Done. Run with: java -jar ~w"
', [SrcFile, SrcFile, JarFile, MainClass, JarFile, JarFile]).

