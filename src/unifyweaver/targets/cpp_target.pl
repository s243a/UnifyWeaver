    ).

translate_fold_expr_cpp(A * B, IV, AV, E) :- translate_cpp_term(A, IV, AV, AT), translate_cpp_term(B, IV, AV, BT), format(string(E), '~w * ~w', [AT, BT]).
translate_fold_expr_cpp(A + B, IV, AV, E) :- translate_cpp_term(A, IV, AV, AT), translate_cpp_term(B, IV, AV, BT), format(string(E), '~w + ~w', [AT, BT]).
translate_fold_expr_cpp(A - B, IV, AV, E) :- translate_cpp_term(A, IV, AV, AT), translate_cpp_term(B, IV, AV, BT), format(string(E), '~w - ~w', [AT, BT]).
translate_fold_expr_cpp(T, IV, AV, E) :- translate_cpp_term(T, IV, AV, E).

translate_cpp_term(T, IV, _, 'current') :- T == IV, !.
translate_cpp_term(T, _, AV, 'result') :- T == AV, !.
translate_cpp_term(N, _, _, S) :- integer(N), !, format(string(S), '~w', [N]).
translate_cpp_term(A, _, _, S) :- format(string(S), '~w', [A]).

%% translate_list_fold_cpp(+PrologExpr, +HeadVar, +AccVar, -CppExpr)
