    int result;
    if (n <= 0) result = 0;
    else if (n == 1) result = 1;
    else result = ~w(n - 1) + n;
    memo[n] = result;
    return result;
}

int main(int argc, char* argv[]) {
    if (argc >= 2) std::cout << ~w(std::atoi(argv[1])) << std::endl;
