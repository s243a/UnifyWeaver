mutual_recursion:compile_mutual_pattern(cpp, Predicates, MemoEnabled, _MemoStrategy, CppCode) :-
    % Forward declarations
    findall(FwdDecl, (
        member(Pred/_Arity, Predicates),
        atom_string(Pred, PredStr),
        format(string(FwdDecl), 'int ~w(int n);', [PredStr])
    ), FwdDecls),
    atomic_list_concat(FwdDecls, '\n', ForwardDecls),
    % Generate functions
    mutual_functions_cpp(Predicates, Predicates, MemoEnabled, FuncCodes),
    atomic_list_concat(FuncCodes, '\n\n', FunctionsCode),
    % Dispatch
    mutual_dispatch_cpp(Predicates, DispatchCode),
    format(string(CppCode),
'/* Generated by UnifyWeaver C++ Target - Mutual Recursion (multifile dispatch) */
#include <iostream>
#include <cstdlib>
#include <cstring>
#include <string>
#include <unordered_map>

static std::unordered_map<std::string, int> memo;

~w

~w

int main(int argc, char *argv[]) {
~w
    return 0;
}
', [ForwardDecls, FunctionsCode, DispatchCode]).

mutual_functions_cpp([], _AllPreds, _MemoEnabled, []).
mutual_functions_cpp([Pred/Arity|Rest], AllPreds, MemoEnabled, [FuncCode|RestCodes]) :-
    atom_string(Pred, PredStr),
    functor(Head, Pred, Arity),
    findall(clause(Head, Body), user:clause(Head, Body), Clauses),
    partition(mutual_recursion:is_mutual_recursive_clause(AllPreds), Clauses, RecClauses, BaseClauses),
    mutual_base_cases_cpp(BaseClauses, PredStr, MemoEnabled, BaseParts),
    atomic_list_concat(BaseParts, '\n', BaseCode),
    mutual_rec_cases_cpp(RecClauses, PredStr, MemoEnabled, RecParts),
    atomic_list_concat(RecParts, '\n', RecCode),
    (   MemoEnabled = true ->
        format(string(FuncCode),
'int ~w(int n) {
    std::string key = "~w:" + std::to_string(n);
    auto it = memo.find(key);
    if (it != memo.end()) return it->second;
~w
~w
    return 0;
}', [PredStr, PredStr, BaseCode, RecCode])
    ;   format(string(FuncCode),
'int ~w(int n) {
~w
~w
    return 0;
}', [PredStr, BaseCode, RecCode])
    ),
    mutual_functions_cpp(Rest, AllPreds, MemoEnabled, RestCodes).

mutual_base_cases_cpp([], _PredStr, _MemoEnabled, []).
mutual_base_cases_cpp([clause(Head, true)|Rest], PredStr, MemoEnabled, [Part|RestParts]) :-
    Head =.. [_Pred, Value],
    (   MemoEnabled = true ->
        format(string(Part),
'    if (n == ~w) { memo["~w:" + std::to_string(n)] = 1; return 1; }',
            [Value, PredStr])
    ;   format(string(Part), '    if (n == ~w) return 1;', [Value])
    ),
    mutual_base_cases_cpp(Rest, PredStr, MemoEnabled, RestParts).
mutual_base_cases_cpp([_|Rest], PredStr, MemoEnabled, RestParts) :-
    mutual_base_cases_cpp(Rest, PredStr, MemoEnabled, RestParts).

mutual_rec_cases_cpp([], _PredStr, _MemoEnabled, []).
mutual_rec_cases_cpp([clause(_Head, Body)|Rest], PredStr, MemoEnabled, [Part|RestParts]) :-
    extract_mutual_rec_info_cpp(Body, Guard, CalledPred, Step),
    atom_string(CalledPred, CalledStr),
    (   Guard = (N > Threshold), var(N) ->
        (   MemoEnabled = true ->
            format(string(Part),
'    if (n > ~w) {
        int result = ~w(n ~w);
        memo["~w:" + std::to_string(n)] = result;
        return result;
    }', [Threshold, CalledStr, Step, PredStr])
        ;   format(string(Part),
'    if (n > ~w) return ~w(n ~w);', [Threshold, CalledStr, Step])
        )
    ;   (   MemoEnabled = true ->
            format(string(Part),
'    {
        int result = ~w(n ~w);
        memo["~w:" + std::to_string(n)] = result;
        return result;
    }', [CalledStr, Step, PredStr])
        ;   format(string(Part), '    return ~w(n ~w);', [CalledStr, Step])
        )
    ),
    mutual_rec_cases_cpp(Rest, PredStr, MemoEnabled, RestParts).
mutual_rec_cases_cpp([_|Rest], PredStr, MemoEnabled, RestParts) :-
    mutual_rec_cases_cpp(Rest, PredStr, MemoEnabled, RestParts).

extract_mutual_rec_info_cpp(Body, Guard, CalledPred, Step) :-
    extract_goals_from_body_cpp(Body, Goals),
    (   member(Guard, Goals), Guard = (_ > _) -> true
    ;   Guard = none
    ),
    member(Call, Goals),
    compound(Call),
    Call \= (_ is _), Call \= (_ > _), Call \= (_ < _),
    Call \= (_ >= _), Call \= (_ =< _),
    functor(Call, CalledPred, _),
    (   member(_ is _ - K, Goals), integer(K) ->
        format(string(Step), '- ~w', [K])
    ;   member(_ is _ + K, Goals), integer(K), K < 0 ->
        AbsK is abs(K),
        format(string(Step), '- ~w', [AbsK])
    ;   Step = "- 1"
    ).

extract_goals_from_body_cpp((A, B), Goals) :- !,
    extract_goals_from_body_cpp(A, GA),
    extract_goals_from_body_cpp(B, GB),
    append(GA, GB, Goals).
extract_goals_from_body_cpp(true, []) :- !.
extract_goals_from_body_cpp(Goal, [Goal]).

mutual_dispatch_cpp(Predicates, Code) :-
    findall(DispLine, (
        member(Pred/_Arity, Predicates),
        atom_string(Pred, PredStr),
        format(string(DispLine),
'    if (argc >= 3 && std::string(argv[1]) == "~w") std::cout << ~w(std::atoi(argv[2])) << std::endl;',
            [PredStr, PredStr])
    ), Lines),
    atomic_list_concat(Lines, '\n', Code).
