tail_recursion:compile_tail_pattern(cpp, PredStr, Arity, _BaseClauses, _RecClauses, _AccPos, StepOp, _ExitAfterResult, Code) :-
    step_op_to_cpp(StepOp, CppStepExpr),
    (   Arity =:= 3 ->
        format(string(Code),
'// Generated by UnifyWeaver C++ Target - Tail Recursion (multifile dispatch)
// Predicate: ~w/~w

#include <iostream>
#include <vector>
#include <string>
#include <sstream>

int ~w(const std::vector<int>& items) {
    int acc = 0;
    for (int item : items) {
        acc = ~w;
    }
    return acc;
}

int main(int argc, char* argv[]) {
    if (argc >= 2) {
        std::vector<int> items;
        std::stringstream ss(argv[1]);
        std::string token;
        while (std::getline(ss, token, '','')) {
            items.push_back(std::stoi(token));
        }
        std::cout << ~w(items) << std::endl;
    }
    return 0;
}
', [PredStr, Arity, PredStr, CppStepExpr, PredStr])
    ;   Arity =:= 2 ->
        format(string(Code),
'// Generated by UnifyWeaver C++ Target - Tail Recursion (binary, multifile dispatch)
// Predicate: ~w/~w

#include <iostream>
#include <vector>
#include <string>
#include <sstream>

template<typename T>
int ~w(const std::vector<T>& items) {
    int count = 0;
    for (const auto& item : items) { (void)item; count++; }
    return count;
}

int main(int argc, char* argv[]) {
    if (argc >= 2) {
        std::vector<std::string> items;
        std::stringstream ss(argv[1]);
        std::string token;
        while (std::getline(ss, token, '','')) {
            items.push_back(token);
        }
        std::cout << ~w(items) << std::endl;
    }
    return 0;
}
', [PredStr, Arity, PredStr, PredStr])
    ;   fail
    ).

step_op_to_cpp(arithmetic(Expr), CppExpr) :- tail_expr_to_cpp(Expr, CppExpr).
step_op_to_cpp(unknown, 'acc + 1').

tail_expr_to_cpp(_ + Const, CppExpr) :- integer(Const), !, format(atom(CppExpr), 'acc + ~w', [Const]).
tail_expr_to_cpp(_ + _, 'acc + item') :- !.
tail_expr_to_cpp(_ - _, 'acc - item') :- !.
tail_expr_to_cpp(_ * _, 'acc * item') :- !.
tail_expr_to_cpp(_, 'acc + 1').

