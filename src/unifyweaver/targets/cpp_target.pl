direct_recursive_calls_cpp(RecCalls, PredStr, Code) :-
    findall(Line, (
        member(RecCall, RecCalls),
        RecCall =.. [_Pred, ArgVar, ResultVar],
        direct_var_to_cpp_name(ArgVar, ArgName),
        direct_var_to_cpp_name(ResultVar, ResName),
        format(string(Line), '    int ~w = ~w(~w);', [ResName, PredStr, ArgName])
    ), Lines),
    atomic_list_concat(Lines, '\n', Code).

direct_aggregation_cpp(_ is Expr, Code) :-
    direct_translate_agg_expr_cpp(Expr, CppExpr),
    format(string(Code), '    int result = ~w;', [CppExpr]).

direct_translate_agg_expr_cpp(A + B, Expr) :-
    direct_var_to_cpp_name(A, AName), direct_var_to_cpp_name(B, BName),
    format(string(Expr), '~w + ~w', [AName, BName]).
direct_translate_agg_expr_cpp(A * B, Expr) :-
    direct_var_to_cpp_name(A, AName), direct_var_to_cpp_name(B, BName),
    format(string(Expr), '~w * ~w', [AName, BName]).

%% direct_var_to_cpp_name(+Var, -CppName)
%  Convert Prolog variable to valid C++ identifier.
%  Internal var names like _G12345 become v12345 in C++.
direct_var_to_cpp_name(Var, CppName) :-
    (   var(Var) ->
        term_string(Var, VarStr),
        atom_string(VarAtom, VarStr),
        downcase_atom(VarAtom, LowerName),
        ensure_cpp_identifier(LowerName, CppName)
    ;   atom(Var) ->
        downcase_atom(Var, LowerName),
        ensure_cpp_identifier(LowerName, CppName)
    ;   term_string(Var, VarStr),
        atom_string(VarAtom, VarStr),
        downcase_atom(VarAtom, LowerName),
        ensure_cpp_identifier(LowerName, CppName)
    ).

ensure_cpp_identifier(Name, ValidName) :-
    atom_chars(Name, [First|Rest]),
    (   (First = '_' ; char_type(First, digit)) ->
        atom_chars(ValidName, [v|Rest])
    ;   ValidName = Name
    ).

direct_translate_expr_cpp(N - K, Expr) :- var(N), integer(K), !,
    format(string(Expr), 'input - ~w', [K]).
direct_translate_expr_cpp(N + K, Expr) :- var(N), integer(K), K < 0, !,
    AbsK is abs(K), format(string(Expr), 'input - ~w', [AbsK]).
direct_translate_expr_cpp(N + K, Expr) :- var(N), integer(K), !,
    format(string(Expr), 'input + ~w', [K]).
direct_translate_expr_cpp(Expr, 'input') :- var(Expr), !.
direct_translate_expr_cpp(N, S) :- integer(N), !, format(string(S), '~w', [N]).
direct_translate_expr_cpp(A, S) :- format(string(S), '~w', [A]).

