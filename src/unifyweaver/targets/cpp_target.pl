translate_list_fold_cpp(A - B, HV, AV, E) :- translate_list_term_cpp(A, HV, AV, AT), translate_list_term_cpp(B, HV, AV, BT), format(string(E), '~w - ~w', [AT, BT]).
translate_list_fold_cpp(T, HV, AV, E) :- translate_list_term_cpp(T, HV, AV, E).

translate_list_term_cpp(T, HV, _, 'current') :- T == HV, !.
translate_list_term_cpp(T, _, AV, 'result') :- T == AV, !.
translate_list_term_cpp(N, _, _, S) :- integer(N), !, format(string(S), '~w', [N]).
translate_list_term_cpp(A, _, _, S) :- format(string(S), '~w', [A]).

