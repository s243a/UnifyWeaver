:- use_module('../core/advanced/linear_recursion').
:- multifile linear_recursion:compile_linear_pattern/8.

linear_recursion:compile_linear_pattern(cpp, PredStr, Arity, BaseClauses, _RecClauses, MemoEnabled, _MemoStrategy, Code) :-
    (   Arity =:= 2 ->
        linear_fold_cpp(PredStr, BaseClauses, MemoEnabled, Code)
    ;   linear_generic_cpp(PredStr, Arity, MemoEnabled, Code)
    ).

linear_fold_cpp(PredStr, BaseClauses, MemoEnabled, Code) :-
    linear_recursion:extract_base_case_info(BaseClauses, BaseInput, BaseOutput),
    linear_recursion:detect_input_type(BaseInput, InputType),
    (   MemoEnabled = true ->
        MemoDecl = "static std::unordered_map<int, int> memo;",
        format(string(MemoCheck), '    auto it = memo.find(n); if (it != memo.end()) return it->second;', []),
        format(string(MemoStore), '    memo[n] = result;', [])
    ;   MemoDecl = "// Memoization disabled",
        MemoCheck = '',
        MemoStore = ''
    ),
    % Extract fold
    atom_string(Pred, PredStr),
    functor(Head, Pred, 2),
    findall(clause(Head, Body), user:clause(Head, Body), AllClauses),
    partition(linear_recursion:is_recursive_clause(Pred), AllClauses, ActualRec, _),
    (   ActualRec = [clause(RHead, RBody)|_] ->
        RHead =.. [_, InputVar, _],
        linear_recursion:find_recursive_call(RBody, RecCall),
        RecCall =.. [_, _, AccVar],
        linear_recursion:find_last_is_expression(RBody, _ is FoldExpr),
        translate_fold_expr_cpp(FoldExpr, InputVar, AccVar, CppOp)
    ;   CppOp = "current * result"
    ),
    (   InputType = numeric ->
        format(string(Code),
'// Generated by UnifyWeaver C++ Target - Linear Recursion (numeric, multifile dispatch)
// Predicate: ~w/2

#include <iostream>
#include <unordered_map>

~w

int ~w(int n) {
~w
    if (n == ~w) return ~w;
    int result = ~w;
    for (int current = n; current >= 1; current--) {
        result = ~w;
    }
~w
    return result;
}

int main(int argc, char* argv[]) {
    if (argc >= 2) std::cout << ~w(std::atoi(argv[1])) << std::endl;
    return 0;
}
', [PredStr, MemoDecl, PredStr, MemoCheck, BaseInput, BaseOutput, BaseOutput, CppOp, MemoStore, PredStr])
    ;   InputType = list ->
        % Re-extract fold with head variable for list patterns
        (   ActualRec = [clause(LRHead, LRBody)|_] ->
            linear_recursion:find_last_is_expression(LRBody, _ is LFoldExpr),
            linear_recursion:find_recursive_call(LRBody, LRecCall),
            LRecCall =.. [_, _, LAccVar],
            LRHead =.. [_, [LHeadVar|_], _],
            translate_list_fold_cpp(LFoldExpr, LHeadVar, LAccVar, ListCppOp)
        ;   ListCppOp = "result + current"
        ),
        format(string(Code),
'// Generated by UnifyWeaver C++ Target - Linear Recursion (list, multifile dispatch)
// Predicate: ~w/2

#include <iostream>
#include <vector>
#include <unordered_map>
#include <numeric>

~w

int ~w(const std::vector<int>& lst) {
    if (lst.empty()) return ~w;
    return std::accumulate(lst.begin(), lst.end(), ~w,
        [](int result, int current) { return ~w; });
}

int main(int argc, char* argv[]) {
    // Usage with list input
    return 0;
}
', [PredStr, '// List pattern — no memoization', PredStr, BaseOutput, BaseOutput, ListCppOp])
    ;   linear_generic_cpp(PredStr, 2, MemoEnabled, Code)
    ).

linear_generic_cpp(PredStr, Arity, MemoEnabled, Code) :-
