direct_computations_cpp(Computations, Code) :-
    findall(Line, (
        member(Var is Expr, Computations),
        direct_var_to_cpp_name(Var, VarName),
        direct_translate_expr_cpp(Expr, CppExpr),
        format(string(Line), '    int ~w = ~w;', [VarName, CppExpr])
    ), Lines),
