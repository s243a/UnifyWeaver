%% native_llvm_clause_body(+PredSpec, +Clauses, -FuncBody, -StrConstants)
%%
%% Generates LLVM IR basic blocks for if/else chains.
%% Returns i64 for numeric results.
%% StrConstants collects any global string constants needed.

% Single clause
native_llvm_clause_body(PredSpec, [Head-Body], FuncBody, "") :-
    native_llvm_clause(PredSpec, Head, Body, Conditions, ResultExpr),
    !,
    (   Conditions == []
    ->  % Unconditional: just compute and return
        format(string(FuncBody),
'entry:
~w
  ret i64 ~w', [ResultExpr.setup, ResultExpr.value])
    ;   % Conditional: branch to then or error
        llvm_combine_conditions(Conditions, 0, CondCode, CondReg, _RegOut),
        format(string(FuncBody),
'entry:
~w  br i1 ~w, label %then, label %error

then:
~w
  ret i64 ~w

error:
  %err_ptr = getelementptr [0 x i8], [0 x i8]* bitcast ([0 x i8]* @.str.err to [0 x i8]*), i64 0, i64 0
  call void @exit(i32 1)
  unreachable', [CondCode, CondReg, ResultExpr.setup, ResultExpr.value])
    ).

% Multi-clause → chained basic blocks
native_llvm_clause_body(PredSpec, Clauses, FuncBody, "") :-
    Clauses = [_|[_|_]],
    maplist(native_llvm_clause_pair(PredSpec), Clauses, Branches),
    Branches \= [],
    llvm_branches_to_blocks(Branches, 0, FuncBody).

native_llvm_clause_pair(PredSpec, Head-Body, branch(Conditions, ResultExpr)) :-
    native_llvm_clause(PredSpec, Head, Body, Conditions, ResultExpr),
    !.

%% native_llvm_clause(+PredSpec, +Head, +Body, -Conditions, -ResultExpr)
%%
%% ResultExpr is a dict with: value (SSA reg or literal), setup (IR code to compute it)
native_llvm_clause(_PredSpec, Head, Body, Conditions, ResultExpr) :-
    Head =.. [_Pred|HeadArgs],
    length(HeadArgs, Arity),
    build_head_varmap(HeadArgs, 1, VarMap),
    (   Arity > 1
    ->  append(_InputHeadArgs, [OutputHeadArg], HeadArgs),
        llvm_head_conditions(HeadArgs, 1, Arity, HeadConditions)
    ;   OutputHeadArg = _,
        llvm_head_conditions(HeadArgs, 1, Arity, HeadConditions)
    ),
    normalize_goals(Body, Goals),
    (   Goals == []
    ->  llvm_resolve_value(VarMap, OutputHeadArg, Value),
        ResultExpr = result{value: Value, setup: "", setup2: ""},
        GoalConditions = []
    ;   %% Try classify_goal_sequence for advanced patterns
        (   classify_goal_sequence(Goals, VarMap, ClassifiedGoals),
            ClassifiedGoals \= [],
            llvm_render_classified(ClassifiedGoals, VarMap, OutputHeadArg, Arity, GoalConditions, ResultExpr)
        ->  true
        ;   %% Fallback to basic split
            (   Arity > 1, nonvar(OutputHeadArg)
            ->  clause_guard_output_split(Goals, VarMap, GuardGoals, OutputGoals),
                maplist(llvm_goal_to_condition(VarMap), GuardGoals, GoalConditions),
                (   OutputGoals == []
                ->  llvm_resolve_value(VarMap, OutputHeadArg, Value),
                    ResultExpr = result{value: Value, setup: "", setup2: ""}
                ;   llvm_output_expr(OutputGoals, VarMap, Value, SetupCode),
                    ResultExpr = result{value: Value, setup: SetupCode, setup2: SetupCode}
                )
            ;   clause_guard_output_split(Goals, VarMap, GuardGoals, OutputGoals),
                maplist(llvm_goal_to_condition(VarMap), GuardGoals, GoalConditions),
                llvm_output_expr(OutputGoals, VarMap, Value, SetupCode),
                ResultExpr = result{value: Value, setup: SetupCode, setup2: SetupCode}
            )
        )
    ),
    append(HeadConditions, GoalConditions, Conditions).

%% llvm_render_classified(+ClassifiedGoals, +VarMap, +OutputHeadArg, +Arity, -Conditions, -ResultExpr)
%  Render classified goals for LLVM IR output.
llvm_render_classified(ClassifiedGoals, VarMap, OutputHeadArg, Arity, Conditions, ResultExpr) :-
    %% Collect guard conditions
    findall(C, (
        member(guard(G, _), ClassifiedGoals),
        llvm_goal_to_condition(VarMap, G, C)
    ), Conditions),
    %% Find the output value
    (   member(output(Goal, _, _), ClassifiedGoals)
    ->  (   Goal = (_ is ArithExpr)
        ->  llvm_arith_expr(ArithExpr, VarMap, Value, SetupCode),
            ResultExpr = result{value: Value, setup: SetupCode, setup2: SetupCode}
        ;   Goal = (_ = Expr)
        ->  llvm_resolve_value(VarMap, Expr, Value),
            ResultExpr = result{value: Value, setup: "", setup2: ""}
        ;   llvm_resolve_value(VarMap, Goal, Value),
            ResultExpr = result{value: Value, setup: "", setup2: ""}
        )
    ;   member(output_ite(If, Then, Else, _), ClassifiedGoals)
    ->  %% If-then-else: compile both branches
        llvm_goal_to_condition(VarMap, If, IfCond),
        llvm_branch_value_from_goals(Then, VarMap, ThenVal),
        llvm_branch_value_from_goals(Else, VarMap, ElseVal),
        format(string(SetupCode),
'  %%ite_cond = ~w
  br i1 %%ite_cond, label %%ite_then, label %%ite_else
ite_then:
  br label %%ite_end
ite_else:
  br label %%ite_end
ite_end:
  %%ite_result = phi i64 [~w, %%ite_then], [~w, %%ite_else]', [IfCond, ThenVal, ElseVal]),
        ResultExpr = result{value: "%ite_result", setup: SetupCode, setup2: SetupCode}
    ;   Arity > 1, nonvar(OutputHeadArg)
    ->  llvm_resolve_value(VarMap, OutputHeadArg, Value),
        ResultExpr = result{value: Value, setup: "", setup2: ""}
    ;   ResultExpr = result{value: "0", setup: "", setup2: ""}
    ).

%% llvm_branch_value_from_goals(+Branch, +VarMap, -Value)
llvm_branch_value_from_goals(Branch, VarMap, Value) :-
    normalize_goals(Branch, Goals),
    last(Goals, LastGoal),
    (   LastGoal = (_ = Expr) -> llvm_resolve_value(VarMap, Expr, Value)
    ;   LastGoal = (_ is Expr) -> llvm_arith_expr(Expr, VarMap, Value, _)
    ;   llvm_resolve_value(VarMap, LastGoal, Value)
    ).

%% llvm_head_conditions(+HeadArgs, +Index, +Arity, -Conditions)
llvm_head_conditions([], _, _, []).
llvm_head_conditions([_], _, Arity, []) :- Arity > 1, !.
llvm_head_conditions([HeadArg|Rest], Index, Arity, Conditions) :-
    (   var(HeadArg)
    ->  Conditions = RestConditions
    ;   (   number(HeadArg)
        ->  format(string(ArgName), '%arg~w', [Index]),
            format(string(Cond), 'icmp eq i64 ~w, ~w', [ArgName, HeadArg]),
            Conditions = [Cond|RestConditions]
        ;   Conditions = RestConditions  % skip non-numeric head matches in LLVM
        )
    ),
    NextIndex is Index + 1,
    llvm_head_conditions(Rest, NextIndex, Arity, RestConditions).

%% llvm_goal_to_condition(+VarMap, +Goal, -Condition)
%%   Condition is an LLVM icmp instruction string (without result register)
llvm_goal_to_condition(VarMap, _Module:Goal, Condition) :-
    !, llvm_goal_to_condition(VarMap, Goal, Condition).
llvm_goal_to_condition(VarMap, Goal, Condition) :-
    compound(Goal),
    Goal =.. [Op, Left, Right],
    expr_op(Op, StdOp),
    !,
    llvm_value(Left, VarMap, LVal),
    llvm_value(Right, VarMap, RVal),
    llvm_icmp_op(StdOp, IcmpOp),
    format(string(Condition), 'icmp ~w i64 ~w, ~w', [IcmpOp, LVal, RVal]).

%% llvm_output_expr(+Goals, +VarMap, -Value, -SetupCode)
%%   Generates LLVM IR instructions for the output expression
llvm_output_expr([], _VarMap, '0', "") :- !.
llvm_output_expr([Goal], VarMap, Value, SetupCode) :-
    !, llvm_output_expr_last(Goal, VarMap, Value, SetupCode).
llvm_output_expr([_Goal|Rest], VarMap, Value, SetupCode) :-
    llvm_output_expr(Rest, VarMap, Value, SetupCode).

llvm_output_expr_last(_Module:Goal, VarMap, Value, Setup) :-
    !, llvm_output_expr_last(Goal, VarMap, Value, Setup).
llvm_output_expr_last(=(Var, Expr), VarMap, Value, Setup) :-
    var(Var), !,
    llvm_arith_expr(Expr, VarMap, Value, Setup).
llvm_output_expr_last(is(Var, Expr), VarMap, Value, Setup) :-
    var(Var), !,
    llvm_arith_expr(Expr, VarMap, Value, Setup).
llvm_output_expr_last(Goal, VarMap, Value, Setup) :-
    if_then_else_goal(Goal, If, Then, Else),
    !,
    % Generate LLVM select chain for if-then-else
    llvm_if_then_else_select(If, Then, Else, VarMap, Value, Setup).
llvm_output_expr_last(_Goal, _VarMap, '0', "") :- !.

%% llvm_arith_expr(+Expr, +VarMap, -Value, -SetupCode)
%%   Compile arithmetic expression to LLVM IR instructions
llvm_arith_expr(Var, VarMap, Value, "") :-
    var(Var), !,
    llvm_value(Var, VarMap, Value).
llvm_arith_expr(Expr, VarMap, Value, SetupCode) :-
    compound(Expr),
    Expr =.. [Op, Left, Right],
    expr_op(Op, StdOp),
    !,
    llvm_value(Left, VarMap, LVal),
    llvm_value(Right, VarMap, RVal),
    llvm_arith_op(StdOp, LLVMOp),
    gensym('%r', Value),
    format(string(SetupCode), '  ~w = ~w i64 ~w, ~w', [Value, LLVMOp, LVal, RVal]).
llvm_arith_expr(-Expr, VarMap, Value, SetupCode) :-
    !,
    llvm_value(Expr, VarMap, Inner),
    gensym('%r', Value),
    format(string(SetupCode), '  ~w = sub i64 0, ~w', [Value, Inner]).
llvm_arith_expr(Atom, _VarMap, Value, "") :-
    number(Atom), !,
    format(string(Value), '~w', [Atom]).
llvm_arith_expr(Var, VarMap, Value, "") :-
    var(Var), !,
    llvm_value(Var, VarMap, Value).
llvm_arith_expr(Atom, _VarMap, '0', "") :-
    atom(Atom), !.

%% llvm_if_then_else_select — generate LLVM select chain for if-then-else
%%   (Cond -> ThenGoal ; ElseGoal) → icmp + select
llvm_if_then_else_select(If, Then, Else, VarMap, Value, Setup) :-
    % Compile the condition
    llvm_goal_to_condition(VarMap, If, Cond),
    gensym('%sel_cond', CondReg),
    format(string(CondCode), '  ~w = ~w~n', [CondReg, Cond]),
    % Compile then value
    llvm_then_value(Then, VarMap, ThenVal, ThenSetup),
    % Compile else value (may be nested if-then-else)
    llvm_else_value(Else, VarMap, ElseVal, ElseSetup),
    % Generate select instruction
    gensym('%sel', Value),
    format(string(SelectCode), '  ~w = select i1 ~w, i64 ~w, i64 ~w~n',
           [Value, CondReg, ThenVal, ElseVal]),
    format(string(Setup), '~w~w~w~w', [ThenSetup, ElseSetup, CondCode, SelectCode]).

%% llvm_then_value — extract value from then branch (assignment or arithmetic)
llvm_then_value(=(_, Expr), VarMap, Value, Setup) :-
    !, llvm_arith_expr(Expr, VarMap, Value, Setup).
llvm_then_value(is(_, Expr), VarMap, Value, Setup) :-
    !, llvm_arith_expr(Expr, VarMap, Value, Setup).
llvm_then_value(Goal, VarMap, Value, Setup) :-
    normalize_goals(Goal, Goals),
    (   Goals = [SingleGoal]
    ->  llvm_then_value(SingleGoal, VarMap, Value, Setup)
    ;   llvm_value(Goal, VarMap, Value), Setup = ""
    ).

%% llvm_else_value — extract value from else branch (may be nested if-then-else)
llvm_else_value(Goal, VarMap, Value, Setup) :-
    if_then_else_goal(Goal, If, Then, Else),
    !,
    llvm_if_then_else_select(If, Then, Else, VarMap, Value, Setup).
llvm_else_value(=(_, Expr), VarMap, Value, Setup) :-
    !, llvm_arith_expr(Expr, VarMap, Value, Setup).
llvm_else_value(is(_, Expr), VarMap, Value, Setup) :-
    !, llvm_arith_expr(Expr, VarMap, Value, Setup).
llvm_else_value(Goal, VarMap, Value, Setup) :-
    normalize_goals(Goal, Goals),
    (   Goals = [SingleGoal]
    ->  llvm_else_value(SingleGoal, VarMap, Value, Setup)
    ;   llvm_value(Goal, VarMap, Value), Setup = ""
    ).

%% llvm_value — resolve a Prolog term to an LLVM IR value
llvm_value(Var, VarMap, Value) :-
    var(Var), !,
    (   lookup_var(Var, VarMap, Name)
    ->  format(string(Value), '%~w', [Name])
    ;   Value = '0'
    ).
llvm_value(Number, _VarMap, Value) :-
    number(Number), !,
    format(string(Value), '~w', [Number]).
llvm_value(Atom, _VarMap, '0') :-
    atom(Atom), !.
llvm_value(Expr, VarMap, Value) :-
    compound(Expr),
    Expr =.. [Op, Left, Right],
    expr_op(Op, _),
    !,
    llvm_value(Left, VarMap, LVal),
    llvm_value(Right, VarMap, RVal),
    % For compound expressions used as values, just use left operand
    % (proper setup code handles the computation)
    Value = LVal.
llvm_value(_, _VarMap, '0').

%% llvm_resolve_value — resolve a head arg or variable to LLVM value
llvm_resolve_value(VarMap, Var, Value) :-
    var(Var), !,
    (   lookup_var(Var, VarMap, Name)
    ->  format(string(Value), '%~w', [Name])
    ;   Value = '0'
    ).
llvm_resolve_value(_VarMap, Number, Value) :-
    number(Number), !,
    format(string(Value), '~w', [Number]).
llvm_resolve_value(_VarMap, _, '0').

