%% compile_shared_library_llvm(+Functions, +Options, -LLVMCode)
%  Compile multiple functions into a shared library with C ABI exports.
%
%  @arg Functions List of func(Name, Arity, Type) where Type is:
%       - tail_recursion, linear_recursion, transitive_closure
%  @arg Options [library_name(Name)]
%  @arg LLVMCode Generated LLVM IR for shared library
compile_shared_library_llvm(Functions, Options, Code) :-
    option(library_name(LibName), Options, 'prolog_math'),
    
    % Generate each function with export wrapper
    findall(FuncCode, (
        member(func(Name, Arity, Type), Functions),
        generate_exported_function(Name, Arity, Type, FuncCode)
    ), FuncCodes),
    atomic_list_concat(FuncCodes, '\n\n', FunctionsSection),
    
    format(string(Code),
'; Generated by UnifyWeaver LLVM Target - Shared Library
; Library: lib~w.so
; Phase 2: C ABI Integration

source_filename = "~w.ll"
target datalayout = "e-m:e-p270:32:32-p271:32:32-p272:64:64-i64:64-f80:128-n8:16:32:64-S128"
target triple = "x86_64-pc-linux-gnu"

~w
', [LibName, LibName, FunctionsSection]).

%% generate_exported_function(+Name, +Arity, +Type, -Code)
generate_exported_function(Name, Arity, tail_recursion, Code) :-
    atom_string(Name, NameStr),
    format(string(Code),
'; Function: ~w/~w (tail recursion)
define internal i64 @~w_impl(i64 %%n, i64 %%acc) {
entry:
  %%cmp = icmp sle i64 %%n, 0
  br i1 %%cmp, label %%base, label %%recurse
base:
  ret i64 %%acc
recurse:
  %%n1 = sub i64 %%n, 1
  %%acc1 = add i64 %%acc, %%n
  %%result = musttail call i64 @~w_impl(i64 %%n1, i64 %%acc1)
  ret i64 %%result
}

; Exported C ABI wrapper
define dllexport i64 @~w(i64 %%n) {
entry:
  %%result = call i64 @~w_impl(i64 %%n, i64 0)
  ret i64 %%result
}
', [NameStr, Arity, NameStr, NameStr, NameStr, NameStr]).

