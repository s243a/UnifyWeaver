%% generate_wasm_string_runtime(-Code)
%  Generate LLVM IR for WASM string runtime (alloc, dealloc, memcpy).
generate_wasm_string_runtime(Code) :-
    format(string(Code),
'; WASM String Runtime - Memory Management
; Global: heap pointer for bump allocator
@heap_ptr = global i32 1024  ; Start after stack

; Allocate memory (bump allocator)
define i32 @alloc(i32 %%size) {
entry:
  %%ptr = load i32, i32* @heap_ptr
  %%new_ptr = add i32 %%ptr, %%size
  store i32 %%new_ptr, i32* @heap_ptr
  ret i32 %%ptr
}

; Deallocate (no-op in bump allocator)
define void @dealloc(i32 %%ptr, i32 %%size) {
entry:
  ret void
}

; Memory copy (for string operations)
define void @memcpy_wasm(i32 %%dest, i32 %%src, i32 %%len) {
entry:
  %%cmp = icmp sle i32 %%len, 0
  br i1 %%cmp, label %%done, label %%loop
loop:
  %%idx = phi i32 [0, %%entry], [%%next_idx, %%loop]
  %%src_ptr = add i32 %%src, %%idx
  %%dest_ptr = add i32 %%dest, %%idx
  %%byte_ptr_src = inttoptr i32 %%src_ptr to i8*
  %%byte_ptr_dest = inttoptr i32 %%dest_ptr to i8*
  %%byte = load i8, i8* %%byte_ptr_src
  store i8 %%byte, i8* %%byte_ptr_dest
  %%next_idx = add i32 %%idx, 1
  %%continue = icmp slt i32 %%next_idx, %%len
  br i1 %%continue, label %%loop, label %%done
done:
  ret void
}

; String length (find null terminator)
define i32 @strlen_wasm(i32 %%ptr) {
entry:
  br label %%loop
loop:
  %%idx = phi i32 [0, %%entry], [%%next_idx, %%continue]
  %%char_ptr = add i32 %%ptr, %%idx
  %%byte_ptr = inttoptr i32 %%char_ptr to i8*
  %%byte = load i8, i8* %%byte_ptr
  %%is_null = icmp eq i8 %%byte, 0
  br i1 %%is_null, label %%done, label %%continue
continue:
  %%next_idx = add i32 %%idx, 1
  br label %%loop
done:
  ret i32 %%idx
}
', []).

