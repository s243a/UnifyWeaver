llvm_icmp_op('>=', 'sge').
llvm_icmp_op('<=', 'sle').
llvm_icmp_op('==', 'eq').
llvm_icmp_op('!=', 'ne').

%% llvm_arith_op — map arithmetic to LLVM instruction
