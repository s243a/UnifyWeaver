%% generate_c_header(+Functions, -HeaderCode)
%  Generate C header file for the exported functions.
generate_c_header(Functions, HeaderCode) :-
    findall(Decl, (
        member(func(Name, _Arity, _Type), Functions),
        atom_string(Name, NameStr),
        format(string(Decl), 'int64_t ~w(int64_t n);', [NameStr])
    ), Decls),
    atomic_list_concat(Decls, '\n', DeclSection),
    
    format(string(HeaderCode),
'/* Generated by UnifyWeaver LLVM Target - C Header */
#ifndef PROLOG_MATH_H
#define PROLOG_MATH_H

#include <stdint.h>

#ifdef __cplusplus
extern "C" {
#endif

~w

#ifdef __cplusplus
}
#endif

#endif /* PROLOG_MATH_H */
', [DeclSection]).

%% generate_cgo_bindings(+Functions, -GoCode)
%  Generate Go code with cgo bindings.
generate_cgo_bindings(Functions, GoCode) :-
    findall(GoFunc, (
        member(func(Name, _Arity, _Type), Functions),
        atom_string(Name, NameStr),
        format(string(GoFunc),
'// ~w calls the LLVM-compiled ~w function
func ~w(n int64) int64 {
    return int64(C.~w(C.int64_t(n)))
}', [NameStr, NameStr, NameStr, NameStr])
    ), GoFuncs),
    atomic_list_concat(GoFuncs, '\n\n', GoFuncSection),
    
    format(string(GoCode),
'// Generated by UnifyWeaver LLVM Target - Go cgo bindings
package prologmath

/*
#cgo LDFLAGS: -L. -lprolog_math
#include "prolog_math.h"
*/
import "C"

~w
', [GoFuncSection]).

%% generate_rust_ffi(+Functions, -RustCode)
%  Generate Rust FFI bindings.
generate_rust_ffi(Functions, RustCode) :-
    findall(RustDecl, (
        member(func(Name, _Arity, _Type), Functions),
        atom_string(Name, NameStr),
        format(string(RustDecl), '    fn ~w(n: i64) -> i64;', [NameStr])
    ), RustDecls),
    atomic_list_concat(RustDecls, '\n', RustDeclSection),
    
    findall(RustWrapper, (
        member(func(Name, _Arity, _Type), Functions),
        atom_string(Name, NameStr),
        format(string(RustWrapper),
'/// Calls the LLVM-compiled ~w function
pub fn ~w(n: i64) -> i64 {
    unsafe { ffi::~w(n) }
}', [NameStr, NameStr, NameStr])
    ), RustWrappers),
    atomic_list_concat(RustWrappers, '\n\n', RustWrapperSection),
    
    format(string(RustCode),
'// Generated by UnifyWeaver LLVM Target - Rust FFI bindings

mod ffi {
    extern "C" {
~w
    }
}

~w
', [RustDeclSection, RustWrapperSection]).

%% build_shared_library(+LLFile, +OutputName, -Commands)
%  Generate shell commands to build shared library.
build_shared_library(LLFile, OutputName, Commands) :-
    format(string(Commands),
