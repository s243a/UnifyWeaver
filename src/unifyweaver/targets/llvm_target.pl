llvm_arith_op('/', 'sdiv').
llvm_arith_op('%', 'srem').

%% llvm_combine_conditions — AND multiple icmp conditions
llvm_combine_conditions([], _, "", 'true', 0) :- !.
llvm_combine_conditions([Cond], RegIn, Code, ResultReg, RegOut) :-
    !,
    RegOut is RegIn + 1,
    format(string(ResultReg), '%cond~w', [RegIn]),
    format(string(Code), '  ~w = ~w~n', [ResultReg, Cond]).
llvm_combine_conditions([Cond|Rest], RegIn, Code, ResultReg, RegOut) :-
    Reg1 is RegIn + 1,
    format(string(ThisReg), '%cond~w', [RegIn]),
    format(string(ThisCode), '  ~w = ~w~n', [ThisReg, Cond]),
    llvm_combine_conditions(Rest, Reg1, RestCode, RestReg, RegMid),
    RegOut is RegMid + 1,
    format(string(ResultReg), '%cond~w', [RegMid]),
    format(string(AndCode), '  ~w = and i1 ~w, ~w~n', [ResultReg, ThisReg, RestReg]),
    format(string(Code), '~w~w~w', [ThisCode, RestCode, AndCode]).

%% llvm_branches_to_blocks — generate chained basic blocks for multi-clause
llvm_branches_to_blocks(Branches, StartReg, Code) :-
    llvm_gen_blocks(Branches, 0, StartReg, Blocks),
    atomic_list_concat(Blocks, '\n', Code).

llvm_gen_blocks([], _BranchIdx, _Reg, [ErrorBlock]) :-
    !,
    format(string(ErrorBlock),
'error:
  %err_ptr = getelementptr [0 x i8], [0 x i8]* bitcast ([0 x i8]* @.str.err to [0 x i8]*), i64 0, i64 0
  call void @exit(i32 1)
  unreachable', []).

llvm_gen_blocks([branch(Conditions, ResultExpr)|Rest], BranchIdx, _Reg, [EntryBlock|RestBlocks]) :-
    NextIdx is BranchIdx + 1,
    format(string(ThenLabel), 'then~w', [BranchIdx]),
    (   Rest == []
    ->  ElseLabel = "error"
    ;   format(string(ElseLabel), 'check~w', [NextIdx])
    ),
    (   BranchIdx == 0
    ->  BlockLabel = "entry"
    ;   format(string(BlockLabel), 'check~w', [BranchIdx])
    ),
    (   Conditions == []
    ->  % Unconditional branch to then
        format(string(EntryBlock),
'~w:
~w
  ret i64 ~w', [BlockLabel, ResultExpr.setup, ResultExpr.value])
    ;   RegBase is BranchIdx * 10,
        llvm_combine_conditions(Conditions, RegBase, CondCode, CondReg, _),
        format(string(EntryBlock),
'~w:
~w  br i1 ~w, label %~w, label %~w

~w:
~w
  ret i64 ~w', [BlockLabel, CondCode, CondReg, ThenLabel, ElseLabel,
                ThenLabel, ResultExpr.setup, ResultExpr.value])
    ),
    llvm_gen_blocks(Rest, NextIdx, 0, RestBlocks).

