; Predicate: ~w (uses global visited array for cycle detection)

@.visited = internal global [1000 x i64] zeroinitializer
@.visited_count = internal global i64 0

define i1 @is_visited(i64 %val) {
entry:
  %count = load i64, i64* @.visited_count
  br label %loop

loop:
  %i = phi i64 [0, %entry], [%i_next, %not_found]
  %done = icmp sge i64 %i, %count
  br i1 %done, label %false_exit, label %check

