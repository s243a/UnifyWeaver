  %%sub = call i64 @~w(i64 %%n1)
  %%res = add i64 %%sub, %%n
  %%sptr = getelementptr [1000 x i64], [1000 x i64]* @~w_memo, i64 0, i64 %%n
  store i64 %%res, i64* %%sptr
  %%svptr = getelementptr [1000 x i1], [1000 x i1]* @~w_valid, i64 0, i64 %%n
