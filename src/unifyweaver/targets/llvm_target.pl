write_llvm_program(Code, Filename) :-
    open(Filename, write, Stream),
    write(Stream, Code),
    close(Stream),
    format('LLVM IR written to: ~w~n', [Filename]),
    format('Compile with: llc -filetype=obj ~w -o ~w.o && clang ~w.o -o ~w~n', 
        [Filename, Filename, Filename, Filename]).

