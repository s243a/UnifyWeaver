  %ptr = getelementptr [1000 x i64], [1000 x i64]* @.visited, i64 0, i64 %i
  %elem = load i64, i64* %ptr
  %eq = icmp eq i64 %elem, %val
  br i1 %eq, label %true_exit, label %not_found

not_found:
  %i_next = add i64 %i, 1
  br label %loop

true_exit:
  ret i1 1

false_exit:
  ret i1 0
}

define void @add_visited(i64 %val) {
  %count = load i64, i64* @.visited_count
  %ptr = getelementptr [1000 x i64], [1000 x i64]* @.visited, i64 0, i64 %count
  store i64 %val, i64* %ptr
  %new_count = add i64 %count, 1
  store i64 %new_count, i64* @.visited_count
  ret void
}

