%% ============================================
%% FACTS COMPILATION
%% ============================================

compile_facts_to_llvm(Pred, Arity, Code) :-
    compile_predicate_to_llvm(Pred/Arity, [], Code).

compile_predicate_to_llvm(Pred/Arity, Options, Code) :-
    functor(Head, Pred, Arity),
    findall(Head-Body, user:clause(Head, Body), Clauses),
    (   Clauses = []
    ->  format(user_error, 'LLVM target: no clauses for ~w/~w~n', [Pred, Arity]),
        fail
    ;   (   forall(member(_-Body, Clauses), Body == true)
        ->  compile_facts_llvm(Pred, Arity, Clauses, Options, Code)
        ;   compile_rule_llvm(Pred, Arity, Clauses, Options, Code)
        )
    ).

%% compile_facts_llvm(+Pred, +Arity, +Clauses, +Options, -Code)
compile_facts_llvm(Pred, Arity, Clauses, _Options, Code) :-
    atom_string(Pred, PredStr),
    length(Clauses, Count),
    
    % Generate string constants
    findall(StrConst, (
        member(Head-true, Clauses),
        Head =.. [_|Args],
        nth0(Idx, Args, Arg),
        format(string(StrConst), '@str.~w_~w = private constant [~w x i8] c"~w\\00"',
            [PredStr, Idx, length(Arg) + 1, Arg])
    ), StrConsts),
    atomic_list_concat(StrConsts, '\n', StrSection),
    
    format(string(Code),
'; Generated by UnifyWeaver LLVM Target
; Predicate: ~w/~w
; ModuleID = ''~w''
source_filename = "~w.ll"
target datalayout = "e-m:e-p270:32:32-p271:32:32-p272:64:64-i64:64-f80:128-n8:16:32:64-S128"
target triple = "x86_64-pc-linux-gnu"

; String constants
~w

; Fact count
@~w_count = constant i64 ~w

; Get count function
define i64 @get_~w_count() {
entry:
  ret i64 ~w
}
', [PredStr, Arity, PredStr, PredStr, StrSection, PredStr, Count, PredStr, Count]).

%% compile_rule_llvm(+Pred, +Arity, +Clauses, +Options, -Code)

% Try native clause body lowering first
compile_rule_llvm(Pred, Arity, Clauses, _Options, Code) :-
    native_llvm_clause_body(Pred/Arity, Clauses, FuncBody, StrConstants),
    !,
    atom_string(Pred, PredStr),
    Arity1 is Arity - 1,
    build_llvm_arg_list(Arity1, ArgList),
    build_llvm_param_list(Arity1, ParamList),
    % Calculate error string length: "No matching clause for Pred/Arity\n\0"
    format(string(ErrContent), 'No matching clause for ~w/~w', [PredStr, Arity]),
    string_length(ErrContent, ContentLen),
    ErrLen is ContentLen + 2,  % +1 for \0A, +1 for \00
    format(string(Code),
'; Generated by UnifyWeaver LLVM Target - Native Clause Lowering
; Predicate: ~w/~w

source_filename = "~w.ll"
target datalayout = "e-m:e-p270:32:32-p271:32:32-p272:64:64-i64:64-f80:128-n8:16:32:64-S128"
target triple = "x86_64-pc-linux-gnu"

~w
declare i32 @printf(i8*, ...)
declare void @exit(i32)

@.str.fmt = private constant [5 x i8] c"%ld\\0A\\00"
@.str.err = private constant [~w x i8] c"~w\\0A\\00"

define i64 @~w(~w) {
~w
}

define i32 @main(i32 %argc, i8** %argv) {
entry:
  %result = call i64 @~w(~w)
  %fmt = getelementptr [5 x i8], [5 x i8]* @.str.fmt, i64 0, i64 0
  call i32 (i8*, ...) @printf(i8* %fmt, i64 %result)
  ret i32 0
}
', [PredStr, Arity, PredStr, StrConstants,
    ErrLen, ErrContent,
    PredStr, ArgList, FuncBody,
    PredStr, ParamList]).

