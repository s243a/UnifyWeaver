:- multifile advanced_recursive_compiler:compile_general_recursive_pattern/6.

advanced_recursive_compiler:compile_general_recursive_pattern(krakatau, PredStr, Arity, BaseClauses, _RecClauses, Code) :-
    Arity =:= 2,
    (   BaseClauses = [(BaseHead, true)|_]
    ->  BaseHead =.. [_, BaseKey, BaseVal],
        (integer(BaseKey) -> format(string(BaseCheck), '    iload_0\n    ldc ~w\n    if_icmpne L_recurse', [BaseKey])
        ;   BaseCheck = '    ; no base match')
    ;   BaseVal = 0, BaseCheck = '    ; no base case'
    ),
    format(string(ClassName), 'Prolog_~w', [PredStr]),
    format(string(Code),
'; Generated by UnifyWeaver Krakatau Target - General Recursion with Visited Set
; Predicate: ~w/~w

.version 52 0
.class public ~w
.super java/lang/Object

.method public static ~w : (I)I
    .code stack 4 locals 2
    new java/util/HashSet
    dup
    invokespecial java/util/HashSet <init> ()V
    astore_1
    iload_0
    aload_1
    invokestatic ~w ~w_worker (ILjava/util/Set;)I
    ireturn
    .end code
.end method

.method public static ~w_worker : (ILjava/util/Set;)I
    .code stack 4 locals 3
    ; Cycle check
    iload_0
    invokestatic java/lang/Integer valueOf (I)Ljava/lang/Integer;
    aload_1
    invokeinterface java/util/Set contains (Ljava/lang/Object;)Z 2
    ifne L_cycle
    ; Add to visited
    iload_0
    invokestatic java/lang/Integer valueOf (I)Ljava/lang/Integer;
    aload_1
    invokeinterface java/util/Set add (Ljava/lang/Object;)Z 2
    pop
    ; Base case
~w
    ldc ~w
    ireturn
L_recurse:
    ; General recursive case (placeholder)
    ldc 0
    ireturn
L_cycle:
    ldc -1
    ireturn
    .end code
.end method

.end class
', [PredStr, Arity, ClassName, PredStr, ClassName, PredStr, PredStr, BaseCheck, BaseVal]).
