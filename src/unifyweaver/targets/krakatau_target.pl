%% PIPELINE MODE
%% ============================================

compile_krakatau_pipeline(Steps, Options, Code) :-
    (   option(class_name(ClassName), Options)
    ->  true
    ;   ClassName = "Pipeline"
    ),
    maplist(krakatau_step_method, Steps, StepMethods),
    atomic_list_concat(StepMethods, '\n', StepCode),
    maplist(krakatau_step_call, Steps, StepCalls),
    atomic_list_concat(StepCalls, '\n', OrchCode),
    format(string(Code),
'; Generated by UnifyWeaver Krakatau Target - Pipeline

.version 50 0
.class public ~w
.super java/lang/Object

~w
.method public static main : ([Ljava/lang/String;)V
    .limit stack 10
    .limit locals 10

    ; Execute steps sequentially
~w
    return
.end method
', [ClassName, StepCode, OrchCode]).

krakatau_step_method(step(Name, Body), Code) :-
    atom_string(Name, NameStr),
    format(string(Code),
'.method public static step_~w : (I)I
    .limit stack 10
    .limit locals 10
~w
.end method
', [NameStr, Body]).

krakatau_step_call(step(Name, _Body), Call) :-
    atom_string(Name, NameStr),
    format(string(Call),
'    ; Step: ~w
    ; invokestatic ~w step_~w (I)I', [NameStr, NameStr, NameStr]).

%% ============================================
%% BUILD FILE GENERATION
%% ============================================

generate_krakatau_build_script(Options, Script) :-
    (   option(source_file(SrcFile), Options)
    ->  true
    ;   SrcFile = "Program.j"
    ),
    (   option(main_class(MainClass), Options)
    ->  true
    ;   MainClass = "Program"
    ),
    (   option(output_jar(JarFile), Options)
    ->  true
    ;   JarFile = "output.jar"
    ),
    format(string(Script),
'#!/bin/bash
# Generated by UnifyWeaver Krakatau Target - Build Script
# Assembles Krakatau source and packages into JAR

KRAK2="${KRAK2:-krak2}"

echo "Assembling ~w..."
$KRAK2 asm --out . ~w

echo "Packaging into ~w..."
echo "Main-Class: ~w" > MANIFEST.MF
echo "Created-By: UnifyWeaver Krakatau Target" >> MANIFEST.MF
jar cfm ~w MANIFEST.MF *.class
rm -f MANIFEST.MF

echo "Done. Run with: java -jar ~w"
', [SrcFile, SrcFile, JarFile, MainClass, JarFile, JarFile]).

