%% Tail recursion
tail_recursion:compile_tail_pattern(krakatau, PredStr, Arity, _BaseClauses, _RecClauses, _AccPos, _StepOp, _ExitAfterResult, Code) :-
    atom_string(Pred, PredStr),
    compile_tail_recursion_krakatau(Pred/Arity, [], Code).

compile_tail_recursion_krakatau(Pred/Arity, _Options, Code) :-
    atom_string(Pred, PredStr),
    jvm_tail_recursion_bytecode(PredStr, Arity, numeric, BodyInstrs),
    jvm_entry_method_bytecode(PredStr, Arity, numeric, EntryInstrs),
    format(string(ClassName), 'Prolog_~w', [PredStr]),
    krakatau_wrap_recursion(ClassName, PredStr, "(II)I", BodyInstrs,
        "(I)I", EntryInstrs, Code).

%% Linear recursion
linear_recursion:compile_linear_pattern(krakatau, PredStr, Arity, _BaseClauses, _RecClauses, _MemoEnabled, _MemoStrategy, Code) :-
    atom_string(Pred, PredStr),
    compile_linear_recursion_krakatau(Pred/Arity, [], Code).

compile_linear_recursion_krakatau(Pred/Arity, _Options, Code) :-
    atom_string(Pred, PredStr),
    jvm_linear_recursion_bytecode(PredStr, Arity, numeric, BodyInstrs),
    krakatau_wrap_single(PredStr, "(I)I", BodyInstrs, Code).

%% Tree recursion
tree_recursion:compile_tree_pattern(krakatau, PredStr, Arity, _BaseClauses, _RecClauses, Code) :-
    atom_string(Pred, PredStr),
    compile_tree_recursion_krakatau(Pred/Arity, [], Code).

compile_tree_recursion_krakatau(Pred/Arity, _Options, Code) :-
    atom_string(Pred, PredStr),
    jvm_tree_recursion_bytecode(PredStr, Arity, numeric, BodyInstrs),
    krakatau_wrap_single(PredStr, "(I)I", BodyInstrs, Code).

%% Multicall recursion
multicall_linear_recursion:compile_multicall_pattern(krakatau, PredStr, BaseClauses, _RecClauses, _MemoEnabled, Code) :-
    atom_string(Pred, PredStr),
    length(BaseClauses, _),
    compile_multicall_recursion_krakatau(Pred/2, [], Code).

compile_multicall_recursion_krakatau(Pred/Arity, _Options, Code) :-
    atom_string(Pred, PredStr),
    jvm_multicall_recursion_bytecode(PredStr, Arity, numeric, BodyInstrs),
    krakatau_wrap_single(PredStr, "(I)I", BodyInstrs, Code).

%% Direct multicall recursion
direct_multi_call_recursion:compile_direct_multicall_pattern(krakatau, PredStr, _BaseClauses, _RecClause, Code) :-
    atom_string(Pred, PredStr),
    compile_direct_multicall_krakatau(Pred/2, [], Code).

compile_direct_multicall_krakatau(Pred/Arity, _Options, Code) :-
    atom_string(Pred, PredStr),
    jvm_direct_multicall_bytecode(PredStr, Arity, numeric, BodyInstrs),
    krakatau_wrap_single(PredStr, "(I)I", BodyInstrs, Code).

%% Mutual recursion
mutual_recursion:compile_mutual_pattern(krakatau, Predicates, _MemoEnabled, _MemoStrategy, Code) :-
    compile_mutual_recursion_krakatau(Predicates, [], Code).

compile_mutual_recursion_krakatau(Predicates, _Options, Code) :-
    (   Predicates = [Pred1|_]
    ->  atom_string(Pred1, P1Str),
        format(string(ClassName), 'Prolog_~w', [P1Str])
    ;   ClassName = "Prolog_mutual"
    ),
    jvm_mutual_recursion_bytecode(Predicates, ClassName, numeric, Methods),
    maplist(krakatau_mutual_method, Methods, MethodCodes),
    atomic_list_concat(MethodCodes, '\n', MethodSection),
    format(string(Code),
'; Generated by UnifyWeaver Krakatau Target - Mutual Recursion

.version 52 0
.class public ~w
.super java/lang/Object

~w
', [ClassName, MethodSection]).

krakatau_mutual_method(method(Name, Instrs), Code) :-
    maplist(ensure_string, Instrs, StrInstrs),
    atomic_list_concat(StrInstrs, '\n', Body),
    format(string(Code),
'.method public static ~w : (I)I
    .limit stack 10
    .limit locals 10

~w
.end method
', [Name, Body]).

