%% native_scala_clause_body(+PredSpec, +Clauses, -Code)

% Single clause
native_scala_clause_body(PredSpec, [Head-Body], Code) :-
    native_scala_clause(PredSpec, Head, Body, Condition, ClauseCode),
    !,
    (   Condition == "true"
    ->  format(string(Code), '    ~w', [ClauseCode])
    ;   format(string(Code),
'    if (~w) ~w
    else throw new RuntimeException("No matching clause for ~w")', [Condition, ClauseCode, PredSpec])
    ).

% Multi-clause → if/else if/else
native_scala_clause_body(PredSpec, Clauses, Code) :-
    Clauses = [_|[_|_]],
    maplist(native_scala_clause_pair(PredSpec), Clauses, Branches),
    Branches \= [],
    branches_to_scala_if_chain(Branches, PredSpec, Code).

native_scala_clause_pair(PredSpec, Head-Body, branch(Condition, ClauseCode)) :-
    native_scala_clause(PredSpec, Head, Body, Condition, ClauseCode),
    !.

%% native_scala_clause(+PredSpec, +Head, +Body, -Condition, -Code)
native_scala_clause(_PredSpec, Head, Body, Condition, Code) :-
    Head =.. [_Pred|HeadArgs],
    length(HeadArgs, Arity),
    build_head_varmap(HeadArgs, 1, VarMap),
    (   Arity > 1
    ->  append(_InputHeadArgs, [OutputHeadArg], HeadArgs),
        scala_head_conditions(HeadArgs, 1, Arity, HeadConditions)
    ;   OutputHeadArg = _,
        scala_head_conditions(HeadArgs, 1, Arity, HeadConditions)
    ),
    normalize_goals(Body, Goals),
    (   Goals == []
    ->  scala_resolve_value(VarMap, OutputHeadArg, Code),
        GoalConditions = []
    ;   (   Arity > 1, nonvar(OutputHeadArg)
        ->  clause_guard_output_split(Goals, VarMap, GuardGoals, OutputGoals),
            maplist(scala_guard_condition(VarMap), GuardGoals, GoalConditions),
            (   OutputGoals == []
            ->  scala_literal(OutputHeadArg, Code)
            ;   scala_output_goals(OutputGoals, VarMap, Code)
            )
        ;   native_scala_goal_sequence(Goals, VarMap, GoalConditions, Code)
        )
    ),
    append(HeadConditions, GoalConditions, AllConditions),
    combine_scala_conditions(AllConditions, Condition).

%% scala_head_conditions(+HeadArgs, +Index, +Arity, -Conditions)
scala_head_conditions([], _, _, []).
scala_head_conditions([_], _, Arity, []) :- Arity > 1, !.
scala_head_conditions([HeadArg|Rest], Index, Arity, Conditions) :-
    (   var(HeadArg)
    ->  Conditions = RestConditions
    ;   format(string(ArgName), 'arg~w', [Index]),
        scala_literal(HeadArg, Literal),
        format(string(Cond), '~w == ~w', [ArgName, Literal]),
        Conditions = [Cond|RestConditions]
    ),
    NextIndex is Index + 1,
    scala_head_conditions(Rest, NextIndex, Arity, RestConditions).

%% native_scala_goal_sequence(+Goals, +VarMap, -Conditions, -Code)
%  Uses classify_goal_sequence for advanced pattern detection.
%  Falls back to clause_guard_output_split if classification fails.
native_scala_goal_sequence(Goals, VarMap, Conditions, Code) :-
    classify_goal_sequence(Goals, VarMap, ClassifiedGoals),
    ClassifiedGoals \= [],
