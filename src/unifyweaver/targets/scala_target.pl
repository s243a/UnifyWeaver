    ;   Goal = (Var is ArithExpr), var(Var)
    ->  ensure_var(VarMap0, Var, VarName, VarMapOut),
        scala_expr(ArithExpr, VarMap0, ExprStr),
        format(string(Line), '    val ~w = ~w', [VarName, ExprStr])
    ;   VarMapOut = VarMap0,
        Line = '    // unsupported'
    ).

