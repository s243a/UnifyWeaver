mutual_recursion:compile_mutual_pattern(scala, Predicates, MemoEnabled, _MemoStrategy, ScalaCode) :-
    % Generate functions for each predicate
    mutual_functions_scala(Predicates, Predicates, MemoEnabled, FuncCodes),
    atomic_list_concat(FuncCodes, '\n\n', FunctionsCode),
    % Generate dispatch in main
    mutual_dispatch_scala(Predicates, DispatchCode),
    format(string(ScalaCode),
'// Generated by UnifyWeaver Scala Target - Mutual Recursion (multifile dispatch)

object Main {
~w

~w
}
', [FunctionsCode, DispatchCode]).

mutual_functions_scala([], _AllPreds, _MemoEnabled, []).
mutual_functions_scala([Pred/Arity|Rest], AllPreds, MemoEnabled, [FuncCode|RestCodes]) :-
    atom_string(Pred, PredStr),
    functor(Head, Pred, Arity),
    findall(clause(Head, Body), user:clause(Head, Body), Clauses),
    partition(mutual_recursion:is_mutual_recursive_clause(AllPreds), Clauses, RecClauses, BaseClauses),
    % Base cases
    findall(BaseLine, (
        member(clause(BHead, true), BaseClauses),
        BHead =.. [_P, BValue],
        format(string(BaseLine), '      case ~w => true', [BValue])
    ), BaseLines),
    atomic_list_concat(BaseLines, '\n', BaseCode),
    % Recursive case
    (   RecClauses = [clause(_RHead, RBody)|_] ->
        extract_mutual_rec_info_scala(RBody, Guard, CalledPred, Step),
        atom_string(CalledPred, CalledStr),
        (   Guard = (N > Threshold), var(N) ->
            format(string(RecCode),
'      case _ if n > ~w => ~w(n ~w)', [Threshold, CalledStr, Step])
        ;   format(string(RecCode),
'      case _ => ~w(n ~w)', [CalledStr, Step])
        )
    ;   RecCode = '      case _ => false'
    ),
    format(string(FuncCode),
'  def ~w(n: Int): Boolean = n match {
~w
~w
  }', [PredStr, BaseCode, RecCode]),
    mutual_functions_scala(Rest, AllPreds, MemoEnabled, RestCodes).

extract_mutual_rec_info_scala(Body, Guard, CalledPred, Step) :-
    extract_goals_from_body_scala(Body, Goals),
    (   member(Guard, Goals), Guard = (_ > _) -> true
    ;   Guard = none
    ),
    member(Call, Goals),
    compound(Call),
    Call \= (_ is _), Call \= (_ > _), Call \= (_ < _),
    Call \= (_ >= _), Call \= (_ =< _),
    functor(Call, CalledPred, _),
    (   member(_ is _ - K, Goals), integer(K) ->
        format(string(Step), '- ~w', [K])
    ;   member(_ is _ + K, Goals), integer(K), K < 0 ->
        AbsK is abs(K),
        format(string(Step), '- ~w', [AbsK])
    ;   Step = "- 1"
    ).

extract_goals_from_body_scala((A, B), Goals) :- !,
    extract_goals_from_body_scala(A, GA),
    extract_goals_from_body_scala(B, GB),
    append(GA, GB, Goals).
extract_goals_from_body_scala(true, []) :- !.
extract_goals_from_body_scala(Goal, [Goal]).

mutual_dispatch_scala(Predicates, Code) :-
    findall(CaseLine, (
        member(Pred/_Arity, Predicates),
        atom_string(Pred, PredStr),
        format(string(CaseLine),
'        case "~w" => println(~w(n))', [PredStr, PredStr])
    ), Lines),
