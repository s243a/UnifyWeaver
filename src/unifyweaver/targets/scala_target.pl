%% branches_to_scala_if_chain(+Branches, +PredSpec, -Code)
branches_to_scala_if_chain(Branches, PredSpec, Code) :-
    scala_if_chain_lines(Branches, PredSpec, true, Lines),
    atomic_list_concat(Lines, '\n', Code).

scala_if_chain_lines([branch(Cond, ClauseCode)], PredSpec, IsFirst, Lines) :-
    !,
    (   Cond == "true"
    ->  (   IsFirst == true
        ->  format(string(Line), '    ~w', [ClauseCode]),
            Lines = [Line]
        ;   format(string(Line), '    else ~w', [ClauseCode]),
            Lines = [Line]
        )
    ;   (   IsFirst == true
        ->  format(string(L1), '    if (~w) ~w', [Cond, ClauseCode]),
            format(string(L2), '    else throw new RuntimeException("No matching clause for ~w")', [PredSpec]),
            Lines = [L1, L2]
        ;   format(string(L1), '    else if (~w) ~w', [Cond, ClauseCode]),
            format(string(L2), '    else throw new RuntimeException("No matching clause for ~w")', [PredSpec]),
            Lines = [L1, L2]
        )
    ).
scala_if_chain_lines([branch(Cond, ClauseCode)|Rest], PredSpec, IsFirst, [Line|RestLines]) :-
    (   IsFirst == true
    ->  format(string(Line), '    if (~w) ~w', [Cond, ClauseCode])
    ;   format(string(Line), '    else if (~w) ~w', [Cond, ClauseCode])
    ),
    scala_if_chain_lines(Rest, PredSpec, false, RestLines).
