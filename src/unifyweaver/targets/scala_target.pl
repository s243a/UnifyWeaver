linear_fold_scala(PredStr, BaseClauses, MemoEnabled, Code) :-
    linear_recursion:extract_base_case_info(BaseClauses, BaseInput, BaseOutput),
    linear_recursion:detect_input_type(BaseInput, InputType),
    % Extract fold
    atom_string(Pred, PredStr),
    functor(Head, Pred, 2),
    findall(clause(Head, Body), user:clause(Head, Body), AllClauses),
    partition(linear_recursion:is_recursive_clause(Pred), AllClauses, ActualRec, _),
    (   ActualRec = [clause(RHead, RBody)|_] ->
        RHead =.. [_, InputVar, _],
        linear_recursion:find_recursive_call(RBody, RecCall),
        RecCall =.. [_, _, AccVar],
        linear_recursion:find_last_is_expression(RBody, _ is FoldExpr),
        translate_fold_expr_scala(FoldExpr, InputVar, AccVar, ScalaOp)
    ;   ScalaOp = "current * acc"
    ),
    (   InputType = numeric ->
        (   MemoEnabled = true ->
            MemoDecl = "  private val memo = scala.collection.mutable.Map[Int, Int]()",
            MemoCheck = "    memo.getOrElseUpdate(n, {",
            MemoStore = "    })"
        ;   MemoDecl = "  // Memoization disabled",
            MemoCheck = "    {",
            MemoStore = "    }"
        ),
        format(string(Code),
'// Generated by UnifyWeaver Scala Target - Linear Recursion (numeric, multifile dispatch)
// Predicate: ~w/2

object Main {
~w

  def ~w(n: Int): Int =
~w
      if (n == ~w) ~w
      else (n to 1 by -1).foldLeft(~w)((acc, current) => ~w)
~w

  def main(args: Array[String]): Unit = {
    if (args.length >= 1) println(~w(args(0).toInt))
  }
}
', [PredStr, MemoDecl, PredStr, MemoCheck, BaseInput, BaseOutput, BaseOutput, ScalaOp, MemoStore, PredStr])
    ;   InputType = list ->
        (   MemoEnabled = true ->
            MemoDecl = "  private val memo = scala.collection.mutable.Map[List[Int], Int]()",
            MemoCheck = "    memo.getOrElseUpdate(lst, {",
            MemoStore = "    })"
        ;   MemoDecl = "  // Memoization disabled",
            MemoCheck = "    {",
            MemoStore = "    }"
        ),
        format(string(Code),
'// Generated by UnifyWeaver Scala Target - Linear Recursion (list, multifile dispatch)
// Predicate: ~w/2

object Main {
~w

  def ~w(lst: List[Int]): Int =
~w
      if (lst.isEmpty) ~w
      else lst.foldLeft(~w)((acc, current) => ~w)
~w

  def main(args: Array[String]): Unit = {
    if (args.length >= 1) println(~w(args(0).split(",").map(_.toInt).toList))
  }
