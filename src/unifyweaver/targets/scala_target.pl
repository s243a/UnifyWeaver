% Try native clause body lowering first
compile_simple_mode_scala(Pred, Arity, _Options, ScalaCode) :-
    functor(Head, Pred, Arity),
    findall(Head-Body, user:clause(Head, Body), Clauses),
    Clauses \= [],
    \+ (member(_-Body, Clauses), Body == true),
    native_scala_clause_body(Pred/Arity, Clauses, FuncBody),
    !,
    atom_string(Pred, PredStr),
    Arity1 is Arity - 1,
    build_scala_arg_list(Arity1, ArgList),
    format(string(ScalaCode),
'// Generated by UnifyWeaver Scala Target - Native Clause Lowering
// Predicate: ~w/~w

object Main {
  def ~w(~w): String = {
~w
  }

  def main(args: Array[String]): Unit = {
    if (args.nonEmpty) println(~w(args(0).toLong))
  }
}
', [PredStr, Arity, PredStr, ArgList, FuncBody, PredStr]).

