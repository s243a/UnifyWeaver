%% scala_expr — convert Prolog expression to Scala syntax
scala_expr(Var, VarMap, SExpr) :-
    var(Var), !,
    (   lookup_var(Var, VarMap, Name)
    ->  atom_string(Name, SExpr)
    ;   SExpr = "0"
    ).
scala_expr(Expr, VarMap, SExpr) :-
    compound(Expr),
    Expr =.. [Op, Left, Right],
    expr_op(Op, StdOp),
    !,
    scala_expr(Left, VarMap, SLeft),
    scala_expr(Right, VarMap, SRight),
    scala_op(StdOp, SOp),
    format(string(SExpr), '(~w ~w ~w)', [SLeft, SOp, SRight]).
scala_expr(-Expr, VarMap, SExpr) :-
    !,
    scala_expr(Expr, VarMap, Inner),
    format(string(SExpr), '(-~w)', [Inner]).
scala_expr(abs(Expr), VarMap, SExpr) :-
    !,
    scala_expr(Expr, VarMap, Inner),
    format(string(SExpr), 'Math.abs(~w)', [Inner]).
scala_expr(Atom, _VarMap, SExpr) :-
    atom(Atom), !,
    scala_literal(Atom, SExpr).
scala_expr(Number, _VarMap, SExpr) :-
    number(Number), !,
    format(string(SExpr), '~w', [Number]).

%% scala_literal — convert Prolog value to Scala literal
scala_literal(Value, "\"\"") :- var(Value), !.
scala_literal(Value, SLit) :-
    number(Value), !,
    format(string(SLit), '~w', [Value]).
scala_literal(Value, SLit) :-
    atom(Value), !,
    format(string(SLit), '"~w"', [Value]).
scala_literal(Value, SLit) :-
    format(string(SLit), '"~w"', [Value]).

%% scala_resolve_value — resolve head arg to Scala expression
