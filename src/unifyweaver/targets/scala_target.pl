    ;   CondExpr = 'true'
    ),
    scala_branch_value(Alt, VarMap, ValExpr),
    format(string(IfLine), '    if (~w) {', [CondExpr]),
    format(string(RetLine), '        ~w', [ValExpr]),
    scala_disj_elif_chain(Rest, VarMap, RestLines).

%% scala_disj_elif_chain(+Alternatives, +VarMap, -Lines)
scala_disj_elif_chain([], _, []).
scala_disj_elif_chain([Alt], VarMap, [ElseLine, RetLine, CloseLine]) :-
    !,
    scala_branch_value(Alt, VarMap, ValExpr),
    ElseLine = '    } else {',
    format(string(RetLine), '        ~w', [ValExpr]),
    CloseLine = '    }'.
scala_disj_elif_chain([Alt|Rest], VarMap, [ElifLine, RetLine|RestLines]) :-
    normalize_goals(Alt, Goals),
    clause_guard_output_split(Goals, VarMap, Guards, _Outputs),
    (   Guards \= []
    ->  maplist(scala_guard_condition(VarMap), Guards, CondStrs),
        atomic_list_concat(CondStrs, ' && ', CondExpr)
    ;   CondExpr = 'true'
    ),
    scala_branch_value(Alt, VarMap, ValExpr),
    format(string(ElifLine), '    } else if (~w) {', [CondExpr]),
    format(string(RetLine), '        ~w', [ValExpr]),
    scala_disj_elif_chain(Rest, VarMap, RestLines).

%% scala_guard_condition(+VarMap, +Goal, -Condition)
scala_guard_condition(VarMap, _Module:Goal, Condition) :-
    !, scala_guard_condition(VarMap, Goal, Condition).
scala_guard_condition(VarMap, Goal, Condition) :-
    compound(Goal),
    Goal =.. [Op, Left, Right],
    expr_op(Op, StdOp),
    !,
    scala_expr(Left, VarMap, SLeft),
    scala_expr(Right, VarMap, SRight),
    scala_op(StdOp, SOp),
    format(string(Condition), '~w ~w ~w', [SLeft, SOp, SRight]).

%% scala_output_goals(+Goals, +VarMap, -Code)
scala_output_goals([], _VarMap, '"error"') :- !.
scala_output_goals([Goal], VarMap, Code) :-
    !, scala_output_goal_last(Goal, VarMap, Code).
scala_output_goals([_Goal|Rest], VarMap, Code) :-
    scala_output_goals(Rest, VarMap, Code).

%% scala_output_goal_last — produce the return expression
scala_output_goal_last(_Module:Goal, VarMap, Code) :-
    !, scala_output_goal_last(Goal, VarMap, Code).
scala_output_goal_last(Goal, VarMap, Code) :-
    if_then_else_goal(Goal, IfGoal, ThenGoal, ElseGoal),
    !,
    scala_if_then_else_output(IfGoal, ThenGoal, ElseGoal, VarMap, Code).
scala_output_goal_last(=(Var, Expr), VarMap, Code) :-
    var(Var), !,
    scala_expr(Expr, VarMap, Code).
scala_output_goal_last(is(Var, Expr), VarMap, Code) :-
    var(Var), !,
    scala_expr(Expr, VarMap, Code).

%% scala_if_then_else_output — generate if/else expressions
scala_if_then_else_output(IfGoal, ThenGoal, ElseGoal, VarMap, Code) :-
    flatten_scala_if_branches(IfGoal, ThenGoal, ElseGoal, Branches, DefaultGoal),
    scala_branches_to_if_expr(Branches, DefaultGoal, VarMap, Code).

flatten_scala_if_branches(If, Then, Else, [branch(If, Then)|RestBranches], Default) :-
    if_then_else_goal(Else, If2, Then2, Else2),
    !,
    flatten_scala_if_branches(If2, Then2, Else2, RestBranches, Default).
flatten_scala_if_branches(If, Then, Else, [branch(If, Then)], Else).

scala_branches_to_if_expr([branch(If, Then)], DefaultGoal, VarMap, Code) :-
    !,
    scala_guard_condition(VarMap, If, IfCond),
    scala_branch_value(Then, VarMap, ThenVal),
    scala_branch_value(DefaultGoal, VarMap, ElseVal),
    format(string(Code), 'if (~w) ~w else ~w', [IfCond, ThenVal, ElseVal]).
scala_branches_to_if_expr([branch(If, Then)|Rest], DefaultGoal, VarMap, Code) :-
    scala_guard_condition(VarMap, If, IfCond),
    scala_branch_value(Then, VarMap, ThenVal),
    scala_branches_to_if_expr(Rest, DefaultGoal, VarMap, ElseCode),
    format(string(Code), 'if (~w) ~w else ~w', [IfCond, ThenVal, ElseCode]).

%% scala_branch_value — extract result value from a branch
scala_branch_value(_Module:Goal, VarMap, Value) :-
    !, scala_branch_value(Goal, VarMap, Value).
scala_branch_value(Goal, VarMap, Value) :-
    if_then_else_goal(Goal, If, Then, Else),
    !,
    scala_guard_condition(VarMap, If, Cond),
    scala_branch_value(Then, VarMap, ThenVal),
    scala_branch_value(Else, VarMap, ElseVal),
    format(string(Value), 'if (~w) ~w else ~w', [Cond, ThenVal, ElseVal]).
scala_branch_value((A, B), VarMap, Value) :-
    !,
    normalize_goals((A, B), Goals),
    last(Goals, LastGoal),
    scala_branch_value(LastGoal, VarMap, Value).
scala_branch_value(=(_, Expr), VarMap, Value) :-
    !, scala_expr(Expr, VarMap, Value).
scala_branch_value(is(_, Expr), VarMap, Value) :-
    !, scala_expr(Expr, VarMap, Value).
scala_branch_value(Goal, VarMap, Value) :-
    scala_expr(Goal, VarMap, Value).

