%% scala_classified_output_goal(+Goal, +VarMap0, -Line, -VarMapOut)
scala_classified_output_goal(Goal, VarMap0, Line, VarMapOut) :-
    (   Goal = (Var = Expr), var(Var)
    ->  ensure_var(VarMap0, Var, VarName, VarMapOut),
        scala_expr(Expr, VarMap0, ExprStr),
