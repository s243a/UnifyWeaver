tail_recursion:compile_tail_pattern(scala, PredStr, Arity, _BaseClauses, _RecClauses, _AccPos, StepOp, _ExitAfterResult, Code) :-
    step_op_to_scala(StepOp, ScalaStepExpr),
    (   Arity =:= 3 ->
        format(string(Code),
'// Generated by UnifyWeaver Scala Target - Tail Recursion (multifile dispatch)
// Predicate: ~w/~w

import scala.annotation.tailrec

object Main {
