#include <stdlib.h>

~w

int ~w(int n) {
~w
    if (n == ~w) return ~w;
    int result = ~w;
    for (int current = n; current >= 1; current--) {
        result = ~w;
    }
~w
    return result;
}

int main(int argc, char* argv[]) {
    if (argc >= 2) printf("%d\\n", ~w(atoi(argv[1])));
    return 0;
}
', [PredStr, MemoDecl, PredStr, MemoCheck, BaseInput, BaseOutput, BaseOutput, COp, MemoStore, PredStr])
    ;   InputType = list ->
        % Re-extract fold with head variable for list patterns
        (   ActualRec = [clause(LRHead, LRBody)|_] ->
            linear_recursion:find_last_is_expression(LRBody, _ is LFoldExpr),
            linear_recursion:find_recursive_call(LRBody, LRecCall),
