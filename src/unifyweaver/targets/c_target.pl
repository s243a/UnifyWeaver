translate_list_fold_c(A - B, HV, AV, E) :- translate_list_term_c(A, HV, AV, AT), translate_list_term_c(B, HV, AV, BT), format(string(E), '~w - ~w', [AT, BT]).
translate_list_fold_c(T, HV, AV, E) :- translate_list_term_c(T, HV, AV, E).

translate_list_term_c(T, HV, _, 'current') :- T == HV, !.
translate_list_term_c(T, _, AV, 'result') :- T == AV, !.
translate_list_term_c(N, _, _, S) :- integer(N), !, format(string(S), '~w', [N]).
translate_list_term_c(A, _, _, S) :- format(string(S), '~w', [A]).

