    ElseLine = '    } else {',
    format(string(ElseRetLine), '        return ~w;', [ElseExpr]),
    CloseLine = '    }',
    Lines = [IfLine, ThenLine, ElseLine, ElseRetLine, CloseLine].
c_render_classified_last(output_disj(Alternatives, _SharedVars), VarMap, [], Lines) :-
