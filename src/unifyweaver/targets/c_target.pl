translate_fold_expr_c(A + B, IV, AV, E) :- translate_c_term(A, IV, AV, AT), translate_c_term(B, IV, AV, BT), format(string(E), '~w + ~w', [AT, BT]).
translate_fold_expr_c(A - B, IV, AV, E) :- translate_c_term(A, IV, AV, AT), translate_c_term(B, IV, AV, BT), format(string(E), '~w - ~w', [AT, BT]).
translate_fold_expr_c(T, IV, AV, E) :- translate_c_term(T, IV, AV, E).

translate_c_term(T, IV, _, 'current') :- T == IV, !.
translate_c_term(T, _, AV, 'result') :- T == AV, !.
translate_c_term(N, _, _, S) :- integer(N), !, format(string(S), '~w', [N]).
translate_c_term(A, _, _, S) :- format(string(S), '~w', [A]).

%% translate_list_fold_c(+PrologExpr, +HeadVar, +AccVar, -CExpr)
