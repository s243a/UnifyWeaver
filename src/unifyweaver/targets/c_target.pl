mutual_recursion:compile_mutual_pattern(c, Predicates, MemoEnabled, _MemoStrategy, CCode) :-
    length(Predicates, NumPreds),
    MemoSize is NumPreds * 1000,
    % Forward declarations
    findall(FwdDecl, (
        member(Pred/_Arity, Predicates),
        atom_string(Pred, PredStr),
        format(string(FwdDecl), 'int ~w(int n);', [PredStr])
    ), FwdDecls),
    atomic_list_concat(FwdDecls, '\n', ForwardDecls),
    % Generate functions
    mutual_functions_c(Predicates, Predicates, MemoEnabled, 0, FuncCodes),
    atomic_list_concat(FuncCodes, '\n\n', FunctionsCode),
    % Dispatch
    mutual_dispatch_c(Predicates, DispatchCode),
    format(string(CCode),
'/* Generated by UnifyWeaver C Target - Mutual Recursion (multifile dispatch) */
#include <stdio.h>
#include <stdlib.h>
#include <string.h>

static int memo[~w];
static int memo_set[~w];

~w

~w

int main(int argc, char *argv[]) {
    memset(memo_set, 0, sizeof(memo_set));
~w
    return 0;
}
', [MemoSize, MemoSize, ForwardDecls, FunctionsCode, DispatchCode]).

mutual_functions_c([], _AllPreds, _MemoEnabled, _Idx, []).
mutual_functions_c([Pred/Arity|Rest], AllPreds, MemoEnabled, Idx, [FuncCode|RestCodes]) :-
    atom_string(Pred, PredStr),
    functor(Head, Pred, Arity),
    findall(clause(Head, Body), user:clause(Head, Body), Clauses),
    partition(mutual_recursion:is_mutual_recursive_clause(AllPreds), Clauses, RecClauses, BaseClauses),
    mutual_base_cases_c(BaseClauses, MemoEnabled, Idx, BaseParts),
    atomic_list_concat(BaseParts, '\n', BaseCode),
    mutual_rec_cases_c(RecClauses, MemoEnabled, Idx, RecParts),
    atomic_list_concat(RecParts, '\n', RecCode),
    (   MemoEnabled = true ->
        format(string(FuncCode),
'int ~w(int n) {
    int idx = ~w * 1000 + n;
    if (n >= 0 && n < 1000 && memo_set[idx]) return memo[idx];
~w
~w
    return 0;
}', [PredStr, Idx, BaseCode, RecCode])
    ;   format(string(FuncCode),
'int ~w(int n) {
~w
~w
    return 0;
}', [PredStr, BaseCode, RecCode])
    ),
    NextIdx is Idx + 1,
    mutual_functions_c(Rest, AllPreds, MemoEnabled, NextIdx, RestCodes).

mutual_base_cases_c([], _MemoEnabled, _Idx, []).
mutual_base_cases_c([clause(Head, true)|Rest], MemoEnabled, Idx, [Part|RestParts]) :-
    Head =.. [_Pred, Value],
    (   MemoEnabled = true ->
        format(string(Part),
'    if (n == ~w) { if (n >= 0 && n < 1000) { memo[~w * 1000 + n] = 1; memo_set[~w * 1000 + n] = 1; } return 1; }',
            [Value, Idx, Idx])
    ;   format(string(Part), '    if (n == ~w) return 1;', [Value])
    ),
    mutual_base_cases_c(Rest, MemoEnabled, Idx, RestParts).
mutual_base_cases_c([_|Rest], MemoEnabled, Idx, RestParts) :-
    mutual_base_cases_c(Rest, MemoEnabled, Idx, RestParts).

mutual_rec_cases_c([], _MemoEnabled, _Idx, []).
mutual_rec_cases_c([clause(_Head, Body)|Rest], MemoEnabled, Idx, [Part|RestParts]) :-
    extract_mutual_rec_info_c(Body, Guard, CalledPred, Step),
    atom_string(CalledPred, CalledStr),
    (   Guard = (N > Threshold), var(N) ->
        (   MemoEnabled = true ->
            format(string(Part),
'    if (n > ~w) {
        int result = ~w(n ~w);
        if (n >= 0 && n < 1000) { memo[~w * 1000 + n] = result; memo_set[~w * 1000 + n] = 1; }
        return result;
    }', [Threshold, CalledStr, Step, Idx, Idx])
        ;   format(string(Part),
'    if (n > ~w) return ~w(n ~w);', [Threshold, CalledStr, Step])
        )
    ;   (   MemoEnabled = true ->
            format(string(Part),
'    {
        int result = ~w(n ~w);
        if (n >= 0 && n < 1000) { memo[~w * 1000 + n] = result; memo_set[~w * 1000 + n] = 1; }
        return result;
    }', [CalledStr, Step, Idx, Idx])
        ;   format(string(Part), '    return ~w(n ~w);', [CalledStr, Step])
        )
    ),
    mutual_rec_cases_c(Rest, MemoEnabled, Idx, RestParts).
mutual_rec_cases_c([_|Rest], MemoEnabled, Idx, RestParts) :-
    mutual_rec_cases_c(Rest, MemoEnabled, Idx, RestParts).

extract_mutual_rec_info_c(Body, Guard, CalledPred, Step) :-
    extract_goals_from_body_c(Body, Goals),
    (   member(Guard, Goals), Guard = (_ > _) -> true
    ;   Guard = none
    ),
    member(Call, Goals),
    compound(Call),
    Call \= (_ is _), Call \= (_ > _), Call \= (_ < _),
    Call \= (_ >= _), Call \= (_ =< _),
    functor(Call, CalledPred, _),
    (   member(_ is _ - K, Goals), integer(K) ->
        format(string(Step), '- ~w', [K])
    ;   member(_ is _ + K, Goals), integer(K), K < 0 ->
        AbsK is abs(K),
        format(string(Step), '- ~w', [AbsK])
    ;   Step = "- 1"
    ).

extract_goals_from_body_c((A, B), Goals) :- !,
    extract_goals_from_body_c(A, GA),
    extract_goals_from_body_c(B, GB),
    append(GA, GB, Goals).
extract_goals_from_body_c(true, []) :- !.
extract_goals_from_body_c(Goal, [Goal]).

mutual_dispatch_c(Predicates, Code) :-
    findall(DispLine, (
        member(Pred/_Arity, Predicates),
        atom_string(Pred, PredStr),
        format(string(DispLine),
'    if (argc >= 3 && strcmp(argv[1], "~w") == 0) printf("%d\\n", ~w(atoi(argv[2])));',
            [PredStr, PredStr])
    ), Lines),
    atomic_list_concat(Lines, '\n', Code).
