%% c_if_then_else_output — generate ternary or if/else
c_if_then_else_output(IfGoal, ThenGoal, ElseGoal, VarMap, Code) :-
    flatten_c_if_branches(IfGoal, ThenGoal, ElseGoal, Branches, DefaultGoal),
    c_branches_to_ternary(Branches, DefaultGoal, VarMap, Code).

flatten_c_if_branches(If, Then, Else, [branch(If, Then)|RestBranches], Default) :-
    if_then_else_goal(Else, If2, Then2, Else2),
    !,
    flatten_c_if_branches(If2, Then2, Else2, RestBranches, Default).
