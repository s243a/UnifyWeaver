c_render_classified_last(passthrough(Goal), VarMap, [], Lines) :-
    c_classified_output_last(Goal, VarMap, Lines).
c_render_classified_last(_, _, [], []).

%% c_classified_output_last(+Goal, +VarMap, -Lines)
