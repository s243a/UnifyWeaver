linear_fold_c(PredStr, BaseClauses, MemoEnabled, Code) :-
    linear_recursion:extract_base_case_info(BaseClauses, BaseInput, BaseOutput),
    linear_recursion:detect_input_type(BaseInput, InputType),
    (   MemoEnabled = true ->
        MemoDecl = "#define MAX_MEMO 10000\nstatic int memo[MAX_MEMO];\nstatic int memo_valid[MAX_MEMO];",
        format(string(MemoCheck), '    if (n >= 0 && n < MAX_MEMO && memo_valid[n]) return memo[n];', []),
        format(string(MemoStore), '    if (n >= 0 && n < MAX_MEMO) { memo[n] = result; memo_valid[n] = 1; }', [])
    ;   MemoDecl = "/* Memoization disabled */",
        MemoCheck = '',
        MemoStore = ''
    ),
    % Extract fold
    atom_string(Pred, PredStr),
    functor(Head, Pred, 2),
    findall(clause(Head, Body), user:clause(Head, Body), AllClauses),
    partition(linear_recursion:is_recursive_clause(Pred), AllClauses, ActualRec, _),
    (   ActualRec = [clause(RHead, RBody)|_] ->
        RHead =.. [_, InputVar, _],
        linear_recursion:find_recursive_call(RBody, RecCall),
        RecCall =.. [_, _, AccVar],
        linear_recursion:find_last_is_expression(RBody, _ is FoldExpr),
        translate_fold_expr_c(FoldExpr, InputVar, AccVar, COp)
    ;   COp = "current * result"
    ),
    (   InputType = numeric ->
        format(string(Code),
'/* Generated by UnifyWeaver C Target - Linear Recursion (numeric, multifile dispatch) */
/* Predicate: ~w/2 */

