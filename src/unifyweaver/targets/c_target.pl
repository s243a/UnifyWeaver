c_branch_value(=(_, Expr), VarMap, Value) :-
    !, c_expr(Expr, VarMap, Value).
c_branch_value(is(_, Expr), VarMap, Value) :-
    !, c_expr(Expr, VarMap, Value).
c_branch_value(Goal, VarMap, Value) :-
    c_expr(Goal, VarMap, Value).

