direct_computations_c(Computations, Code) :-
    findall(Line, (
        member(Var is Expr, Computations),
        direct_var_to_c_name(Var, VarName),
        direct_translate_expr_c(Expr, CExpr),
        format(string(Line), '    int ~w = ~w;', [VarName, CExpr])
    ), Lines),
    atomic_list_concat(Lines, '\n', Code).

direct_recursive_calls_c(RecCalls, PredStr, Code) :-
    findall(Line, (
        member(RecCall, RecCalls),
        RecCall =.. [_Pred, ArgVar, ResultVar],
        direct_var_to_c_name(ArgVar, ArgName),
        direct_var_to_c_name(ResultVar, ResName),
        format(string(Line), '    int ~w = ~w(~w);', [ResName, PredStr, ArgName])
    ), Lines),
    atomic_list_concat(Lines, '\n', Code).

direct_aggregation_c(_ is Expr, Code) :-
    direct_translate_agg_expr_c(Expr, CExpr),
    format(string(Code), '    int result = ~w;', [CExpr]).

direct_translate_agg_expr_c(A + B, Expr) :-
    direct_var_to_c_name(A, AName), direct_var_to_c_name(B, BName),
    format(string(Expr), '~w + ~w', [AName, BName]).
direct_translate_agg_expr_c(A * B, Expr) :-
    direct_var_to_c_name(A, AName), direct_var_to_c_name(B, BName),
    format(string(Expr), '~w * ~w', [AName, BName]).

%% direct_var_to_c_name(+Var, -CName)
%  Convert Prolog variable to valid C identifier.
%  Internal var names like _G12345 become v12345 in C.
direct_var_to_c_name(Var, CName) :-
    (   var(Var) ->
        term_string(Var, VarStr),
        atom_string(VarAtom, VarStr),
        downcase_atom(VarAtom, LowerName),
        ensure_c_identifier(LowerName, CName)
    ;   atom(Var) ->
        downcase_atom(Var, LowerName),
        ensure_c_identifier(LowerName, CName)
    ;   term_string(Var, VarStr),
        atom_string(VarAtom, VarStr),
        downcase_atom(VarAtom, LowerName),
        ensure_c_identifier(LowerName, CName)
    ).

ensure_c_identifier(Name, ValidName) :-
    atom_chars(Name, [First|Rest]),
    (   (First = '_' ; char_type(First, digit)) ->
        atom_chars(ValidName, [v|Rest])
    ;   ValidName = Name
    ).

direct_translate_expr_c(N - K, Expr) :- var(N), integer(K), !,
    format(string(Expr), 'input - ~w', [K]).
direct_translate_expr_c(N + K, Expr) :- var(N), integer(K), K < 0, !,
    AbsK is abs(K), format(string(Expr), 'input - ~w', [AbsK]).
direct_translate_expr_c(N + K, Expr) :- var(N), integer(K), !,
    format(string(Expr), 'input + ~w', [K]).
direct_translate_expr_c(Expr, 'input') :- var(Expr), !.
direct_translate_expr_c(N, S) :- integer(N), !, format(string(S), '~w', [N]).
direct_translate_expr_c(A, S) :- format(string(S), '~w', [A]).

