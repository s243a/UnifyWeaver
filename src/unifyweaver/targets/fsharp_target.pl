%% translate_fold_expr_fsharp(+PrologExpr, +InputVar, +AccVar, -FSharpExpr)
translate_fold_expr_fsharp(Expr, InputVar, AccVar, FSharpExpr) :-
    translate_fsharp_expr(Expr, InputVar, AccVar, FSharpExpr).

translate_fsharp_expr(A * B, InputVar, AccVar, FSharpExpr) :-
    translate_fsharp_term(A, InputVar, AccVar, AT),
    translate_fsharp_term(B, InputVar, AccVar, BT),
    format(string(FSharpExpr), '~w * ~w', [AT, BT]).
translate_fsharp_expr(A + B, InputVar, AccVar, FSharpExpr) :-
    translate_fsharp_term(A, InputVar, AccVar, AT),
    translate_fsharp_term(B, InputVar, AccVar, BT),
    format(string(FSharpExpr), '~w + ~w', [AT, BT]).
translate_fsharp_expr(A - B, InputVar, AccVar, FSharpExpr) :-
    translate_fsharp_term(A, InputVar, AccVar, AT),
    translate_fsharp_term(B, InputVar, AccVar, BT),
    format(string(FSharpExpr), '~w - ~w', [AT, BT]).
translate_fsharp_expr(Term, InputVar, AccVar, FSharpExpr) :-
    translate_fsharp_term(Term, InputVar, AccVar, FSharpExpr).

translate_fsharp_term(Term, InputVar, _AccVar, 'current') :-
    Term == InputVar, !.
translate_fsharp_term(Term, _InputVar, AccVar, 'acc') :-
    Term == AccVar, !.
translate_fsharp_term(Number, _, _, FSharpTerm) :-
    integer(Number), !,
    format(string(FSharpTerm), '~w', [Number]).
translate_fsharp_term(Atom, _, _, FSharpTerm) :-
    format(string(FSharpTerm), '~w', [Atom]).

