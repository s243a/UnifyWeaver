    ;   format('Warning: F# tail recursion with arity ~w not supported~n', [Arity]),
        fail
    ).

%% step_op_to_fsharp(+StepOp, -FSharpExpr)
step_op_to_fsharp(arithmetic(Expr), FSharpExpr) :-
    expr_to_fsharp(Expr, FSharpExpr).
step_op_to_fsharp(unknown, 'acc + 1').

%% expr_to_fsharp(+PrologExpr, -FSharpExpr)
expr_to_fsharp(_ + Const, FSharpExpr) :- integer(Const), !,
    format(atom(FSharpExpr), 'acc + ~w', [Const]).
expr_to_fsharp(_ + _, 'acc + item') :- !.
expr_to_fsharp(_ - _, 'acc - item') :- !.
expr_to_fsharp(_ * _, 'acc * item') :- !.
expr_to_fsharp(_, 'acc + 1').

