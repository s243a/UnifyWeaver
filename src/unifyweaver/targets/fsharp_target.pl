mutual_dispatch_fsharp(Predicates, Code) :-
    findall(MatchArm, (
        member(Pred/_Arity, Predicates),
        atom_string(Pred, PredStr),
        format(string(MatchArm), '        | "~w" -> printfn "%b" (~w n)', [PredStr, PredStr])
    ), Arms),
    atomic_list_concat(Arms, '\n', ArmsCode),
    format(string(Code),
'[<EntryPoint>]
let main argv =
    if argv.Length >= 2 then
        let func = argv.[0]
        let n = int argv.[1]
        match func with
~w
        | _ -> printfn "Unknown function"
    0
', [ArmsCode]).
