', [PredStr, Arity, PredStr, FSharpStepExpr, PredStr])
    ;   Arity =:= 2 ->
        format(string(Code),
'// Generated by UnifyWeaver F# Target - Tail Recursion (binary, multifile dispatch)
// Predicate: ~w/~w

open System

let ~w (items: ''a list) =
    let rec loop remaining count =
        match remaining with
        | [] -> count
        | _ :: rest -> loop rest (count + 1)
    loop items 0

[<EntryPoint>]
