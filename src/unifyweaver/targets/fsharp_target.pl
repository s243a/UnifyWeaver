%% build_fsharp_arg_list(+N, -ArgList)
%  Generates "arg1 arg2 " (trailing space) or "" for 0 args
build_fsharp_arg_list(0, "") :- !.
build_fsharp_arg_list(N, ArgList) :-
    findall(ArgName, (
        between(1, N, I),
        format(string(ArgName), 'arg~w', [I])
    ), ArgNames),
    atomic_list_concat(ArgNames, ' ', ArgListNoTrail),
    format(string(ArgList), '~w ', [ArgListNoTrail]).

%% native_fsharp_clause_body(+PredSpec, +Clauses, -Code)

% Single clause
native_fsharp_clause_body(PredSpec, [Head-Body], Code) :-
    native_fsharp_clause(PredSpec, Head, Body, Condition, ClauseCode),
    !,
    (   Condition == "true"
    ->  format(string(Code), '    ~w', [ClauseCode])
    ;   format(string(Code),
'    if ~w then ~w
    else failwith "No matching clause for ~w"', [Condition, ClauseCode, PredSpec])
    ).

% Multi-clause → if/elif/else
native_fsharp_clause_body(PredSpec, Clauses, Code) :-
    Clauses = [_|[_|_]],
    maplist(native_fsharp_clause_pair(PredSpec), Clauses, Branches),
    Branches \= [],
    branches_to_fsharp_if_chain(Branches, PredSpec, Code).

native_fsharp_clause_pair(PredSpec, Head-Body, branch(Condition, ClauseCode)) :-
    native_fsharp_clause(PredSpec, Head, Body, Condition, ClauseCode),
    !.

%% native_fsharp_clause(+PredSpec, +Head, +Body, -Condition, -Code)
native_fsharp_clause(_PredSpec, Head, Body, Condition, Code) :-
    Head =.. [_Pred|HeadArgs],
    length(HeadArgs, Arity),
    build_head_varmap(HeadArgs, 1, VarMap),
    (   Arity > 1
    ->  append(_InputHeadArgs, [OutputHeadArg], HeadArgs),
        fsharp_head_conditions(HeadArgs, 1, Arity, HeadConditions)
    ;   OutputHeadArg = _,
        fsharp_head_conditions(HeadArgs, 1, Arity, HeadConditions)
    ),
    normalize_goals(Body, Goals),
    (   Goals == []
    ->  fsharp_resolve_value(VarMap, OutputHeadArg, OutputExpr),
        Code = OutputExpr,
        GoalConditions = []
    ;   (   Arity > 1, nonvar(OutputHeadArg)
        ->  clause_guard_output_split(Goals, VarMap, GuardGoals, OutputGoals),
            maplist(fsharp_guard_condition(VarMap), GuardGoals, GoalConditions),
            (   OutputGoals == []
            ->  fsharp_literal(OutputHeadArg, Code)
            ;   fsharp_output_goals(OutputGoals, VarMap, Code)
            )
        ;   native_fsharp_goal_sequence(Goals, VarMap, GoalConditions, Code)
        )
    ),
    append(HeadConditions, GoalConditions, AllConditions),
    combine_fsharp_conditions(AllConditions, Condition).

%% fsharp_head_conditions(+HeadArgs, +Index, +Arity, -Conditions)
fsharp_head_conditions([], _, _, []).
fsharp_head_conditions([_], _, Arity, []) :- Arity > 1, !.
fsharp_head_conditions([HeadArg|Rest], Index, Arity, Conditions) :-
    (   var(HeadArg)
    ->  Conditions = RestConditions
    ;   format(string(ArgName), 'arg~w', [Index]),
        fsharp_literal(HeadArg, Literal),
        format(string(Cond), '~w = ~w', [ArgName, Literal]),
        Conditions = [Cond|RestConditions]
    ),
    NextIndex is Index + 1,
    fsharp_head_conditions(Rest, NextIndex, Arity, RestConditions).

%% native_fsharp_goal_sequence(+Goals, +VarMap, -Conditions, -Code)
%  Uses classify_goal_sequence for advanced pattern detection.
%  Falls back to clause_guard_output_split if classification fails.
native_fsharp_goal_sequence(Goals, VarMap, Conditions, Code) :-
    classify_goal_sequence(Goals, VarMap, ClassifiedGoals),
    ClassifiedGoals \= [],
    fsharp_render_classified_goals(ClassifiedGoals, VarMap, Conditions, Lines),
    Lines \= [],
    atomic_list_concat(Lines, '\n', Code),
    !.
native_fsharp_goal_sequence(Goals, VarMap, Conditions, Code) :-
    clause_guard_output_split(Goals, VarMap, GuardGoals, OutputGoals),
    maplist(fsharp_guard_condition(VarMap), GuardGoals, Conditions),
    fsharp_output_goals(OutputGoals, VarMap, Code).

%% fsharp_render_classified_goals(+ClassifiedGoals, +VarMap, -Conditions, -Lines)
fsharp_render_classified_goals([], _VarMap, [], []).
fsharp_render_classified_goals([Classified], VarMap, Conds, Lines) :-
    !,
    fsharp_render_classified_last(Classified, VarMap, Conds, Lines).
%% Guarded tail: output followed by guard(s)
fsharp_render_classified_goals([output(Goal, _, _)|Rest], VarMap, [], Lines) :-
    Rest = [guard(_, _)|_],
    !,
    fsharp_output_goal(Goal, VarMap, LetBinding, VarMap1),
    fsharp_collect_trailing_guards(Rest, VarMap1, GuardGoals, _Remaining),
    maplist(fsharp_guard_condition(VarMap1), GuardGoals, GuardConds),
    atomic_list_concat(GuardConds, ' && ', GuardExpr),
    (   goal_output_var(Goal, OutVar), lookup_var(OutVar, VarMap1, OutName)
    ->  true
    ;   OutName = '_'
    ),
    format(string(IfLine), '    if ~w then ~w else failwith "guard failed"', [GuardExpr, OutName]),
    Lines = [LetBinding, IfLine].
fsharp_render_classified_goals([Classified|Rest], VarMap, Conds, Lines) :-
    fsharp_render_classified_mid(Classified, VarMap, MidConds, MidLines, VarMap1),
    fsharp_render_classified_goals(Rest, VarMap1, RestConds, RestLines),
    append(MidConds, RestConds, Conds),
    append(MidLines, RestLines, Lines).

%% fsharp_render_classified_mid(+Classified, +VarMap, -Conds, -Lines, -VarMapOut)
fsharp_render_classified_mid(guard(Goal, _), VarMap, [Cond], [], VarMap) :-
    fsharp_guard_condition(VarMap, Goal, Cond).
fsharp_render_classified_mid(output(Goal, _, _), VarMap0, [], [Line], VarMapOut) :-
    fsharp_output_goal(Goal, VarMap0, Line, VarMapOut).
fsharp_render_classified_mid(output_ite(If, Then, Else, _SharedVars), VarMap0, [], Lines, VarMap0) :-
    fsharp_guard_condition(VarMap0, If, Cond),
    fsharp_branch_value(Then, VarMap0, ThenExpr),
    fsharp_branch_value(Else, VarMap0, ElseExpr),
    format(string(IfLine), '    if ~w then', [Cond]),
    format(string(ThenLine), '        ~w', [ThenExpr]),
    ElseLine = '    else',
    format(string(ElseRetLine), '        ~w', [ElseExpr]),
    Lines = [IfLine, ThenLine, ElseLine, ElseRetLine].
fsharp_render_classified_mid(passthrough(Goal), VarMap0, [], [Line], VarMapOut) :-
    fsharp_output_goal(Goal, VarMap0, Line, VarMapOut).
fsharp_render_classified_mid(_, VarMap, [], [], VarMap).

%% fsharp_render_classified_last(+Classified, +VarMap, -Conds, -Lines)
fsharp_render_classified_last(guard(Goal, _), VarMap, [Cond], []) :-
    fsharp_guard_condition(VarMap, Goal, Cond).
fsharp_render_classified_last(output(Goal, _, _), VarMap, [], Lines) :-
    fsharp_render_output_goal_last(Goal, VarMap, Lines).
fsharp_render_classified_last(output_ite(If, Then, Else, _), VarMap, [], Lines) :-
    fsharp_guard_condition(VarMap, If, Cond),
    fsharp_branch_value(Then, VarMap, ThenExpr),
    fsharp_branch_value(Else, VarMap, ElseExpr),
    format(string(IfLine), '    if ~w then', [Cond]),
    format(string(ThenLine), '        ~w', [ThenExpr]),
    ElseLine = '    else',
    format(string(ElseRetLine), '        ~w', [ElseExpr]),
    Lines = [IfLine, ThenLine, ElseLine, ElseRetLine].
fsharp_render_classified_last(output_disj(Alternatives, _SharedVars), VarMap, [], Lines) :-
    fsharp_disj_if_chain(Alternatives, VarMap, Lines).
fsharp_render_classified_last(passthrough(Goal), VarMap, [], Lines) :-
    fsharp_render_output_goal_last(Goal, VarMap, Lines).
fsharp_render_classified_last(_, _, [], []).

%% fsharp_render_output_goal_last(+Goal, +VarMap, -Lines)
%  3-arg wrapper used by classified goal renderers.
fsharp_render_output_goal_last(Goal, VarMap, [Expr]) :-
    fsharp_output_goal_last(Goal, VarMap, Expr, _VarMapOut),
    !.
fsharp_render_output_goal_last(Goal, VarMap, [Expr]) :-
    fsharp_branch_value(Goal, VarMap, Expr).

%% fsharp_collect_trailing_guards(+ClassifiedGoals, +VarMap, -GuardGoals, -Remaining)
fsharp_collect_trailing_guards([guard(Goal, _)|Rest], VarMap, [Goal|Guards], Remaining) :-
    !, fsharp_collect_trailing_guards(Rest, VarMap, Guards, Remaining).
fsharp_collect_trailing_guards(Remaining, _, [], Remaining).

%% fsharp_disj_if_chain(+Alternatives, +VarMap, -Lines)
fsharp_disj_if_chain([], _, []).
fsharp_disj_if_chain([Alt], VarMap, [ElseLine, RetLine]) :-
    !,
    fsharp_branch_value(Alt, VarMap, ValExpr),
    ElseLine = '    else',
    format(string(RetLine), '        ~w', [ValExpr]).
fsharp_disj_if_chain([Alt|Rest], VarMap, [IfLine, RetLine|RestLines]) :-
    normalize_goals(Alt, Goals),
    clause_guard_output_split(Goals, VarMap, Guards, _Outputs),
    (   Guards \= []
    ->  maplist(fsharp_guard_condition(VarMap), Guards, CondStrs),
        atomic_list_concat(CondStrs, ' && ', CondExpr)
    ;   CondExpr = 'true'
    ),
    fsharp_branch_value(Alt, VarMap, ValExpr),
    format(string(IfLine), '    if ~w then', [CondExpr]),
    format(string(RetLine), '        ~w', [ValExpr]),
    fsharp_disj_elif_chain(Rest, VarMap, RestLines).

%% fsharp_disj_elif_chain(+Alternatives, +VarMap, -Lines)
fsharp_disj_elif_chain([], _, []).
fsharp_disj_elif_chain([Alt], VarMap, [ElseLine, RetLine]) :-
    !,
    fsharp_branch_value(Alt, VarMap, ValExpr),
    ElseLine = '    else',
    format(string(RetLine), '        ~w', [ValExpr]).
fsharp_disj_elif_chain([Alt|Rest], VarMap, [ElifLine, RetLine|RestLines]) :-
    normalize_goals(Alt, Goals),
    clause_guard_output_split(Goals, VarMap, Guards, _Outputs),
    (   Guards \= []
    ->  maplist(fsharp_guard_condition(VarMap), Guards, CondStrs),
        atomic_list_concat(CondStrs, ' && ', CondExpr)
    ;   CondExpr = 'true'
    ),
    fsharp_branch_value(Alt, VarMap, ValExpr),
    format(string(ElifLine), '    elif ~w then', [CondExpr]),
    format(string(RetLine), '        ~w', [ValExpr]),
    fsharp_disj_elif_chain(Rest, VarMap, RestLines).

%% fsharp_guard_condition(+VarMap, +Goal, -Condition)
fsharp_guard_condition(VarMap, _Module:Goal, Condition) :-
    !, fsharp_guard_condition(VarMap, Goal, Condition).
fsharp_guard_condition(VarMap, Goal, Condition) :-
    compound(Goal),
    Goal =.. [Op, Left, Right],
    expr_op(Op, StdOp),
    !,
    fsharp_expr(Left, VarMap, FsLeft),
    fsharp_expr(Right, VarMap, FsRight),
    fsharp_op(StdOp, FsOp),
    format(string(Condition), '~w ~w ~w', [FsLeft, FsOp, FsRight]).
fsharp_guard_condition(VarMap, integer(X), Condition) :-
    !, fsharp_expr(X, VarMap, FsX),
    format(string(Condition), '~w :? int', [FsX]).
fsharp_guard_condition(VarMap, float(X), Condition) :-
    !, fsharp_expr(X, VarMap, FsX),
    format(string(Condition), '~w :? float', [FsX]).

%% fsharp_output_goals(+Goals, +VarMap, -Code)
fsharp_output_goals([], _VarMap, 'failwith "no output"') :- !.
fsharp_output_goals(Goals, VarMap, Code) :-
    fsharp_output_goals_acc(Goals, VarMap, Parts, _VarMapOut),
    (   Parts = [SingleExpr]
    ->  Code = SingleExpr
    ;   last(Parts, ResultExpr),
        append(LetParts, [_], Parts),
        findall(LetLine, (
            member(LP, LetParts),
            format(string(LetLine), '    let ~w', [LP])
        ), LetLines),
        atomic_list_concat(LetLines, '\n', LetBlock),
        format(string(Code), '~w\n    ~w', [LetBlock, ResultExpr])
    ).

fsharp_output_goals_acc([], VarMap, [], VarMap).
fsharp_output_goals_acc([Goal], VarMap, [Expr], VarMapOut) :-
    !, fsharp_output_goal_last(Goal, VarMap, Expr, VarMapOut).
fsharp_output_goals_acc([Goal|Rest], VarMap0, [Binding|RestParts], VarMapOut) :-
    fsharp_output_goal(Goal, VarMap0, Binding, VarMap1),
    fsharp_output_goals_acc(Rest, VarMap1, RestParts, VarMapOut).

%% fsharp_output_goal_last — produce the result expression
fsharp_output_goal_last(_Module:Goal, VarMap, Expr, VarMapOut) :-
    !, fsharp_output_goal_last(Goal, VarMap, Expr, VarMapOut).
fsharp_output_goal_last(Goal, VarMap0, Expr, VarMapOut) :-
    if_then_else_goal(Goal, IfGoal, ThenGoal, ElseGoal),
    !,
    fsharp_if_then_else_output(IfGoal, ThenGoal, ElseGoal, VarMap0, Expr, VarMapOut).
fsharp_output_goal_last(=(Var, Value), VarMap, Expr, VarMap) :-
    var(Var), !,
    fsharp_expr(Value, VarMap, Expr).
fsharp_output_goal_last(is(Var, Value), VarMap, Expr, VarMap) :-
    var(Var), !,
    fsharp_expr(Value, VarMap, Expr).
fsharp_output_goal_last(Goal, VarMap0, Expr, VarMapOut) :-
    fsharp_output_goal(Goal, VarMap0, Binding, VarMapOut),
    (   goal_output_var(Goal, OutVar),
        lookup_var(OutVar, VarMapOut, OutName)
    ->  format(string(Expr), 'let ~w in ~w', [Binding, OutName])
    ;   Expr = Binding
    ).

%% fsharp_output_goal — produce a let binding
fsharp_output_goal(_Module:Goal, VarMap0, Binding, VarMapOut) :-
    !, fsharp_output_goal(Goal, VarMap0, Binding, VarMapOut).
fsharp_output_goal(=(Var, Value), VarMap0, Binding, VarMapOut) :-
    var(Var), !,
    ensure_var(VarMap0, Var, VarName, VarMapOut),
    fsharp_expr(Value, VarMap0, FsExpr),
    format(string(Binding), '~w = ~w', [VarName, FsExpr]).
fsharp_output_goal(is(Var, Value), VarMap0, Binding, VarMapOut) :-
    var(Var), !,
    ensure_var(VarMap0, Var, VarName, VarMapOut),
    fsharp_expr(Value, VarMap0, FsExpr),
    format(string(Binding), '~w = ~w', [VarName, FsExpr]).

%% fsharp_if_then_else_output — generate if/elif/else expression
fsharp_if_then_else_output(IfGoal, ThenGoal, ElseGoal, VarMap0, Expr, VarMapOut) :-
    flatten_fsharp_if_branches(IfGoal, ThenGoal, ElseGoal, Branches, DefaultGoal),
    fsharp_branches_to_expr(Branches, DefaultGoal, VarMap0, Expr),
    VarMapOut = VarMap0.

flatten_fsharp_if_branches(If, Then, Else, [branch(If, Then)|RestBranches], Default) :-
    if_then_else_goal(Else, If2, Then2, Else2),
    !,
    flatten_fsharp_if_branches(If2, Then2, Else2, RestBranches, Default).
flatten_fsharp_if_branches(If, Then, Else, [branch(If, Then)], Else).

fsharp_branches_to_expr([branch(If, Then)], DefaultGoal, VarMap, Expr) :-
    !,
    fsharp_guard_condition(VarMap, If, IfCond),
    fsharp_branch_value(Then, VarMap, ThenVal),
    fsharp_branch_value(DefaultGoal, VarMap, ElseVal),
    format(string(Expr), 'if ~w then ~w else ~w', [IfCond, ThenVal, ElseVal]).
fsharp_branches_to_expr(Branches, DefaultGoal, VarMap, Expr) :-
    fsharp_nested_if_expr(Branches, DefaultGoal, VarMap, Expr).

fsharp_nested_if_expr([branch(If, Then)], DefaultGoal, VarMap, Expr) :-
    !,
    fsharp_guard_condition(VarMap, If, IfCond),
    fsharp_branch_value(Then, VarMap, ThenVal),
    fsharp_branch_value(DefaultGoal, VarMap, ElseVal),
    format(string(Expr), 'if ~w then ~w else ~w', [IfCond, ThenVal, ElseVal]).
fsharp_nested_if_expr([branch(If, Then)|Rest], DefaultGoal, VarMap, Expr) :-
    fsharp_guard_condition(VarMap, If, IfCond),
    fsharp_branch_value(Then, VarMap, ThenVal),
    fsharp_nested_if_expr(Rest, DefaultGoal, VarMap, ElseExpr),
    format(string(Expr), 'if ~w then ~w el~w', [IfCond, ThenVal, ElseExpr]).

%% fsharp_branch_value — extract result value from a branch
