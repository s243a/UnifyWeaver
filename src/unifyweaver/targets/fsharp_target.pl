:- use_module('../core/advanced/linear_recursion').
:- multifile linear_recursion:compile_linear_pattern/8.

linear_recursion:compile_linear_pattern(fsharp, PredStr, Arity, BaseClauses, RecClauses, MemoEnabled, _MemoStrategy, Code) :-
    (   Arity =:= 2 ->
        linear_fold_based_fsharp(PredStr, BaseClauses, RecClauses, MemoEnabled, Code)
    ;   linear_generic_fsharp(PredStr, Arity, MemoEnabled, Code)
    ).

%% linear_fold_based_fsharp(+PredStr, +BaseClauses, +RecClauses, +MemoEnabled, -Code)
linear_fold_based_fsharp(PredStr, BaseClauses, RecClauses, MemoEnabled, Code) :-
    linear_recursion:extract_base_case_info(BaseClauses, BaseInput, BaseOutput),
    linear_recursion:detect_input_type(BaseInput, InputType),
    (   InputType = numeric ->
        linear_numeric_fsharp(PredStr, BaseInput, BaseOutput, RecClauses, MemoEnabled, Code)
    ;   InputType = list ->
        linear_list_fsharp(PredStr, BaseOutput, RecClauses, MemoEnabled, Code)
    ;   linear_generic_fsharp(PredStr, 2, MemoEnabled, Code)
    ).

%% linear_numeric_fsharp(+PredStr, +BaseInput, +BaseOutput, +RecClauses, +MemoEnabled, -Code)
linear_numeric_fsharp(PredStr, BaseInput, BaseOutput, RecClauses, MemoEnabled, Code) :-
    % Extract fold operation from rec clauses
    atom_string(Pred, PredStr),
    functor(Head, Pred, 2),
    findall(clause(Head, Body), user:clause(Head, Body), AllClauses),
    partition(linear_recursion:is_recursive_clause(Pred), AllClauses, ActualRecClauses, _),
    (   ActualRecClauses = [clause(RHead, RBody)|_] ->
        RHead =.. [_, InputVar, _],
        linear_recursion:find_recursive_call(RBody, RecCall),
        RecCall =.. [_, _, AccVar],
        linear_recursion:find_last_is_expression(RBody, _ is ActualFoldExpr),
        translate_fold_expr_fsharp(ActualFoldExpr, InputVar, AccVar, FSharpFoldOp)
    ;   RecClauses = [clause(RHead2, RBody2)|_] ->
        RHead2 =.. [_, InputVar2, _],
        linear_recursion:find_recursive_call(RBody2, RecCall2),
        RecCall2 =.. [_, _, AccVar2],
        linear_recursion:find_last_is_expression(RBody2, _ is ActualFoldExpr2),
        translate_fold_expr_fsharp(ActualFoldExpr2, InputVar2, AccVar2, FSharpFoldOp)
    ;   FSharpFoldOp = "current * acc"
    ),

    (   MemoEnabled = true ->
        format(string(MemoDecl), 'let private memo = System.Collections.Generic.Dictionary<int, int>()', []),
        format(string(MemoCheck), '    match memo.TryGetValue(n) with~n    | true, v -> v~n    | false, _ ->', []),
        format(string(MemoStore), '        memo.[n] <- result~n        result', [])
    ;   MemoDecl = '// Memoization disabled',
        MemoCheck = '    let compute () =',
        MemoStore = '        result'
    ),

    format(string(Code),
'// Generated by UnifyWeaver F# Target - Linear Recursion (numeric fold, multifile dispatch)
// Predicate: ~w/2

open System
open System.Collections.Generic

~w

let ~w_op current acc = ~w

let rec ~w n =
~w
        let result =
            match n with
            | ~w -> ~w
            | n -> ~w_op n (~w (n - 1))
~w

let clearMemo () = memo.Clear()

[<EntryPoint>]
let main argv =
    if argv.Length >= 1 then
        let n = int argv.[0]
        printfn "%d" (~w n)
    0
', [PredStr, MemoDecl, PredStr, FSharpFoldOp, PredStr, MemoCheck, BaseInput, BaseOutput, PredStr, PredStr, MemoStore, PredStr]).

