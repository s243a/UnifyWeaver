'// Generated by UnifyWeaver F# Target - Mutual Recursion (multifile dispatch)
// Group: ~w
// Constraints: memo=~w, strategy=~w

open System

~w
', [GroupName, MemoEnabled, MemoStrategy, MemoDecl]),

    % Generate mutually recursive function block
    mutual_functions_fsharp(Predicates, MemoEnabled, FunctionsCode),

    % Generate dispatch
    mutual_dispatch_fsharp(Predicates, DispatchCode),

    atomic_list_concat([HeaderCode, '\n', FunctionsCode, '\n\n', DispatchCode], Code).

%% mutual_functions_fsharp(+Predicates, +MemoEnabled, -Code)
mutual_functions_fsharp(Predicates, MemoEnabled, Code) :-
    mutual_functions_fsharp_(Predicates, Predicates, MemoEnabled, first, FuncCodes),
    atomic_list_concat(FuncCodes, '\n\n', Code).

mutual_functions_fsharp_([], _AllPreds, _MemoEnabled, _Pos, []).
mutual_functions_fsharp_([Pred/Arity|Rest], AllPreds, MemoEnabled, Pos, [FuncCode|RestCodes]) :-
    atom_string(Pred, PredStr),
    % Get clauses for this predicate
    functor(Head, Pred, Arity),
    findall(clause(Head, Body), user:clause(Head, Body), Clauses),
    partition(mutual_recursion:is_mutual_recursive_clause(AllPreds), Clauses, RecClauses, BaseClauses),

    % Keyword: first function uses 'let rec', others use 'and'
    (   Pos = first -> Keyword = "let rec"
    ;   Keyword = "and"
    ),

    % Base cases
    mutual_base_cases_fsharp(BaseClauses, BaseParts),

    % Recursive cases
    mutual_rec_cases_fsharp(RecClauses, MemoEnabled, RecParts),

    % Combine match arms
    append(BaseParts, RecParts, AllParts),
    (   AllParts = [] ->
        MatchCode = "    | _ -> false"
    ;   atomic_list_concat(AllParts, '\n', MatchCode)
    ),

    % Memo wrapping
    (   MemoEnabled = true ->
        format(string(FuncCode),
'~w ~w n =
    let key = sprintf "~w:%d" n
    match memo.TryGetValue(key) with
    | true, v -> v
    | false, _ ->
        let result =
            match n with
~w
            | _ -> false
        memo.[key] <- result
        result', [Keyword, PredStr, PredStr, MatchCode])
    ;   format(string(FuncCode),
'~w ~w n =
    match n with
~w
    | _ -> false', [Keyword, PredStr, MatchCode])
    ),

    mutual_functions_fsharp_(Rest, AllPreds, MemoEnabled, subsequent, RestCodes).

%% mutual_base_cases_fsharp(+BaseClauses, -Parts)
mutual_base_cases_fsharp([], []).
mutual_base_cases_fsharp([clause(Head, true)|Rest], [Part|RestParts]) :-
    Head =.. [_Pred, Value],
    (   Value == 0 -> format(string(Part), '            | 0 -> true', [])
    ;   Value == 1 -> format(string(Part), '            | 1 -> true', [])
    ;   format(string(Part), '            | ~w -> true', [Value])
    ),
    mutual_base_cases_fsharp(Rest, RestParts).
mutual_base_cases_fsharp([_|Rest], RestParts) :-
    mutual_base_cases_fsharp(Rest, RestParts).

%% mutual_rec_cases_fsharp(+RecClauses, +MemoEnabled, -Parts)
mutual_rec_cases_fsharp([], _MemoEnabled, []).
mutual_rec_cases_fsharp([clause(Head, Body)|Rest], MemoEnabled, [Part|RestParts]) :-
    Head =.. [_Pred|_Args],
    % Extract the guard condition and recursive call from body
    extract_mutual_rec_info_fsharp(Body, Guard, CalledPred, ArgExpr),
    (   Guard = (N > Threshold) ->
        (   var(N) -> GuardStr = "n"
        ;   format(string(GuardStr), '~w', [N])
        ),
        format(string(Part), '            | n when ~w > ~w -> ~w (n ~w)', [GuardStr, Threshold, CalledPred, ArgExpr])
    ;   format(string(Part), '            | n -> ~w (n ~w)', [CalledPred, ArgExpr])
    ),
    mutual_rec_cases_fsharp(Rest, MemoEnabled, RestParts).
mutual_rec_cases_fsharp([_|Rest], MemoEnabled, RestParts) :-
    mutual_rec_cases_fsharp(Rest, MemoEnabled, RestParts).

%% extract_mutual_rec_info_fsharp(+Body, -Guard, -CalledPred, -ArgExpr)
extract_mutual_rec_info_fsharp(Body, Guard, CalledPred, ArgExpr) :-
    extract_goals_from_body(Body, Goals),
    % Find guard (comparison)
    (   member(Guard, Goals), Guard = (_ > _) -> true
    ;   Guard = none
    ),
    % Find the recursive call (non-arithmetic, non-comparison goal)
    member(Call, Goals),
    compound(Call),
    Call \= (_ is _), Call \= (_ > _), Call \= (_ < _),
    Call \= (_ >= _), Call \= (_ =< _),
    functor(Call, CalledPred, _),
    % Find arithmetic step (N1 is N - 1 pattern)
    (   member(_ is _ - K, Goals), integer(K) ->
        format(string(ArgExpr), '- ~w', [K])
    ;   member(_ is _ + K, Goals), integer(K), K < 0 ->
        AbsK is abs(K),
        format(string(ArgExpr), '- ~w', [AbsK])
    ;   ArgExpr = "- 1"
    ).

%% extract_goals_from_body(+Body, -Goals)
extract_goals_from_body((A, B), Goals) :- !,
    extract_goals_from_body(A, GA),
    extract_goals_from_body(B, GB),
    append(GA, GB, Goals).
extract_goals_from_body(true, []) :- !.
extract_goals_from_body(Goal, [Goal]).

%% mutual_dispatch_fsharp(+Predicates, -Code)
mutual_dispatch_fsharp(Predicates, Code) :-
    findall(MatchArm, (
        member(Pred/_Arity, Predicates),
        atom_string(Pred, PredStr),
        format(string(MatchArm), '        | "~w" -> printfn "%b" (~w n)', [PredStr, PredStr])
    ), Arms),
    atomic_list_concat(Arms, '\n', ArmsCode),
    format(string(Code),
'[<EntryPoint>]
let main argv =
    if argv.Length >= 2 then
        let func = argv.[0]
        let n = int argv.[1]
        match func with
~w
        | _ -> printfn "Unknown function"
    0
', [ArmsCode]).

