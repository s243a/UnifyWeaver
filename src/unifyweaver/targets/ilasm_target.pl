%% Mutual recursion → CIL static methods with .tail prefix
mutual_recursion:compile_mutual_pattern(ilasm, Predicates, _MemoEnabled, _MemoStrategy, Code) :-
    ClassName = 'PrologGenerated',
    %% Generate a method for each predicate
    ilasm_mutual_methods(Predicates, Predicates, ClassName, MethodCodes),
    atomic_list_concat(MethodCodes, '\n\n', MethodSection),
    %% Entry point uses first predicate
    Predicates = [FirstPred/_|_],
    atom_string(FirstPred, FirstStr),
    format(string(Code),
'// Generated by UnifyWeaver ILAsm Target - Mutual Recursion
// Group: ~w

.assembly extern mscorlib {}
.assembly PrologGenerated {}

.class public auto ansi PrologGenerated extends [mscorlib]System.Object {

~w

.method public static void Main(string[] args) cil managed {
    .entrypoint
    .maxstack 4
    ldarg.0
    ldc.i4.0
    ldelem.ref
    call int64 [mscorlib]System.Int64::Parse(string)
    call int64 PrologGenerated::~w(int64)
    call void [mscorlib]System.Console::WriteLine(int64)
    ret
}

}
', [Predicates, MethodSection, FirstStr]).

ilasm_mutual_methods([], _, _, []).
ilasm_mutual_methods([Pred/Arity|Rest], AllPreds, ClassName, [MethodCode|RestCodes]) :-
    atom_string(Pred, PredStr),
    functor(Head, Pred, Arity),
    findall(clause(Head, Body), user:clause(Head, Body), Clauses),
    partition(mutual_recursion:is_mutual_recursive_clause(AllPreds), Clauses, RecClauses, BaseClauses),
    %% Base case value
    (   BaseClauses = [clause(BHead, true)|_],
        BHead =.. [_, BaseInput]
    ->  (number(BaseInput) -> true ; BaseInput = 0)
    ;   BaseInput = 0
    ),
    %% Find called predicate
    (   RecClauses = [clause(_, RBody)|_]
    ->  normalize_goals(RBody, Goals),
        (   member(Call, Goals), compound(Call), Call =.. [CalledPred, _],
            member(CalledPred/_CA, AllPreds), CalledPred \= Pred
        ->  atom_string(CalledPred, CalledStr)
        ;   CalledStr = PredStr
        )
    ;   CalledStr = PredStr
    ),
    format(string(CallInstr), '    .tail\n    call int64 ~w::~w(int64)', [ClassName, CalledStr]),
    format(string(MethodCode),
'.method public static int64 ~w(int64 arg0) cil managed {
    .maxstack 4
    // Base case: arg0 == ~w
    ldarg.0
    ldc.i8 ~w
    beq BASE_~w
    // Recursive: .tail call ~w(arg0 - 1)
    ldarg.0
    ldc.i8 1
    sub
~w
    ret
BASE_~w:
    ldc.i8 1
    ret
}', [PredStr, BaseInput, BaseInput, PredStr, CalledStr, CallInstr, PredStr]),
    ilasm_mutual_methods(Rest, AllPreds, ClassName, RestCodes).

