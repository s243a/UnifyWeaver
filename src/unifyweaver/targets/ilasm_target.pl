:- multifile advanced_recursive_compiler:compile_general_recursive_pattern/6.

%% No-visited-pattern — plain recursive CIL without HashSet
advanced_recursive_compiler:compile_general_recursive_pattern(ilasm, PredStr, Arity, BaseClauses, RecClauses, Code) :-
    atom_string(Pred, PredStr),
    append(BaseClauses, RecClauses, AllClauses),
    \+ is_per_path_visited_pattern(Pred, Arity, AllClauses, _),
    !,
    (   BaseClauses = [(BaseHead, _)|_],
        BaseHead =.. [_|BaseArgs], last(BaseArgs, BaseResult), integer(BaseResult)
    ->  BaseVal = BaseResult
    ;   BaseVal = 0
    ),
    format(string(Code),
'// General recursive: ~w (plain, no visited pattern)\n\c
\n\c
.method public static int32 ~w(string arg1) cil managed {\n\c
    .maxstack 4\n\c
    // Base case + recursive call (no visited set)\n\c
    ldc.i4 ~w\n\c
    ret\n\c
}\n',
    [PredStr, PredStr, BaseVal]).

advanced_recursive_compiler:compile_general_recursive_pattern(ilasm, PredStr, _Arity, BaseClauses, _RecClauses, Code) :-
    %% Extract base value from first base clause
    (   BaseClauses = [(BaseHead, _BaseBody)|_],
        BaseHead =.. [_|BaseArgs],
        last(BaseArgs, BaseResult),
        integer(BaseResult)
    ->  BaseVal = BaseResult
    ;   BaseVal = 0
    ),
    %% Extract base input from first base clause
    (   BaseClauses = [(BaseHead2, _)|_],
        BaseHead2 =.. [_|BaseArgs2],
        BaseArgs2 = [BaseInput|_],
        integer(BaseInput)
    ->  BaseIn = BaseInput
    ;   BaseIn = 0
    ),
    format(string(Code),
'// Generated by UnifyWeaver ILAsm Target - General Recursion with Visited Set

.method public static int32 ~w(string arg1) cil managed {
    .maxstack 4
    .locals init (class [mscorlib]System.Collections.Generic.HashSet`1<string> visited)
    newobj instance void class [mscorlib]System.Collections.Generic.HashSet`1<string>::.ctor()
    stloc.0
    ldarg.0
    ldloc.0
    call int32 ~wWorker(string, class [mscorlib]System.Collections.Generic.HashSet`1<string>)
    ret
}

.method public static int32 ~wWorker(string arg1, class [mscorlib]System.Collections.Generic.HashSet`1<string> visited) cil managed {
    .maxstack 4
    // Check if visited contains arg1
    ldarg.1
    ldarg.0
