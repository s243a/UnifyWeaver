write_namespace_file(ProjectDir, Namespace, Content) :-
    namespace_relative_path(Namespace, RelativePath),
    directory_file_path(ProjectDir, 'src', SrcDir),
    directory_file_path(SrcDir, RelativePath, FullPath),
