'    if (~w) {
        return ~w
    }
    throw RuntimeException("No matching clause for ~w")', [Condition, ClauseCode, PredSpec])
    ).

% Multi-clause → if/else if/else
native_kotlin_clause_body(PredSpec, Clauses, Code) :-
    Clauses = [_|[_|_]],
    maplist(native_kotlin_clause_pair(PredSpec), Clauses, Branches),
    Branches \= [],
    branches_to_kotlin_if_chain(Branches, PredSpec, Code).

native_kotlin_clause_pair(PredSpec, Head-Body, branch(Condition, ClauseCode)) :-
    native_kotlin_clause(PredSpec, Head, Body, Condition, ClauseCode),
    !.

%% native_kotlin_clause(+PredSpec, +Head, +Body, -Condition, -Code)
native_kotlin_clause(_PredSpec, Head, Body, Condition, Code) :-
    Head =.. [_Pred|HeadArgs],
    length(HeadArgs, Arity),
    build_head_varmap(HeadArgs, 1, VarMap),
    (   Arity > 1
    ->  append(_InputHeadArgs, [OutputHeadArg], HeadArgs),
        kotlin_head_conditions(HeadArgs, 1, Arity, HeadConditions)
    ;   OutputHeadArg = _,
        kotlin_head_conditions(HeadArgs, 1, Arity, HeadConditions)
    ),
    normalize_goals(Body, Goals),
    (   Goals == []
    ->  kotlin_resolve_value(VarMap, OutputHeadArg, Code),
        GoalConditions = []
    ;   (   Arity > 1, nonvar(OutputHeadArg)
        ->  clause_guard_output_split(Goals, VarMap, GuardGoals, OutputGoals),
            maplist(kotlin_guard_condition(VarMap), GuardGoals, GoalConditions),
            (   OutputGoals == []
            ->  kotlin_literal(OutputHeadArg, Code)
            ;   kotlin_output_goals(OutputGoals, VarMap, Code)
            )
        ;   native_kotlin_goal_sequence(Goals, VarMap, GoalConditions, Code)
        )
    ),
    append(HeadConditions, GoalConditions, AllConditions),
    combine_kotlin_conditions(AllConditions, Condition).

%% kotlin_head_conditions(+HeadArgs, +Index, +Arity, -Conditions)
kotlin_head_conditions([], _, _, []).
kotlin_head_conditions([_], _, Arity, []) :- Arity > 1, !.
kotlin_head_conditions([HeadArg|Rest], Index, Arity, Conditions) :-
    (   var(HeadArg)
    ->  Conditions = RestConditions
    ;   format(string(ArgName), 'arg~w', [Index]),
        kotlin_literal(HeadArg, Literal),
        format(string(Cond), '~w == ~w', [ArgName, Literal]),
        Conditions = [Cond|RestConditions]
    ),
    NextIndex is Index + 1,
    kotlin_head_conditions(Rest, NextIndex, Arity, RestConditions).

%% native_kotlin_goal_sequence(+Goals, +VarMap, -Conditions, -Code)
%  Uses classify_goal_sequence for advanced pattern detection.
%  Falls back to clause_guard_output_split if classification fails.
native_kotlin_goal_sequence(Goals, VarMap, Conditions, Code) :-
    classify_goal_sequence(Goals, VarMap, ClassifiedGoals),
    ClassifiedGoals \= [],
    kotlin_render_classified_goals(ClassifiedGoals, VarMap, Conditions, Lines),
    Lines \= [],
    atomic_list_concat(Lines, '\n', Code),
    !.
native_kotlin_goal_sequence(Goals, VarMap, Conditions, Code) :-
    clause_guard_output_split(Goals, VarMap, GuardGoals, OutputGoals),
    maplist(kotlin_guard_condition(VarMap), GuardGoals, Conditions),
    kotlin_output_goals(OutputGoals, VarMap, Code).

%% kotlin_render_classified_goals(+ClassifiedGoals, +VarMap, -Conditions, -Lines)
kotlin_render_classified_goals([], _VarMap, [], []).
kotlin_render_classified_goals([Classified], VarMap, Conds, Lines) :-
    !,
    kotlin_render_classified_last(Classified, VarMap, Conds, Lines).
%% Guarded tail: output followed by guard(s)
kotlin_render_classified_goals([output(Goal, _, _)|Rest], VarMap, [], Lines) :-
    Rest = [guard(_, _)|_],
    !,
    kotlin_classified_output_goal(Goal, VarMap, LetBinding, VarMap1),
    kotlin_collect_trailing_guards(Rest, VarMap1, GuardGoals, _Remaining),
    maplist(kotlin_guard_condition(VarMap1), GuardGoals, GuardConds),
    atomic_list_concat(GuardConds, ' && ', GuardExpr),
    (   goal_output_var(Goal, OutVar), lookup_var(OutVar, VarMap1, OutName)
    ->  true
    ;   OutName = '_'
    ),
    format(string(IfLine), '    if (~w) ~w else throw RuntimeException("guard failed")', [GuardExpr, OutName]),
    Lines = [LetBinding, IfLine].
kotlin_render_classified_goals([Classified|Rest], VarMap, Conds, Lines) :-
    kotlin_render_classified_mid(Classified, VarMap, MidConds, MidLines, VarMap1),
    kotlin_render_classified_goals(Rest, VarMap1, RestConds, RestLines),
    append(MidConds, RestConds, Conds),
    append(MidLines, RestLines, Lines).

