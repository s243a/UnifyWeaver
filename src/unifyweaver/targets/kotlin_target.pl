linear_fold_kotlin(PredStr, BaseClauses, MemoEnabled, Code) :-
    linear_recursion:extract_base_case_info(BaseClauses, BaseInput, BaseOutput),
    linear_recursion:detect_input_type(BaseInput, InputType),
    % Extract fold
    atom_string(Pred, PredStr),
    functor(Head, Pred, 2),
    findall(clause(Head, Body), user:clause(Head, Body), AllClauses),
    partition(linear_recursion:is_recursive_clause(Pred), AllClauses, ActualRec, _),
    (   ActualRec = [clause(RHead, RBody)|_] ->
        RHead =.. [_, InputVar, _],
        linear_recursion:find_recursive_call(RBody, RecCall),
        RecCall =.. [_, _, AccVar],
        linear_recursion:find_last_is_expression(RBody, _ is FoldExpr),
        translate_fold_expr_kotlin(FoldExpr, InputVar, AccVar, KotlinOp)
    ;   KotlinOp = "acc * current"
    ),
    (   InputType = numeric ->
        (   MemoEnabled = true ->
            MemoDecl = "private val memo = mutableMapOf<Int, Int>()",
            MemoCheck = "    return memo.getOrPut(n) {",
            MemoStore = "    }"
        ;   MemoDecl = "// Memoization disabled",
            MemoCheck = "    run {",
            MemoStore = "    }"
        ),
        format(string(Code),
'// Generated by UnifyWeaver Kotlin Target - Linear Recursion (numeric, multifile dispatch)
// Predicate: ~w/2

~w

fun ~w(n: Int): Int {
~w
        if (n == ~w) ~w
        else (n downTo 1).fold(~w) { acc, current -> ~w }
~w
}

fun main(args: Array<String>) {
    if (args.isNotEmpty()) println(~w(args[0].toInt()))
}
', [PredStr, MemoDecl, PredStr, MemoCheck, BaseInput, BaseOutput, BaseOutput, KotlinOp, MemoStore, PredStr])
    ;   InputType = list ->
        (   MemoEnabled = true ->
            MemoDecl = "private val memo = mutableMapOf<List<Int>, Int>()",
            MemoCheck = "    return memo.getOrPut(lst) {",
            MemoStore = "    }"
        ;   MemoDecl = "// Memoization disabled",
            MemoCheck = "    run {",
            MemoStore = "    }"
        ),
        format(string(Code),
'// Generated by UnifyWeaver Kotlin Target - Linear Recursion (list, multifile dispatch)
// Predicate: ~w/2

~w

fun ~w(lst: List<Int>): Int {
~w
        if (lst.isEmpty()) ~w
        else lst.fold(~w) { acc, current -> ~w }
~w
}

fun main(args: Array<String>) {
    if (args.isNotEmpty()) println(~w(args[0].split(",").map { it.toInt() }))
}
', [PredStr, MemoDecl, PredStr, MemoCheck, BaseOutput, BaseOutput, KotlinOp, MemoStore, PredStr])
    ;   linear_generic_kotlin(PredStr, 2, MemoEnabled, Code)
    ).

