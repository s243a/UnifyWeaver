kotlin_branch_value(=(_, Expr), VarMap, Value) :-
    !, kotlin_expr(Expr, VarMap, Value).
kotlin_branch_value(is(_, Expr), VarMap, Value) :-
    !, kotlin_expr(Expr, VarMap, Value).
kotlin_branch_value(Goal, VarMap, Value) :-
    kotlin_expr(Goal, VarMap, Value).

