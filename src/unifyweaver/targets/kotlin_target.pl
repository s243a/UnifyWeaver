% Try native clause body lowering first
compile_simple_mode_kotlin(Pred, Arity, _Options, KotlinCode) :-
    functor(Head, Pred, Arity),
    findall(Head-Body, user:clause(Head, Body), Clauses),
    Clauses \= [],
    \+ (member(_-Body, Clauses), Body == true),
    native_kotlin_clause_body(Pred/Arity, Clauses, FuncBody),
    !,
    atom_string(Pred, PredStr),
    Arity1 is Arity - 1,
    build_kotlin_arg_list(Arity1, ArgList),
    format(string(KotlinCode),
'// Generated by UnifyWeaver Kotlin Target - Native Clause Lowering
// Predicate: ~w/~w

package generated

fun ~w(~w): String {
~w
}

fun main(args: Array<String>) {
    if (args.isNotEmpty()) {
        println(~w(args[0].toLong()))
    }
}
', [PredStr, Arity, PredStr, ArgList, FuncBody, PredStr]).

% Fallback stub
compile_simple_mode_kotlin(Pred, Arity, _Options, KotlinCode) :-
