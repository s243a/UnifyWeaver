mutual_functions_kotlin([], _AllPreds, _MemoEnabled, []).
mutual_functions_kotlin([Pred/Arity|Rest], AllPreds, MemoEnabled, [FuncCode|RestCodes]) :-
    atom_string(Pred, PredStr),
    functor(Head, Pred, Arity),
    findall(clause(Head, Body), user:clause(Head, Body), Clauses),
    partition(mutual_recursion:is_mutual_recursive_clause(AllPreds), Clauses, RecClauses, BaseClauses),
    findall(BaseLine, (
        member(clause(BHead, true), BaseClauses),
        BHead =.. [_P, BValue],
        format(string(BaseLine), '    if (n == ~w) return true', [BValue])
    ), BaseLines),
    atomic_list_concat(BaseLines, '\n', BaseCode),
    (   RecClauses = [clause(_RHead, RBody)|_] ->
        extract_mutual_rec_info_kotlin(RBody, Guard, CalledPred, Step),
        atom_string(CalledPred, CalledStr),
        (   Guard = (N > Threshold), var(N) ->
            (   MemoEnabled = true ->
                format(string(RecCode),
'    if (n > ~w) {
        val key = "~w:$n"
        mutual_memo[key]?.let { return it }
        val result = ~w(n ~w)
        mutual_memo[key] = result
        return result
    }
    return false', [Threshold, PredStr, CalledStr, Step])
            ;   format(string(RecCode),
'    return if (n > ~w) ~w(n ~w) else false', [Threshold, CalledStr, Step])
            )
        ;   format(string(RecCode), '    return ~w(n ~w)', [CalledStr, Step])
        )
    ;   RecCode = '    return false'
    ),
    format(string(FuncCode),
'fun ~w(n: Int): Boolean {
~w
~w
}', [PredStr, BaseCode, RecCode]),
    mutual_functions_kotlin(Rest, AllPreds, MemoEnabled, RestCodes).

mutual_dispatch_kotlin(Predicates, Code) :-
    findall(DispatchLine, (
        member(Pred/_Arity, Predicates),
        atom_string(Pred, PredStr),
        format(string(DispatchLine), '        "~w" -> println(~w(n))', [PredStr, PredStr])
    ), Lines),
    atomic_list_concat(Lines, '\n', WhenBody),
    format(string(Code),
'        when (func) {
~w
        }', [WhenBody]).

extract_mutual_rec_info_kotlin(Body, Guard, CalledPred, Step) :-
    extract_goals_kotlin(Body, Goals),
    (   member(Guard, Goals), Guard = (_ > _) -> true
    ;   Guard = none
    ),
    member(Call, Goals),
    compound(Call),
    Call \= (_ is _), Call \= (_ > _), Call \= (_ < _),
    Call \= (_ >= _), Call \= (_ =< _),
    functor(Call, CalledPred, _),
    (   member(_ is _ - K, Goals), integer(K) ->
        format(string(Step), '- ~w', [K])
    ;   member(_ is _ + K, Goals), integer(K), K < 0 ->
        AbsK is abs(K),
        format(string(Step), '- ~w', [AbsK])
    ;   Step = "- 1"
    ).

extract_goals_kotlin((A, B), Goals) :- !,
    extract_goals_kotlin(A, GA),
    extract_goals_kotlin(B, GB),
    append(GA, GB, Goals).
extract_goals_kotlin(true, []) :- !.
extract_goals_kotlin(Goal, [Goal]).

