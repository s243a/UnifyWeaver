    kotlin_expr(Expr, VarMap, Code).
kotlin_output_goal_last(is(Var, Expr), VarMap, Code) :-
    var(Var), !,
    kotlin_expr(Expr, VarMap, Code).

%% kotlin_if_then_else_output — generate if/else expressions (Kotlin uses if/else as expressions)
kotlin_if_then_else_output(IfGoal, ThenGoal, ElseGoal, VarMap, Code) :-
    flatten_kotlin_if_branches(IfGoal, ThenGoal, ElseGoal, Branches, DefaultGoal),
    kotlin_branches_to_if_expr(Branches, DefaultGoal, VarMap, Code).

flatten_kotlin_if_branches(If, Then, Else, [branch(If, Then)|RestBranches], Default) :-
    if_then_else_goal(Else, If2, Then2, Else2),
    !,
    flatten_kotlin_if_branches(If2, Then2, Else2, RestBranches, Default).
flatten_kotlin_if_branches(If, Then, Else, [branch(If, Then)], Else).

kotlin_branches_to_if_expr([branch(If, Then)], DefaultGoal, VarMap, Code) :-
    !,
    kotlin_guard_condition(VarMap, If, IfCond),
    kotlin_branch_value(Then, VarMap, ThenVal),
    kotlin_branch_value(DefaultGoal, VarMap, ElseVal),
    format(string(Code), 'if (~w) ~w else ~w', [IfCond, ThenVal, ElseVal]).
kotlin_branches_to_if_expr([branch(If, Then)|Rest], DefaultGoal, VarMap, Code) :-
    kotlin_guard_condition(VarMap, If, IfCond),
    kotlin_branch_value(Then, VarMap, ThenVal),
    kotlin_branches_to_if_expr(Rest, DefaultGoal, VarMap, ElseCode),
    format(string(Code), 'if (~w) ~w else ~w', [IfCond, ThenVal, ElseCode]).

%% kotlin_branch_value — extract result value from a branch
kotlin_branch_value(_Module:Goal, VarMap, Value) :-
