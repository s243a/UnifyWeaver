%% build_haskell_arg_list(+N, -ArgList)
%  Generates "arg1 arg2 " (trailing space for format convenience)
build_haskell_arg_list(0, "") :- !.
build_haskell_arg_list(N, ArgList) :-
    findall(ArgName, (
        between(1, N, I),
        format(string(ArgName), 'arg~w', [I])
    ), ArgNames),
    atomic_list_concat(ArgNames, ' ', ArgListNoTrail),
    format(string(ArgList), '~w ', [ArgListNoTrail]).

%% build_haskell_type_sig(+InputArity, -TypeSig)
%  Generates "Int -> Int -> String" (inputs are Int, output is String)
build_haskell_type_sig(0, "String") :- !.
build_haskell_type_sig(N, TypeSig) :-
    findall("Int", between(1, N, _), InputTypes),
    append(InputTypes, ["String"], AllTypes),
    atomic_list_concat(AllTypes, ' -> ', TypeSig).

%% native_haskell_clause_body(+PredSpec, +Clauses, -Code)

% Single clause
native_haskell_clause_body(PredSpec, [Head-Body], Code) :-
    native_haskell_clause(PredSpec, Head, Body, Condition, ClauseCode),
    !,
    (   Condition == "true"
    ->  Code = ClauseCode
    ;   format(string(Code),
'if ~w then ~w else error "No matching clause for ~w"', [Condition, ClauseCode, PredSpec])
    ).

% Multi-clause — use guards
native_haskell_clause_body(PredSpec, Clauses, Code) :-
    Clauses = [_|[_|_]],
    maplist(native_haskell_clause_pair(PredSpec), Clauses, Branches),
    Branches \= [],
    branches_to_haskell_guards(Branches, PredSpec, Code).

native_haskell_clause_pair(PredSpec, Head-Body, branch(Condition, ClauseCode)) :-
    native_haskell_clause(PredSpec, Head, Body, Condition, ClauseCode),
    !.

%% native_haskell_clause(+PredSpec, +Head, +Body, -Condition, -Code)
native_haskell_clause(_PredSpec, Head, Body, Condition, Code) :-
    Head =.. [_Pred|HeadArgs],
    length(HeadArgs, Arity),
    build_head_varmap(HeadArgs, 1, VarMap),
    (   Arity > 1
    ->  append(_InputHeadArgs, [OutputHeadArg], HeadArgs),
        haskell_head_conditions(HeadArgs, 1, Arity, HeadConditions)
    ;   OutputHeadArg = _,
        haskell_head_conditions(HeadArgs, 1, Arity, HeadConditions)
    ),
    normalize_goals(Body, Goals),
    (   Goals == []
    ->  haskell_resolve_value(VarMap, OutputHeadArg, OutputExpr),
        Code = OutputExpr,
        GoalConditions = []
    ;   (   Arity > 1, nonvar(OutputHeadArg)
        ->  clause_guard_output_split(Goals, VarMap, GuardGoals, OutputGoals),
            maplist(haskell_guard_condition(VarMap), GuardGoals, GoalConditions),
            (   OutputGoals == []
            ->  haskell_literal(OutputHeadArg, Code)
            ;   haskell_output_goals(OutputGoals, VarMap, Code)
            )
        ;   native_haskell_goal_sequence(Goals, VarMap, GoalConditions, Code)
        )
    ),
    append(HeadConditions, GoalConditions, AllConditions),
    combine_haskell_conditions(AllConditions, Condition).

%% haskell_head_conditions(+HeadArgs, +Index, +Arity, -Conditions)
haskell_head_conditions([], _, _, []).
haskell_head_conditions([_], _, Arity, []) :- Arity > 1, !.
haskell_head_conditions([HeadArg|Rest], Index, Arity, Conditions) :-
    (   var(HeadArg)
    ->  Conditions = RestConditions
    ;   format(string(ArgName), 'arg~w', [Index]),
        haskell_literal(HeadArg, Literal),
        format(string(Cond), '~w == ~w', [ArgName, Literal]),
        Conditions = [Cond|RestConditions]
    ),
    NextIndex is Index + 1,
    haskell_head_conditions(Rest, NextIndex, Arity, RestConditions).

%% native_haskell_goal_sequence(+Goals, +VarMap, -Conditions, -Code)
%  Uses classify_goal_sequence for advanced pattern detection.
%  Falls back to clause_guard_output_split if classification fails.
native_haskell_goal_sequence(Goals, VarMap, Conditions, Code) :-
    classify_goal_sequence(Goals, VarMap, ClassifiedGoals),
    ClassifiedGoals \= [],
    haskell_render_classified_goals(ClassifiedGoals, VarMap, Conditions, Lines),
    Lines \= [],
    atomic_list_concat(Lines, '\n', Code),
    !.
native_haskell_goal_sequence(Goals, VarMap, Conditions, Code) :-
    clause_guard_output_split(Goals, VarMap, GuardGoals, OutputGoals),
    maplist(haskell_guard_condition(VarMap), GuardGoals, Conditions),
    haskell_output_goals(OutputGoals, VarMap, Code).

%% haskell_render_classified_goals(+ClassifiedGoals, +VarMap, -Conditions, -Lines)
haskell_render_classified_goals([], _VarMap, [], []).
haskell_render_classified_goals([Classified], VarMap, Conds, Lines) :-
    !,
    haskell_render_classified_last(Classified, VarMap, Conds, Lines).
%% Guarded tail: output followed by guard(s)
haskell_render_classified_goals([output(Goal, _, _)|Rest], VarMap, [], Lines) :-
    Rest = [guard(_, _)|_],
    !,
    haskell_output_goal(Goal, VarMap, LetBinding, VarMap1),
    haskell_collect_trailing_guards(Rest, VarMap1, GuardGoals, _Remaining),
    maplist(haskell_guard_condition(VarMap1), GuardGoals, GuardConds),
    atomic_list_concat(GuardConds, ' && ', GuardExpr),
    (   goal_output_var(Goal, OutVar), lookup_var(OutVar, VarMap1, OutName)
    ->  true
    ;   OutName = '_'
    ),
    format(string(IfLine), 'if ~w then ~w else error "guard failed"', [GuardExpr, OutName]),
    Lines = [LetBinding, IfLine].
haskell_render_classified_goals([Classified|Rest], VarMap, Conds, Lines) :-
    haskell_render_classified_mid(Classified, VarMap, MidConds, MidLines, VarMap1),
    haskell_render_classified_goals(Rest, VarMap1, RestConds, RestLines),
    append(MidConds, RestConds, Conds),
    append(MidLines, RestLines, Lines).

%% haskell_render_classified_mid(+Classified, +VarMap, -Conds, -Lines, -VarMapOut)
haskell_render_classified_mid(guard(Goal, _), VarMap, [Cond], [], VarMap) :-
    haskell_guard_condition(VarMap, Goal, Cond).
haskell_render_classified_mid(output(Goal, _, _), VarMap0, [], [Line], VarMapOut) :-
    haskell_output_goal(Goal, VarMap0, Line, VarMapOut).
haskell_render_classified_mid(output_ite(If, Then, Else, _SharedVars), VarMap0, [], [Line], VarMap0) :-
    haskell_guard_condition(VarMap0, If, Cond),
    haskell_branch_value(Then, VarMap0, ThenExpr),
    haskell_branch_value(Else, VarMap0, ElseExpr),
    format(string(Line), 'if ~w then ~w else ~w', [Cond, ThenExpr, ElseExpr]).
haskell_render_classified_mid(passthrough(Goal), VarMap0, [], [Line], VarMapOut) :-
    haskell_output_goal(Goal, VarMap0, Line, VarMapOut).
haskell_render_classified_mid(_, VarMap, [], [], VarMap).

%% haskell_render_classified_last(+Classified, +VarMap, -Conds, -Lines)
haskell_render_classified_last(guard(Goal, _), VarMap, [Cond], []) :-
    haskell_guard_condition(VarMap, Goal, Cond).
haskell_render_classified_last(output(Goal, _, _), VarMap, [], Lines) :-
    haskell_render_output_goal_last(Goal, VarMap, Lines).
haskell_render_classified_last(output_ite(If, Then, Else, _), VarMap, [], [Line]) :-
    haskell_guard_condition(VarMap, If, Cond),
    haskell_branch_value(Then, VarMap, ThenExpr),
    haskell_branch_value(Else, VarMap, ElseExpr),
    format(string(Line), 'if ~w then ~w else ~w', [Cond, ThenExpr, ElseExpr]).
haskell_render_classified_last(output_disj(Alternatives, _SharedVars), VarMap, [], Lines) :-
    haskell_disj_if_chain(Alternatives, VarMap, Lines).
haskell_render_classified_last(passthrough(Goal), VarMap, [], Lines) :-
    haskell_render_output_goal_last(Goal, VarMap, Lines).
haskell_render_classified_last(_, _, [], []).

%% haskell_render_output_goal_last(+Goal, +VarMap, -Lines)
%  3-arg wrapper used by classified goal renderers.
haskell_render_output_goal_last(Goal, VarMap, [Expr]) :-
    haskell_output_goal_last(Goal, VarMap, Expr, _VarMapOut),
    !.
haskell_render_output_goal_last(Goal, VarMap, [Expr]) :-
    haskell_branch_value(Goal, VarMap, Expr).

%% haskell_collect_trailing_guards(+ClassifiedGoals, +VarMap, -GuardGoals, -Remaining)
haskell_collect_trailing_guards([guard(Goal, _)|Rest], VarMap, [Goal|Guards], Remaining) :-
    !, haskell_collect_trailing_guards(Rest, VarMap, Guards, Remaining).
haskell_collect_trailing_guards(Remaining, _, [], Remaining).

%% haskell_disj_if_chain(+Alternatives, +VarMap, -Lines)
haskell_disj_if_chain([], _, []).
haskell_disj_if_chain([Alt], VarMap, [Line]) :-
    !,
    haskell_branch_value(Alt, VarMap, ValExpr),
    format(string(Line), 'else ~w', [ValExpr]).
haskell_disj_if_chain([Alt|Rest], VarMap, [Line|RestLines]) :-
    normalize_goals(Alt, Goals),
    clause_guard_output_split(Goals, VarMap, Guards, _Outputs),
    (   Guards \= []
    ->  maplist(haskell_guard_condition(VarMap), Guards, CondStrs),
        atomic_list_concat(CondStrs, ' && ', CondExpr)
    ;   CondExpr = 'True'
    ),
    haskell_branch_value(Alt, VarMap, ValExpr),
    format(string(Line), 'if ~w then ~w', [CondExpr, ValExpr]),
    haskell_disj_elif_chain(Rest, VarMap, RestLines).

%% haskell_disj_elif_chain(+Alternatives, +VarMap, -Lines)
haskell_disj_elif_chain([], _, []).
haskell_disj_elif_chain([Alt], VarMap, [Line]) :-
    !,
    haskell_branch_value(Alt, VarMap, ValExpr),
    format(string(Line), 'else ~w', [ValExpr]).
haskell_disj_elif_chain([Alt|Rest], VarMap, [Line|RestLines]) :-
    normalize_goals(Alt, Goals),
    clause_guard_output_split(Goals, VarMap, Guards, _Outputs),
    (   Guards \= []
    ->  maplist(haskell_guard_condition(VarMap), Guards, CondStrs),
        atomic_list_concat(CondStrs, ' && ', CondExpr)
    ;   CondExpr = 'True'
    ),
    haskell_branch_value(Alt, VarMap, ValExpr),
    format(string(Line), 'else if ~w then ~w', [CondExpr, ValExpr]),
    haskell_disj_elif_chain(Rest, VarMap, RestLines).

%% haskell_guard_condition(+VarMap, +Goal, -Condition)
haskell_guard_condition(VarMap, _Module:Goal, Condition) :-
    !, haskell_guard_condition(VarMap, Goal, Condition).
haskell_guard_condition(VarMap, Goal, Condition) :-
    compound(Goal),
    Goal =.. [Op, Left, Right],
    expr_op(Op, StdOp),
    !,
    haskell_expr(Left, VarMap, HsLeft),
    haskell_expr(Right, VarMap, HsRight),
    haskell_op(StdOp, HsOp),
    format(string(Condition), '~w ~w ~w', [HsLeft, HsOp, HsRight]).

%% haskell_output_goals(+Goals, +VarMap, -Code)
haskell_output_goals([], _VarMap, 'error "no output"') :- !.
haskell_output_goals(Goals, VarMap, Code) :-
    haskell_output_goals_acc(Goals, VarMap, Parts, _VarMapOut),
    (   Parts = [SingleExpr]
    ->  Code = SingleExpr
    ;   % Multiple outputs: use let..in
        last(Parts, ResultExpr),
        append(LetParts, [_], Parts),
        atomic_list_concat(LetParts, '; ', LetBindings),
        format(string(Code), 'let ~w in ~w', [LetBindings, ResultExpr])
    ).

haskell_output_goals_acc([], VarMap, [], VarMap).
haskell_output_goals_acc([Goal], VarMap, [Expr], VarMapOut) :-
    !, haskell_output_goal_last(Goal, VarMap, Expr, VarMapOut).
haskell_output_goals_acc([Goal|Rest], VarMap0, [Binding|RestParts], VarMapOut) :-
    haskell_output_goal(Goal, VarMap0, Binding, VarMap1),
    haskell_output_goals_acc(Rest, VarMap1, RestParts, VarMapOut).

%% haskell_output_goal_last — produce the result expression
haskell_output_goal_last(_Module:Goal, VarMap, Expr, VarMapOut) :-
    !, haskell_output_goal_last(Goal, VarMap, Expr, VarMapOut).
haskell_output_goal_last(Goal, VarMap0, Expr, VarMapOut) :-
    if_then_else_goal(Goal, IfGoal, ThenGoal, ElseGoal),
    !,
    haskell_if_then_else_output(IfGoal, ThenGoal, ElseGoal, VarMap0, Expr, VarMapOut).
haskell_output_goal_last(=(Var, Value), VarMap, Expr, VarMap) :-
    var(Var), !,
    haskell_expr(Value, VarMap, Expr).
haskell_output_goal_last(is(Var, Value), VarMap, Expr, VarMap) :-
    var(Var), !,
    haskell_expr(Value, VarMap, Expr).
haskell_output_goal_last(Goal, VarMap0, Expr, VarMapOut) :-
    haskell_output_goal(Goal, VarMap0, Binding, VarMapOut),
    (   goal_output_var(Goal, OutVar),
        lookup_var(OutVar, VarMapOut, OutName)
    ->  format(string(Expr), 'let ~w in ~w', [Binding, OutName])
    ;   Expr = Binding
    ).

%% haskell_output_goal — produce a let binding
haskell_output_goal(_Module:Goal, VarMap0, Binding, VarMapOut) :-
    !, haskell_output_goal(Goal, VarMap0, Binding, VarMapOut).
haskell_output_goal(=(Var, Value), VarMap0, Binding, VarMapOut) :-
    var(Var), !,
    ensure_var(VarMap0, Var, VarName, VarMapOut),
    haskell_expr(Value, VarMap0, HsExpr),
    format(string(Binding), '~w = ~w', [VarName, HsExpr]).
haskell_output_goal(is(Var, Value), VarMap0, Binding, VarMapOut) :-
    var(Var), !,
    ensure_var(VarMap0, Var, VarName, VarMapOut),
    haskell_expr(Value, VarMap0, HsExpr),
    format(string(Binding), '~w = ~w', [VarName, HsExpr]).

%% haskell_if_then_else_output — generate if/then/else expression
haskell_if_then_else_output(IfGoal, ThenGoal, ElseGoal, VarMap0, Expr, VarMapOut) :-
    flatten_haskell_if_branches(IfGoal, ThenGoal, ElseGoal, Branches, DefaultGoal),
    haskell_guard_condition(VarMap0, IfGoal, _),  % verify guards translate
    haskell_branches_to_expr(Branches, DefaultGoal, VarMap0, Expr),
    VarMapOut = VarMap0.

flatten_haskell_if_branches(If, Then, Else, [branch(If, Then)|RestBranches], Default) :-
    if_then_else_goal(Else, If2, Then2, Else2),
    !,
    flatten_haskell_if_branches(If2, Then2, Else2, RestBranches, Default).
flatten_haskell_if_branches(If, Then, Else, [branch(If, Then)], Else).

haskell_branches_to_expr([branch(If, Then)], DefaultGoal, VarMap, Expr) :-
    !,
    haskell_guard_condition(VarMap, If, IfCond),
    haskell_branch_value(Then, VarMap, ThenVal),
    haskell_branch_value(DefaultGoal, VarMap, ElseVal),
    format(string(Expr), 'if ~w then ~w else ~w', [IfCond, ThenVal, ElseVal]).
haskell_branches_to_expr(Branches, DefaultGoal, VarMap, Expr) :-
    % Multiple branches: use nested if/then/else
    haskell_nested_if_expr(Branches, DefaultGoal, VarMap, Expr).

haskell_nested_if_expr([branch(If, Then)], DefaultGoal, VarMap, Expr) :-
    !,
    haskell_guard_condition(VarMap, If, IfCond),
    haskell_branch_value(Then, VarMap, ThenVal),
    haskell_branch_value(DefaultGoal, VarMap, ElseVal),
    format(string(Expr), 'if ~w then ~w else ~w', [IfCond, ThenVal, ElseVal]).
haskell_nested_if_expr([branch(If, Then)|Rest], DefaultGoal, VarMap, Expr) :-
    haskell_guard_condition(VarMap, If, IfCond),
    haskell_branch_value(Then, VarMap, ThenVal),
    haskell_nested_if_expr(Rest, DefaultGoal, VarMap, ElseExpr),
    format(string(Expr), 'if ~w then ~w else ~w', [IfCond, ThenVal, ElseExpr]).

%% haskell_branch_value — extract result value from a branch
haskell_branch_value(_Module:Goal, VarMap, Value) :-
    !, haskell_branch_value(Goal, VarMap, Value).
haskell_branch_value(Goal, VarMap, Value) :-
    if_then_else_goal(Goal, If, Then, Else),
    !,
    haskell_guard_condition(VarMap, If, Cond),
    haskell_branch_value(Then, VarMap, ThenVal),
    haskell_branch_value(Else, VarMap, ElseVal),
    format(string(Value), 'if ~w then ~w else ~w', [Cond, ThenVal, ElseVal]).
haskell_branch_value((A, B), VarMap, Value) :-
    !,
    normalize_goals((A, B), Goals),
    last(Goals, LastGoal),
    haskell_branch_value(LastGoal, VarMap, Value).
haskell_branch_value(=(_, Expr), VarMap, Value) :-
    !, haskell_expr(Expr, VarMap, Value).
haskell_branch_value(is(_, Expr), VarMap, Value) :-
    !, haskell_expr(Expr, VarMap, Value).
haskell_branch_value(Goal, VarMap, Value) :-
    haskell_expr(Goal, VarMap, Value).

