% Try native clause body lowering first
compile_predicate_to_haskell(Pred/Arity, Options, Code) :-
    functor(Head, Pred, Arity),
    findall(Head-Body, user:clause(Head, Body), Clauses),
    Clauses \= [],
    native_haskell_clause_body(Pred/Arity, Clauses, FuncBody),
    !,
    atom_string(Pred, PredStr),
    capitalize(PredStr, ModCap),
    Arity1 is Arity - 1,
    build_haskell_arg_list(Arity1, ArgList),
    build_haskell_type_sig(Arity1, TypeSig),
    option(module_name(ModName), Options, ModCap),
    capitalize(ModName, ModCapName),
    % Detect if body uses guards (starts with "  |") or is a direct expression
    (   sub_string(FuncBody, 0, _, _, "  |")
    ->  format(string(Code),
'-- Generated by UnifyWeaver Haskell Target - Native Clause Lowering
module ~w where

~w :: ~w
~w ~w
~w
', [ModCapName, PredStr, TypeSig, PredStr, ArgList, FuncBody])
    ;   format(string(Code),
'-- Generated by UnifyWeaver Haskell Target - Native Clause Lowering
module ~w where

~w :: ~w
~w ~w= ~w
', [ModCapName, PredStr, TypeSig, PredStr, ArgList, FuncBody])
    ).

% Fallback to existing dispatch
compile_predicate_to_haskell(Pred/Arity, Options, Code) :-
    option(type(Type), Options, facts),
    (   Type == facts
    ->  compile_facts_to_haskell(Pred, Arity, Code)
    ;   Type == recursion
    ->  compile_recursion_to_haskell(Pred/Arity, Options, Code)
    ;   Type == rules
    ->  compile_rules_to_haskell(Pred/Arity, Options, Code)
    ;   compile_facts_to_haskell(Pred, Arity, Code)
    ).

%% ============================================
%% FACTS → LIST OF TUPLES
