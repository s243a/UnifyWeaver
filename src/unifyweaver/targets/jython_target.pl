    ->  expr_to_jython(X, JX), expr_to_jython(Y, JY),
        format(atom(JythonExpr), "(~w / ~w)", [JX, JY])
    ;   Expr = X mod Y
    ->  expr_to_jython(X, JX), expr_to_jython(Y, JY),
        format(atom(JythonExpr), "(~w % ~w)", [JX, JY])
    ;   format(atom(JythonExpr), "~w", [Expr])
