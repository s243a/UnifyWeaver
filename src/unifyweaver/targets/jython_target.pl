% Multi-clause → if/elif/else
native_jython_clause_body(PredSpec, Clauses, Code) :-
    Clauses = [_|[_|_]],
    maplist(native_jython_clause_pair(PredSpec), Clauses, Branches),
    Branches \= [],
    branches_to_jython_if_chain(Branches, PredSpec, Code).

