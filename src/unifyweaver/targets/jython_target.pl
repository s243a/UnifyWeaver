%% branches_to_jython_if_chain(+Branches, +PredSpec, -Code)
branches_to_jython_if_chain(Branches, PredSpec, Code) :-
    jython_if_chain_lines(Branches, PredSpec, true, Lines),
    atomic_list_concat(Lines, '\n', Code).

jython_if_chain_lines([branch(Cond, ClauseCode)], PredSpec, IsFirst, Lines) :-
    !,
    (   Cond == "True"
    ->  (   IsFirst == true
        ->  format(string(Line), '    return ~w', [ClauseCode]),
            Lines = [Line]
        ;   format(string(Line), '    else:~n        return ~w', [ClauseCode]),
            Lines = [Line]
        )
    ;   (   IsFirst == true
        ->  format(string(L1), '    if ~w:', [Cond]),
            format(string(L2), '        return ~w', [ClauseCode]),
            format(string(L3), '    raise ValueError("No matching clause for ~w")', [PredSpec]),
            Lines = [L1, L2, L3]
        ;   format(string(L1), '    elif ~w:', [Cond]),
            format(string(L2), '        return ~w', [ClauseCode]),
            format(string(L3), '    raise ValueError("No matching clause for ~w")', [PredSpec]),
            Lines = [L1, L2, L3]
        )
    ).
jython_if_chain_lines([branch(Cond, ClauseCode)|Rest], PredSpec, IsFirst, [L1, L2|RestLines]) :-
    (   IsFirst == true
    ->  format(string(L1), '    if ~w:', [Cond])
    ;   format(string(L1), '    elif ~w:', [Cond])
    ),
    format(string(L2), '        return ~w', [ClauseCode]),
    jython_if_chain_lines(Rest, PredSpec, false, RestLines).
