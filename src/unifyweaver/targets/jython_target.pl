%% compile_predicate_to_jython(+Predicate, +Options, -JythonCode)
%  Compile a Prolog predicate to Jython code
%
%  @arg Predicate Predicate indicator (Name/Arity)
%  @arg Options List of options
%  @arg JythonCode Generated Jython code as atom
%
%  Options:
%  - pipeline_input(true|false) - Enable streaming input
%  - generator_mode(true|false) - Enable generator mode with yield
%  - java_imports(List) - Java classes to import
%
compile_predicate_to_jython(PredIndicator, Options, JythonCode) :-
    (   PredIndicator = _Module:Pred/Arity
    ->  true
    ;   PredIndicator = Pred/Arity
    ),
    format('=== Compiling ~w/~w to Jython ===~n', [Pred, Arity]),

    % Clear any previously collected imports
    clear_jython_imports,

    % Check for mode
    (   option(generator_mode(true), Options)
    ->  format('  Mode: Generator (yield)~n'),
        compile_generator_mode_jython(Pred, Arity, Options, JythonCode)
    ;   option(pipeline_input(true), Options)
    ->  format('  Mode: Pipeline (streaming JSONL)~n'),
        compile_pipeline_mode_jython(Pred, Arity, Options, JythonCode)
    % Default: simple predicate
    ;   format('  Mode: Simple predicate~n'),
        compile_simple_mode_jython(Pred, Arity, Options, JythonCode)
    ).

%% ============================================
%% COMPILE FACTS TO JYTHON
%% ============================================

compile_facts_to_jython(Pred, Arity, JythonCode) :-
    atom_string(Pred, PredStr),
    upcase_atom(Pred, PredCap),
    functor(Head, Pred, Arity),
    
    findall(Args, (clause(Head, true), Head =.. [_|Args]), AllFacts),
    
    (   AllFacts == []
    ->  FactEntries = "    # No facts defined"
    ;   findall(Entry, (
            member(Args, AllFacts),
            format_jython_fact_entry(Args, Entry)
        ), Entries),
        atomic_list_concat(Entries, ',\n', FactEntries)
    ),
    
    format(string(JythonCode),
'# Generated by UnifyWeaver Jython Target - Facts Export
# Predicate: ~w/~w

class ~w:
    \"\"\"~w facts as Python data\"\"\"
    
    facts = [
~w
    ]

    @classmethod
    def get_all(cls):
        return cls.facts

    @classmethod
    def stream(cls):
        for fact in cls.facts:
            yield fact

    @classmethod
    def contains(cls, *args):
        return list(args) in cls.facts

if __name__ == \"__main__\":
    for f in ~w.facts:
        print(\":\".join(f))
', [PredStr, Arity, PredCap, PredStr, FactEntries, PredCap]).

format_jython_fact_entry(Args, Entry) :-
    findall(Formatted, (
        member(Arg, Args),
        format(string(Formatted), '"~w"', [Arg])
    ), FormattedArgs),
    atomic_list_concat(FormattedArgs, ', ', ArgsStr),
    format(string(Entry), '        [~w]', [ArgsStr]).

%% ============================================
%% GENERATOR MODE (Python generators with yield)
%% ============================================

compile_generator_mode_jython(Pred, Arity, Options, JythonCode) :-
    % Standard imports for generator mode
    collect_jython_import(sys),
    collect_jython_import(json),
    
    % Java imports for performance
    (   option(use_java_json(true), Options)
    ->  collect_jython_import('com.google.gson.Gson')
    ;   true
    ),
    
    % Gather clauses
    functor(Head, Pred, Arity),
    findall((Head, Body), clause(Head, Body), Clauses),
    
    % Generate generator body
    (   Clauses == []
    ->  GeneratorBody = "    # No clauses found - yield input unchanged\n    yield record"
    ;   generate_generator_body_jython(Clauses, GeneratorBody)
    ),
    
    get_jython_imports(ImportList),
    format_jython_imports(ImportList, ImportsStr),
    
    format(string(JythonCode),
"#!/usr/bin/env jython
# -*- coding: utf-8 -*-
# Generated by UnifyWeaver Jython Target - Generator Mode
# Predicate: ~w/~w
# Uses Python generators for lazy evaluation

from __future__ import print_function
~w
from java.io import BufferedReader, InputStreamReader
from java.lang import System as JavaSystem

def process(record):
    '''
    Process a single record, yielding zero or more results.
    
    Args:
        record: Input record as dict
        
    Yields:
        Output records (dicts)
    '''
~w

def process_all(records):
    '''Process all records, flattening results.'''
    for record in records:
        for result in process(record):
            yield result

def read_jsonl(stream):
    '''Read JSONL from input stream.'''
    reader = BufferedReader(InputStreamReader(stream))
    line = reader.readLine()
    while line is not None:
        if line.strip():
            try:
                yield json.loads(line)
            except ValueError as e:
                print >> sys.stderr, 'JSON parse error:', e
        line = reader.readLine()

def write_jsonl(record):
    '''Write record as JSONL.'''
    print(json.dumps(record))

def run_pipeline():
    '''Run the generator pipeline.'''
    for result in process_all(read_jsonl(JavaSystem.in)):
        write_jsonl(result)

if __name__ == '__main__':
    run_pipeline()
", [Pred, Arity, ImportsStr, GeneratorBody]).

%% generate_generator_body_jython(+Clauses, -Code)
generate_generator_body_jython(Clauses, Code) :-
    Clauses = [(Head, _)|_],
    functor(Head, Name, _),
    (   is_recursive_predicate_jython(Name, Clauses)
    ->  partition(is_recursive_clause_jython(Name), Clauses, RecClauses, BaseClauses),
        compile_generator_recursive_jython(Name, BaseClauses, RecClauses, Code)
    ;   % Non-recursive: yield with transformations
        Code = "    # Non-recursive: pass through with transformations\n    yield record"
    ).

compile_generator_recursive_jython(Name, BaseClauses, _RecClauses, Code) :-
    (   BaseClauses = [(BaseHead, _)|_]
    ->  generate_base_condition_jython(BaseHead, BaseCondition)
    ;   BaseCondition = "False"
    ),
    
    format(string(Code),
"    # Recursive generator: ~w
    current = dict(record)
    max_iterations = 10000
    
    for _ in xrange(max_iterations):
        # Yield current state
        yield dict(current)
        
        # Check base case - stop generating
        if ~w:
            return
        
        # Transform for next iteration (placeholder)
        pass
    
    print >> sys.stderr, 'Warning: Max iterations exceeded for ~w'", [Name, BaseCondition, Name]).

