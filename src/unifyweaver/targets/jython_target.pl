%% jython_expr — convert Prolog expression to Jython syntax
jython_expr(Var, VarMap, JExpr) :-
    var(Var), !,
    (   lookup_var(Var, VarMap, Name)
    ->  atom_string(Name, JExpr)
    ;   JExpr = "0"
    ).
jython_expr(Expr, VarMap, JExpr) :-
    compound(Expr),
    Expr =.. [Op, Left, Right],
    expr_op(Op, StdOp),
    !,
    jython_expr(Left, VarMap, JLeft),
    jython_expr(Right, VarMap, JRight),
    jython_op(StdOp, JOp),
    format(string(JExpr), '(~w ~w ~w)', [JLeft, JOp, JRight]).
jython_expr(-Expr, VarMap, JExpr) :-
    !,
    jython_expr(Expr, VarMap, Inner),
    format(string(JExpr), '(-~w)', [Inner]).
jython_expr(abs(Expr), VarMap, JExpr) :-
    !,
    jython_expr(Expr, VarMap, Inner),
    format(string(JExpr), 'abs(~w)', [Inner]).
jython_expr(Atom, _VarMap, JExpr) :-
    atom(Atom), !,
    jython_literal(Atom, JExpr).
jython_expr(Number, _VarMap, JExpr) :-
    number(Number), !,
    format(string(JExpr), '~w', [Number]).

