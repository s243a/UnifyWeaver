%% snake_to_pascal_java(+SnakeStr, -PascalStr)
%  Convert snake_case string to PascalCase for Java class names.
%  e.g. "test_sum" -> "TestSum", "factorial" -> "Factorial"
snake_to_pascal_java(SnakeStr, PascalStr) :-
    split_string(SnakeStr, "_", "", Parts),
    maplist(capitalize_first, Parts, CapParts),
    atomics_to_string(CapParts, PascalStr).

capitalize_first("", "") :- !.
capitalize_first(Str, Cap) :-
    string_code(1, Str, Code),
    (   Code >= 0'a, Code =< 0'z ->
        Upper is Code - 32,
        sub_string(Str, 1, _, 0, Rest),
        char_code(UpperChar, Upper),
        atom_string(UpperChar, UpperStr),
        string_concat(UpperStr, Rest, Cap)
    ;   Cap = Str
    ).

atomics_to_string(Parts, Str) :-
    atomic_list_concat(Parts, '', Atom),
    atom_string(Atom, Str).

