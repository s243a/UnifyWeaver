mutual_functions_java([], _AllPreds, _MemoEnabled, []).
mutual_functions_java([Pred/Arity|Rest], AllPreds, MemoEnabled, [FuncCode|RestCodes]) :-
    atom_string(Pred, PredStr),
    functor(Head, Pred, Arity),
    findall(clause(Head, Body), user:clause(Head, Body), Clauses),
    partition(mutual_recursion:is_mutual_recursive_clause(AllPreds), Clauses, RecClauses, BaseClauses),
    % Base cases
    findall(BaseLine, (
        member(clause(BHead, true), BaseClauses),
        BHead =.. [_P, BValue],
        (   MemoEnabled = true ->
            format(string(BaseLine),
'        if (n == ~w) { memo.put(key, true); return true; }', [BValue])
        ;   format(string(BaseLine),
'        if (n == ~w) return true;', [BValue])
        )
    ), BaseLines),
    atomic_list_concat(BaseLines, '\n', BaseCode),
    % Recursive case
    (   RecClauses = [clause(_RHead, RBody)|_] ->
        extract_mutual_rec_info_java(RBody, Guard, CalledPred, Step),
        atom_string(CalledPred, CalledStr),
        (   Guard = (N > Threshold), var(N) ->
            (   MemoEnabled = true ->
                format(string(RecCode),
'        if (n > ~w) {
            boolean result = ~w(n ~w);
            memo.put(key, result);
            return result;
        }', [Threshold, CalledStr, Step])
            ;   format(string(RecCode),
'        if (n > ~w) return ~w(n ~w);', [Threshold, CalledStr, Step])
            )
        ;   format(string(RecCode),
'        return ~w(n ~w);', [CalledStr, Step])
        )
    ;   RecCode = ''
    ),
    (   MemoEnabled = true ->
        format(string(FuncCode),
'    public static boolean ~w(int n) {
        String key = "~w:" + n;
        if (memo.containsKey(key)) return memo.get(key);
~w
~w
        return false;
    }', [PredStr, PredStr, BaseCode, RecCode])
    ;   format(string(FuncCode),
'    public static boolean ~w(int n) {
~w
~w
        return false;
    }', [PredStr, BaseCode, RecCode])
    ),
    mutual_functions_java(Rest, AllPreds, MemoEnabled, RestCodes).

extract_mutual_rec_info_java(Body, Guard, CalledPred, Step) :-
    extract_goals_from_body_java(Body, Goals),
    (   member(Guard, Goals), Guard = (_ > _) -> true
    ;   Guard = none
    ),
    member(Call, Goals),
    compound(Call),
    Call \= (_ is _), Call \= (_ > _), Call \= (_ < _),
    Call \= (_ >= _), Call \= (_ =< _),
    functor(Call, CalledPred, _),
    (   member(_ is _ - K, Goals), integer(K) ->
        format(string(Step), '- ~w', [K])
    ;   member(_ is _ + K, Goals), integer(K), K < 0 ->
        AbsK is abs(K),
        format(string(Step), '- ~w', [AbsK])
    ;   Step = "- 1"
    ).

extract_goals_from_body_java((A, B), Goals) :- !,
    extract_goals_from_body_java(A, GA),
    extract_goals_from_body_java(B, GB),
    append(GA, GB, Goals).
extract_goals_from_body_java(true, []) :- !.
extract_goals_from_body_java(Goal, [Goal]).

mutual_dispatch_java(Predicates, Code) :-
    findall(CaseLine, (
        member(Pred/_Arity, Predicates),
        atom_string(Pred, PredStr),
        format(string(CaseLine),
'                case "~w": System.out.println(~w(n)); break;', [PredStr, PredStr])
    ), Lines),
    atomic_list_concat(Lines, '\n', CaseCode),
    format(string(Code),
'    public static void main(String[] args) {
        if (args.length >= 2) {
            int n = Integer.parseInt(args[1]);
            switch (args[0]) {
~w
            }
        }
    }', [CaseCode]).
