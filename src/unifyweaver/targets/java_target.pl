%% compile_facts_to_java(+Pred, +Arity, -JavaCode)
%  Compile facts to a Java class with static data
%  Matches stream_compiler:compile_facts for Bash
compile_facts_to_java(Pred, Arity, JavaCode) :-
    atom_string(Pred, PredStr),
    upcase_atom(Pred, PredCap),
    functor(Head, Pred, Arity),
    
    % Gather all facts
    findall(Args, (clause(Head, true), Head =.. [_|Args]), AllFacts),
    
    % Generate Java array entries
    (   AllFacts == []
    ->  FactEntries = "        // No facts defined"
    ;   findall(Entry, (
            member(Args, AllFacts),
            format_java_fact_entry(Args, Entry)
        ), Entries),
        atomic_list_concat(Entries, ',\n', FactEntries)
    ),
    
    % Generate arity-specific types
    generate_java_fact_types(Arity, RecordType, StreamReturnType),
    
    % Generate formatter code
    generate_fact_formatter(Arity, FormatterCode),
    
    format(string(JavaCode),
'// Generated by UnifyWeaver Java Target - Facts Export
// Predicate: ~w/~w

package generated;

import java.util.*;
import java.util.stream.*;

/**
 * ~w facts as static Java data
 */
public class ~w {

    // Fact storage
    private static final List<~w> facts = Arrays.asList(
~w
    );

    /**
     * Get all facts as a stream
     */
    public static ~w stream() {
        return facts.stream();
    }

    /**
     * Get all facts as a list
     */
    public static List<~w> getAll() {
        return Collections.unmodifiableList(facts);
    }

    /**
     * Check if a fact exists
     */
    public static boolean contains(~w args) {
        return facts.contains(args);
    }

    /**
     * Print all facts in colon-separated format
     */
    public static void main(String[] args) {
        stream().forEach(f -> System.out.println(formatFact(f)));
    }

    private static String formatFact(~w fact) {
        ~w
    }
}
', [PredStr, Arity, PredStr, PredCap, RecordType, FactEntries, StreamReturnType, 
    RecordType, RecordType, RecordType, FormatterCode]).

%% format_java_fact_entry(+Args, -Entry)
%  Format arguments as a Java tuple/array entry
format_java_fact_entry(Args, Entry) :-
    findall(Formatted, (
        member(Arg, Args),
        format_java_value(Arg, Formatted)
    ), FormattedArgs),
    (   length(Args, 2)
    ->  FormattedArgs = [A1, A2],
        format(string(Entry), '        new String[]{~w, ~w}', [A1, A2])
    ;   atomic_list_concat(FormattedArgs, ', ', ArgsStr),
        format(string(Entry), '        new String[]{~w}', [ArgsStr])
    ).

format_java_value(Arg, Formatted) :-
    (   atom(Arg)
    ->  format(string(Formatted), '"~w"', [Arg])
    ;   number(Arg)
    ->  format(string(Formatted), '"~w"', [Arg])
    ;   format(string(Formatted), '"~w"', [Arg])
    ).

%% generate_java_fact_types(+Arity, -RecordType, -StreamReturnType)
generate_java_fact_types(1, "String", "Stream<String>").
generate_java_fact_types(2, "String[]", "Stream<String[]>").
generate_java_fact_types(N, "String[]", "Stream<String[]>") :- N > 2.

%% generate_fact_formatter(+Arity, -FormatterCode)
generate_fact_formatter(1, "return fact;").
generate_fact_formatter(2, "return fact[0] + \":\" + fact[1];").
generate_fact_formatter(N, "return String.join(\":\", fact);") :- N > 2.

%% ============================================
%% GENERATOR MODE (Java Stream.iterate/flatMap)
%% ============================================

compile_generator_mode_java(Pred, Arity, Options, JavaCode) :-
    % Standard imports for generator mode
    collect_java_import('java.util.*'),
    collect_java_import('java.util.stream.*'),
    collect_java_import('java.io.*'),
    collect_java_import('com.google.gson.Gson'),
    collect_java_import('com.google.gson.reflect.TypeToken'),
    collect_java_import('java.lang.reflect.Type'),
    
    option(package(Package), Options, generated),
    capitalize_atom(Pred, CapPred),
    
    % Gather clauses
    functor(Head, Pred, Arity),
    findall((Head, Body), clause(Head, Body), Clauses),
    
    % Generate process function body
    (   Clauses == []
    ->  ProcessBody = "        // No clauses found - yield input unchanged\n        return Stream.of(record);"
    ;   generate_generator_body_java(Clauses, ProcessBody)
    ),
    
    get_java_imports(ImportList),
    format_java_imports(ImportList, ImportsStr),
    
    format(string(JavaCode),
"// Generated by UnifyWeaver Java Target - Generator Mode
// Predicate: ~w/~w
// Uses Java Stream.flatMap for generator semantics

package ~w;

~w

/**
 * Generator for ~w predicate.
 * Uses Java Streams for lazy, memory-efficient processing.
 */
public class ~wGenerator {

    private static final Gson gson = new Gson();
    private static final Type MAP_TYPE = new TypeToken<Map<String, Object>>(){}.getType();

    /**
     * Process a single record, returning zero or more results.
     * @param record Input record
     * @return Stream of output records
     */
    public static Stream<Map<String, Object>> process(Map<String, Object> record) {
~w
    }

    /**
     * Process all records, flattening results.
     */
    public static Stream<Map<String, Object>> processAll(Stream<Map<String, Object>> records) {
        return records.flatMap(~wGenerator::process);
    }

    /**
     * Parse a JSON line to a Map.
     */
    private static Map<String, Object> parseJson(String line) {
        try {
            return gson.fromJson(line, MAP_TYPE);
        } catch (Exception e) {
            System.err.println(\"JSON parse error: \" + e.getMessage());
            return null;
        }
    }

    /**
     * Run the generator pipeline.
     */
    public static void runPipeline() throws IOException {
        BufferedReader reader = new BufferedReader(new InputStreamReader(System.in));
        
        reader.lines()
            .filter(line -> !line.isBlank())
            .map(~wGenerator::parseJson)
            .filter(Objects::nonNull)
            .flatMap(~wGenerator::process)
            .forEach(result -> System.out.println(gson.toJson(result)));
    }

    public static void main(String[] args) throws IOException {
        runPipeline();
    }
}
", [Pred, Arity, Package, ImportsStr, Pred, CapPred, ProcessBody, CapPred, CapPred, CapPred]).

%% generate_generator_body_java(+Clauses, -Code)
generate_generator_body_java(Clauses, Code) :-
    Clauses = [(Head, _)|_],
    functor(Head, Name, _),
    (   is_recursive_predicate(Name, Clauses)
    ->  % For recursive predicates, use Stream.iterate
        partition(is_recursive_clause(Name), Clauses, RecClauses, BaseClauses),
        compile_generator_recursive_java(Name, BaseClauses, RecClauses, Code)
    ;   % Non-recursive: return Stream.of(record)
        Code = "        // Non-recursive: pass through with transformations\n        return Stream.of(record);"
    ).

compile_generator_recursive_java(Name, BaseClauses, _RecClauses, Code) :-
    (   BaseClauses = [(BaseHead, _)|_]
    ->  generate_base_condition(BaseHead, BaseCondition)
    ;   BaseCondition = "false"
    ),
    
    format(string(Code),
"        // Recursive generator: ~w - using Stream.iterate
        return Stream.iterate(record, current -> {
            // Transform for next iteration
            return new HashMap<>(current);
        })
        .limit(10000)
        .takeWhile(current -> !(~w))
        .map(HashMap::new);", [Name, BaseCondition]).

