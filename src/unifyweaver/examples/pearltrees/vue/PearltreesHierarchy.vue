<template>
  <div class="pearltrees-hierarchy">
    <div class="toolbar">
      <h2>{{ title }}</h2>
      <div class="controls">
        <button @click="zoomIn" title="Zoom In">+</button>
        <button @click="zoomOut" title="Zoom Out">-</button>
        <button @click="zoomReset" title="Reset">1:1</button>
        <button @click="fitToContent" title="Fit">[ ]</button>
        <span class="zoom-indicator">{{ Math.round(currentScale * 100) }}%</span>
      </div>
    </div>

    <div class="viewport" ref="viewport">
      <svg
        ref="svgRef"
        :width="width"
        :height="height"
        :style="{ background: theme === 'dark' ? '#1a1a2e' : '#ffffff' }"
      />
    </div>

    <div v-if="selectedNode" class="node-details">
      <h3>{{ selectedNode.label }}</h3>
      <p v-if="selectedNode.url">
        <a :href="selectedNode.url" target="_blank">Open Link</a>
      </p>
      <p>Type: {{ selectedNode.type }}</p>
      <p>Depth: {{ selectedNode.depth }}</p>
    </div>

    <div v-if="loading" class="loading-overlay">
      <div class="spinner"></div>
      <p>Loading hierarchy...</p>
    </div>

    <div v-if="error" class="error-message">
      {{ error.message }}
    </div>
  </div>
</template>

<script setup lang="ts">
/**
 * PearltreesHierarchy.vue
 *
 * Vue 3 component for visualizing Pearltrees hierarchy.
 * Generated by UnifyWeaver from Prolog specifications.
 *
 * This component uses D3.js for force-directed graph layout
 * and provides interactive zoom, pan, and node selection.
 *
 * Usage:
 *   <PearltreesHierarchy
 *     :nodes="treeNodes"
 *     :edges="treeEdges"
 *     :width="800"
 *     :height="600"
 *     theme="light"
 *     @nodeClick="handleNodeClick"
 *   />
 */

import { ref, onMounted, onUnmounted, watch, computed, type PropType } from 'vue';
import * as d3 from 'd3';

// Types matching Prolog pearl_trees/5 structure
interface PearltreeNode {
  id: string;
  label: string;
  type: 'root' | 'hub' | 'branch' | 'leaf' | 'default';
  url?: string;
  depth?: number;
  x?: number;
  y?: number;
  fx?: number | null;
  fy?: number | null;
}

interface PearltreeEdge {
  source: string | PearltreeNode;
  target: string | PearltreeNode;
  type?: string;
}

interface Props {
  nodes: PearltreeNode[];
  edges: PearltreeEdge[];
  width?: number;
  height?: number;
  theme?: 'light' | 'dark';
  title?: string;
  apiEndpoint?: string;
}

const props = withDefaults(defineProps<Props>(), {
  nodes: () => [],
  edges: () => [],
  width: 800,
  height: 600,
  theme: 'light',
  title: 'Pearltrees Hierarchy'
});

const emit = defineEmits<{
  (e: 'nodeClick', node: PearltreeNode): void;
  (e: 'nodesLoaded', nodes: PearltreeNode[]): void;
  (e: 'error', error: Error): void;
}>();

// Refs
const svgRef = ref<SVGSVGElement | null>(null);
const viewport = ref<HTMLDivElement | null>(null);
const currentScale = ref(1.0);
const selectedNode = ref<PearltreeNode | null>(null);
const loading = ref(false);
const error = ref<Error | null>(null);

// D3 references
let simulation: d3.Simulation<PearltreeNode, PearltreeEdge> | null = null;
let zoom: d3.ZoomBehavior<SVGSVGElement, unknown> | null = null;
let contentGroup: d3.Selection<SVGGElement, unknown, null, undefined> | null = null;

// Theme colors - matches Prolog theme definitions
const themeColors = {
  light: {
    background: '#ffffff',
    nodeColors: {
      default: { fill: '#e8f4fc', stroke: '#4a90d9', text: '#333333' },
      root: { fill: '#4a90d9', stroke: '#2c5a8c', text: '#ffffff' },
      hub: { fill: '#6ab04c', stroke: '#4a904c', text: '#ffffff' },
      branch: { fill: '#f0932b', stroke: '#c07020', text: '#ffffff' },
      leaf: { fill: '#eb4d4b', stroke: '#cb2d2b', text: '#ffffff' }
    },
    edgeColor: '#666666'
  },
  dark: {
    background: '#1a1a2e',
    nodeColors: {
      default: { fill: '#2d3748', stroke: '#4a9ce9', text: '#e2e8f0' },
      root: { fill: '#5a9ce9', stroke: '#3c6a9c', text: '#ffffff' },
      hub: { fill: '#7ac05c', stroke: '#5aa05c', text: '#ffffff' },
      branch: { fill: '#ffaa4b', stroke: '#d08030', text: '#000000' },
      leaf: { fill: '#fb5d5b', stroke: '#db3d3b', text: '#ffffff' }
    },
    edgeColor: '#718096'
  }
};

const colors = computed(() => themeColors[props.theme]);

// Zoom controls
const zoomIn = () => {
  if (!svgRef.value || !zoom) return;
  d3.select(svgRef.value)
    .transition()
    .duration(300)
    .call(zoom.scaleBy, 1.3);
};

const zoomOut = () => {
  if (!svgRef.value || !zoom) return;
  d3.select(svgRef.value)
    .transition()
    .duration(300)
    .call(zoom.scaleBy, 0.7);
};

const zoomReset = () => {
  if (!svgRef.value || !zoom) return;
  d3.select(svgRef.value)
    .transition()
    .duration(300)
    .call(zoom.transform, d3.zoomIdentity);
};

const fitToContent = () => {
  if (!svgRef.value || !zoom || !contentGroup) return;

  const bounds = contentGroup.node()?.getBBox();
  if (!bounds || bounds.width === 0 || bounds.height === 0) return;

  const scale = Math.min(
    (props.width - 100) / bounds.width,
    (props.height - 100) / bounds.height,
    5
  );

  const tx = (props.width - bounds.width * scale) / 2 - bounds.x * scale;
  const ty = (props.height - bounds.height * scale) / 2 - bounds.y * scale;

  d3.select(svgRef.value)
    .transition()
    .duration(300)
    .call(zoom.transform, d3.zoomIdentity.translate(tx, ty).scale(scale));
};

// Initialize visualization
const initVisualization = () => {
  if (!svgRef.value || props.nodes.length === 0) return;

  const svg = d3.select(svgRef.value);
  svg.selectAll('*').remove();

  contentGroup = svg.append('g');

  // Zoom behavior
  zoom = d3.zoom<SVGSVGElement, unknown>()
    .scaleExtent([0.1, 5])
    .on('zoom', (event) => {
      contentGroup?.attr('transform', event.transform);
      currentScale.value = event.transform.k;
    });

  svg.call(zoom);

  // Create local copies for D3
  const nodes = props.nodes.map(n => ({ ...n }));
  const edges = props.edges.map(e => ({ ...e }));

  // Force simulation
  simulation = d3.forceSimulation(nodes)
    .force('link', d3.forceLink(edges)
      .id((d: any) => d.id)
      .distance(100))
    .force('charge', d3.forceManyBody().strength(-300))
    .force('center', d3.forceCenter(props.width / 2, props.height / 2))
    .force('collision', d3.forceCollide().radius(50));

  // Draw edges
  const link = contentGroup.append('g')
    .attr('class', 'edges')
    .selectAll('line')
    .data(edges)
    .join('line')
    .attr('stroke', colors.value.edgeColor)
    .attr('stroke-width', 2);

  // Draw nodes
  const node = contentGroup.append('g')
    .attr('class', 'nodes')
    .selectAll<SVGGElement, PearltreeNode>('g')
    .data(nodes)
    .join('g')
    .attr('class', 'node')
    .style('cursor', 'pointer')
    .call(d3.drag<SVGGElement, PearltreeNode>()
      .on('start', (event, d) => {
        if (!event.active && simulation) simulation.alphaTarget(0.3).restart();
        d.fx = d.x;
        d.fy = d.y;
      })
      .on('drag', (event, d) => {
        d.fx = event.x;
        d.fy = event.y;
      })
      .on('end', (event, d) => {
        if (!event.active && simulation) simulation.alphaTarget(0);
      })
    );

  // Node ellipses
  node.append('ellipse')
    .attr('rx', d => d.type === 'root' ? 50 : 40)
    .attr('ry', d => d.type === 'root' ? 30 : 25)
    .attr('fill', d => colors.value.nodeColors[d.type]?.fill || colors.value.nodeColors.default.fill)
    .attr('stroke', d => colors.value.nodeColors[d.type]?.stroke || colors.value.nodeColors.default.stroke)
    .attr('stroke-width', d => d.type === 'root' ? 3 : 2);

  // Node labels
  node.append('text')
    .attr('text-anchor', 'middle')
    .attr('dominant-baseline', 'central')
    .attr('fill', d => colors.value.nodeColors[d.type]?.text || colors.value.nodeColors.default.text)
    .attr('font-size', d => d.type === 'root' ? 14 : 12)
    .attr('font-weight', d => d.type === 'root' ? 'bold' : 'normal')
    .text(d => d.label.length > 15 ? d.label.substring(0, 12) + '...' : d.label);

  // URL indicator
  node.filter(d => !!d.url)
    .append('text')
    .attr('x', 30)
    .attr('y', -15)
    .attr('font-size', 10)
    .text('ðŸ”—');

  // Click handler
  node.on('click', (event, d) => {
    selectedNode.value = d;
    emit('nodeClick', d);
  });

  // Simulation tick
  simulation.on('tick', () => {
    link
      .attr('x1', (d: any) => d.source.x)
      .attr('y1', (d: any) => d.source.y)
      .attr('x2', (d: any) => d.target.x)
      .attr('y2', (d: any) => d.target.y);

    node.attr('transform', d => `translate(${d.x},${d.y})`);
  });
};

// Lifecycle
onMounted(() => {
  initVisualization();
});

onUnmounted(() => {
  simulation?.stop();
});

// Watch for changes
watch(() => [props.nodes, props.edges, props.theme], () => {
  initVisualization();
}, { deep: true });

// Expose methods for parent control
defineExpose({
  zoomIn,
  zoomOut,
  zoomReset,
  fitToContent
});
</script>

