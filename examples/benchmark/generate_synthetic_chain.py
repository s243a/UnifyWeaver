#!/usr/bin/env python3
"""
Generate a synthetic linear-chain category graph with deep paths.

Produces a chain `cat_001 -> cat_002 -> ... -> cat_N` where each
category has exactly one parent and `cat_N` is the sole root. This
forces the effective-distance workload's `category_ancestor` recursion
to walk a long visited list (up to N-1 elements deep) — exercising
exactly the pathological shape Phase H IntSet was designed for.

Articles are placed at a few nodes near the leaf end (`cat_001`,
`cat_002`, ...) so a query reaches the root through the full chain.

Usage:
    python examples/benchmark/generate_synthetic_chain.py \\
        --depth 100 \\
        --output data/benchmark/synth_chain_100/

The output directory will contain `facts.pl` ready to be consumed by
`tests/benchmarks/wam_effective_distance_macro_bench.pl` via the
`WAM_EFF_DIST_BENCH_SCALE=synth_chain_100` env var.
"""

import argparse
from pathlib import Path


HEADER = """%% Synthetic linear-chain category graph for IntSet visited-set
%% benchmarks. Generated by examples/benchmark/generate_synthetic_chain.py.
%%
%% Topology: cat_001 -> cat_002 -> ... -> cat_{N}.
%% Single root: cat_{N}. All articles reach it through the full chain.
%%
%% Designed to produce visited lists of length up to {N}-1 during
%% category_ancestor recursion, forcing the algorithmic difference
%% between O(N) list walk and O(log N) IntSet to dominate the
%% per-call constant factors.

:- discontiguous article_category/2.
:- discontiguous category_parent/2.
:- discontiguous root_category/1.

"""


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--depth", type=int, default=100,
                    help="chain length (default 100)")
    ap.add_argument("--articles", type=int, default=20,
                    help="number of articles to place near the leaf end")
    ap.add_argument("--output", required=True,
                    help="output directory; facts.pl + TSVs will be written there")
    args = ap.parse_args()

    out_dir = Path(args.output)
    out_dir.mkdir(parents=True, exist_ok=True)

    n = args.depth
    a = max(1, min(args.articles, n))

    # facts.pl for SWI-Prolog consumers
    lines = [HEADER.format(N=n)]
    for i in range(1, n):
        lines.append(f"category_parent(cat_{i:03d}, cat_{i+1:03d}).")
    lines.append("")
    lines.append(f"root_category(cat_{n:03d}).")
    lines.append("")
    for i in range(1, a + 1):
        lines.append(f"article_category(art_{i:03d}, cat_{i:03d}).")
    lines.append("")

    facts_path = out_dir / "facts.pl"
    facts_path.write_text("\n".join(lines))

    # TSVs for the compiled WAM Haskell target's external_source FFI
    cp_path = out_dir / "category_parent.tsv"
    with cp_path.open("w") as f:
        f.write("child\tparent\n")
        for i in range(1, n):
            f.write(f"cat_{i:03d}\tcat_{i+1:03d}\n")

    ac_path = out_dir / "article_category.tsv"
    with ac_path.open("w") as f:
        f.write("article\tcategory\n")
        for i in range(1, a + 1):
            f.write(f"art_{i:03d}\tcat_{i:03d}\n")

    rc_path = out_dir / "root_categories.tsv"
    with rc_path.open("w") as f:
        f.write("category\n")
        f.write(f"cat_{n:03d}\n")

    print(f"[OK] wrote {facts_path}, {cp_path}, {ac_path}, {rc_path} "
          f"(chain depth={n}, articles={a})")


if __name__ == "__main__":
    main()
