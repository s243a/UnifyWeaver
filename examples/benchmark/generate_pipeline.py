def load_facts(facts_path):
    """Load facts from Prolog file."""
    article_cats = defaultdict(list)
    category_parents = defaultdict(list)  # child → [parent, ...]
    root_cats = set()

    with open(facts_path) as f:
        for line in f:
            line = line.strip()
            if line.startswith("article_category("):
                inner = line[len("article_category("):-2]
                parts = inner.split("', '")
                if len(parts) == 2:
                    art = parts[0].strip("'")
                    cat = parts[1].strip("'")
                    article_cats[art].append(cat)
            elif line.startswith("category_parent("):
                inner = line[len("category_parent("):-2]
                parts = inner.split("', '")
                if len(parts) == 2:
                    child = parts[0].strip("'")
                    parent = parts[1].strip("'")
                    category_parents[child].append(parent)
            elif line.startswith("root_category("):
                inner = line[len("root_category("):-2]
                root = inner.strip("'")
                root_cats.add(root)

    # Unescape Prolog string escapes (\' → ', \\ → \)
    def unescape(d):
        return {k.replace("\\'", "'").replace("\\\\", "\\"):
                [v.replace("\\'", "'").replace("\\\\", "\\") for v in vs]
                for k, vs in d.items()}

    return unescape(article_cats), unescape(category_parents), root_cats


def generate_awk(article_cats, category_parents, root_cats, n=5, max_depth=10):
    """Generate self-contained AWK program for effective distance."""
    root = list(root_cats)[0] if root_cats else "Physics"

    # Build edge lines
    edge_lines = []
    for child, parents in sorted(category_parents.items()):
        for parent in parents:
            edge_lines.append(f'    edges[++ne] = "{child}\\t{parent}"')

    # Build article→category lines
    ac_lines = []
    for art, cats in sorted(article_cats.items()):
        for cat in cats:
            ac_lines.append(f'    art_cat[++nac] = "{art}\\t{cat}"')

    edges_block = '\n'.join(edge_lines)
    ac_block = '\n'.join(ac_lines)

    return f'''# Self-contained effective distance benchmark (AWK)
# d_eff = (Σ hops^(-{n}))^(-1/{n})
# Root: {root}
# Generated by generate_pipeline.py
BEGIN {{
    FS = "\\t"
    ROOT = "{root}"
    N = {n}
    MAX_DEPTH = {max_depth}

    # Load category hierarchy edges
{edges_block}

    # Load article → category mappings
{ac_block}

    # Build adjacency list
    for (i = 1; i <= ne; i++) {{
        split(edges[i], e, FS)
        adj_n[e[1]]++
        adj[e[1], adj_n[e[1]]] = e[2]
    }}

    # DFS all-simple-paths using recursive function simulation.
    # Uses AWK associative array for O(1) visited lookup.
    # Since AWK has no real recursion, we implement iterative DFS
    # where each stack frame records the node to REMOVE from visited
    # when backtracking (the node that was ADDED when entering this frame).
    # Store: ancestor_hops[source_cat, ancestor] = "h1 h2 h3 ..."
    for (src in adj_n) {{
        # visited[node] = 1 means node is on current path
        delete vis
        vis[src] = 1

        # Stack stores: node, hops, child_index (which child to explore next)
        # When child_index > adj_n[node], we backtrack (remove node from vis)
        sp = 1
        stk_node[1] = src
        stk_hops[1] = 0
        stk_cidx[1] = 1

        while (sp > 0) {{
            cur = stk_node[sp]
            hops = stk_hops[sp]
            ci = stk_cidx[sp]

            if (ci > adj_n[cur] || hops >= MAX_DEPTH) {{
                # Backtrack: all children explored or depth limit
                delete vis[cur]
                sp--
                continue
            }}

            # Advance to next child for this frame
            stk_cidx[sp] = ci + 1

            nb = adj[cur, ci]
            if (nb in vis) continue  # O(1) cycle check

            nh = hops + 1

            # Record path to root
            if (nb == ROOT) {{
                key = src SUBSEP ROOT
                ancestor_hops[key] = ancestor_hops[key] " " nh
            }}

            # Push child frame
            vis[nb] = 1
            sp++
            stk_node[sp] = nb
            stk_hops[sp] = nh
            stk_cidx[sp] = 1
        }}
    }}

    # Compute effective distance per (article, root)
    print "article" FS "root_category" FS "effective_distance"

    # Collect results in array for sorting
    nresults = 0
    for (i = 1; i <= nac; i++) {{
        split(art_cat[i], ac, FS)
        art = ac[1]
        cat = ac[2]

        # Direct: category IS root
        if (cat == ROOT) {{
            art_hops[art] = art_hops[art] " 1"
        }}

        # Via ancestors
        key = cat SUBSEP ROOT
        if (key in ancestor_hops) {{
            # Add 1 for article→category hop
            n_hops = split(ancestor_hops[key], hs, " ")
            for (h = 1; h <= n_hops; h++) {{
                if (hs[h] != "") {{
                    art_hops[art] = art_hops[art] " " (hs[h] + 1)
                }}
            }}
        }}
    }}

    # Compute d_eff for each article
    for (art in art_hops) {{
        n_hops = split(art_hops[art], hs, " ")
        weight_sum = 0
        count = 0
        for (h = 1; h <= n_hops; h++) {{
            if (hs[h] != "" && hs[h] + 0 > 0) {{
                weight_sum += (hs[h] + 0) ^ (-N)
                count++
            }}
        }}
        if (weight_sum > 0) {{
            deff = weight_sum ^ (-1.0 / N)
            results[++nresults] = sprintf("%s\\t%s\\t%.6f", art, ROOT, deff)
            result_deff[nresults] = deff
            result_art[nresults] = art
        }}
    }}

    # Sort by distance (insertion sort — small dataset)
    for (i = 2; i <= nresults; i++) {{
        key_d = result_deff[i]
        key_a = result_art[i]
        key_r = results[i]
        j = i - 1
        while (j > 0 && (result_deff[j] > key_d || (result_deff[j] == key_d && result_art[j] > key_a))) {{
            result_deff[j+1] = result_deff[j]
            result_art[j+1] = result_art[j]
            results[j+1] = results[j]
            j--
        }}
        result_deff[j+1] = key_d
        result_art[j+1] = key_a
        results[j+1] = key_r
    }}

    for (i = 1; i <= nresults; i++) {{
        print results[i]
    }}
}}
'''


