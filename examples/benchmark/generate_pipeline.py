def generate_rust(article_cats, category_parents, root_cats, n=5, max_depth=10):
    """Generate self-contained Rust program for effective distance."""
    root = list(root_cats)[0] if root_cats else "Physics"

    # Build adjacency initialization
    edge_lines = []
    for child, parents in sorted(category_parents.items()):
        child_esc = child.replace('"', '\\"')
        for parent in parents:
            parent_esc = parent.replace('"', '\\"')
            edge_lines.append(f'    adj.entry("{child_esc}".to_string()).or_insert_with(Vec::new).push("{parent_esc}".to_string());')

    # Build article→category initialization
    ac_lines = []
    for art, cats in sorted(article_cats.items()):
        art_esc = art.replace('"', '\\"')
        for cat in cats:
            cat_esc = cat.replace('"', '\\"')
            ac_lines.append(f'    art_cats.entry("{art_esc}".to_string()).or_insert_with(Vec::new).push("{cat_esc}".to_string());')

    edge_block = '\n'.join(edge_lines)
    ac_block = '\n'.join(ac_lines)

    return f'''// Self-contained effective distance benchmark (Rust)
// d_eff = (Σ hops^(-{n}))^(-1/{n})
// Root: {root}
// Generated by generate_pipeline.py
use std::collections::{{HashMap, HashSet}};

const ROOT: &str = "{root}";
const N: f64 = {n}.0;
const MAX_DEPTH: usize = {max_depth};

fn category_ancestor_dfs(
    cat: &str,
    visited: &HashSet<String>,
    adj: &HashMap<String, Vec<String>>,
    depth: usize,
) -> Vec<(String, i32)> {{
    if visited.contains(cat) || depth >= MAX_DEPTH {{
        return Vec::new();
    }}
    let mut new_visited = visited.clone();
    new_visited.insert(cat.to_string());

    let mut results = Vec::new();
    if let Some(neighbors) = adj.get(cat) {{
        for nb in neighbors {{
            if !new_visited.contains(nb.as_str()) {{
                results.push((nb.clone(), 1));
                for (ancestor, h) in category_ancestor_dfs(nb, &new_visited, adj, depth + 1) {{
                    results.push((ancestor, h + 1));
                }}
            }}
        }}
    }}
    results
}}

fn compute_deff(hops: &[i32]) -> f64 {{
    if hops.is_empty() {{
        return f64::INFINITY;
    }}
    let w: f64 = hops.iter().map(|&h| (h as f64).powf(-N)).sum();
    if w <= 0.0 {{
        return f64::INFINITY;
    }}
    w.powf(-1.0 / N)
}}

fn main() {{
    let mut adj: HashMap<String, Vec<String>> = HashMap::new();
{edge_block}

    let mut art_cats: HashMap<String, Vec<String>> = HashMap::new();
{ac_block}

    let mut arts: Vec<&String> = art_cats.keys().collect();
    arts.sort();

    let mut results: Vec<(f64, &String)> = Vec::new();

    for art in &arts {{
        let mut all_hops: Vec<i32> = Vec::new();
        if let Some(cats) = art_cats.get(*art) {{
            for cat in cats {{
                if cat == ROOT {{
                    all_hops.push(1);
                }}
                let visited = HashSet::new();
                for (ancestor, h) in category_ancestor_dfs(cat, &visited, &adj, 0) {{
                    if ancestor == ROOT {{
                        all_hops.push(h + 1);
                    }}
                }}
            }}
        }}
        if !all_hops.is_empty() {{
            let deff = compute_deff(&all_hops);
            results.push((deff, art));
        }}
    }}

    results.sort_by(|a, b| a.0.partial_cmp(&b.0).unwrap().then(a.1.cmp(b.1)));

    println!("article\\troot_category\\teffective_distance");
    for (deff, art) in &results {{
        println!("{{}}\\t{{}}\\t{{:.6}}", art, ROOT, deff);
    }}
}}
'''


def generate_csharp(article_cats, category_parents, root_cats, n=5, max_depth=10):
    """Generate self-contained C# program for effective distance."""
    root = list(root_cats)[0] if root_cats else "Physics"

    # Build adjacency initialization
    edge_lines = []
    for child, parents in sorted(category_parents.items()):
        child_esc = child.replace('"', '\\"')
        for parent in parents:
            parent_esc = parent.replace('"', '\\"')
            edge_lines.append(f'        if (!adj.ContainsKey("{child_esc}")) adj["{child_esc}"] = new List<string>();')
            edge_lines.append(f'        adj["{child_esc}"].Add("{parent_esc}");')

    # Build article→category initialization
    ac_lines = []
    for art, cats in sorted(article_cats.items()):
        art_esc = art.replace('"', '\\"')
        for cat in cats:
            cat_esc = cat.replace('"', '\\"')
            ac_lines.append(f'        if (!artCats.ContainsKey("{art_esc}")) artCats["{art_esc}"] = new List<string>();')
            ac_lines.append(f'        artCats["{art_esc}"].Add("{cat_esc}");')

    edge_block = '\n'.join(edge_lines)
    ac_block = '\n'.join(ac_lines)

    return f'''// Self-contained effective distance benchmark (C#)
// d_eff = (Σ hops^(-{n}))^(-1/{n})
// Root: {root}
// Generated by generate_pipeline.py
using System;
using System.Collections.Generic;
using System.Linq;

class EffectiveDistanceBenchmark
{{
    const string ROOT = "{root}";
    const double N = {n}.0;
    const int MAX_DEPTH = {max_depth};

    static Dictionary<string, List<string>> adj = new();
    static Dictionary<string, List<string>> artCats = new();

    static void InitData()
    {{
{edge_block}
{ac_block}
    }}

    static List<(string ancestor, int hops)> CategoryAncestorDFS(
        string cat, HashSet<string> visited, int depth)
    {{
        var results = new List<(string, int)>();
        if (visited.Contains(cat) || depth >= MAX_DEPTH) return results;

        var newVisited = new HashSet<string>(visited);
        newVisited.Add(cat);

        if (adj.TryGetValue(cat, out var neighbors))
        {{
            foreach (var nb in neighbors)
            {{
                if (!newVisited.Contains(nb))
                {{
                    results.Add((nb, 1));
                    foreach (var (ancestor, h) in CategoryAncestorDFS(nb, newVisited, depth + 1))
                    {{
                        results.Add((ancestor, h + 1));
                    }}
                }}
            }}
        }}
        return results;
    }}

    static double ComputeDeff(List<int> hops)
    {{
        if (hops.Count == 0) return double.PositiveInfinity;
        double w = hops.Sum(h => Math.Pow(h, -N));
        return w > 0 ? Math.Pow(w, -1.0 / N) : double.PositiveInfinity;
    }}

    static void Main()
    {{
        InitData();

        var results = new List<(double deff, string article)>();

        foreach (var art in artCats.Keys.OrderBy(x => x))
        {{
            var allHops = new List<int>();
            foreach (var cat in artCats[art])
            {{
                if (cat == ROOT) allHops.Add(1);
                foreach (var (ancestor, h) in CategoryAncestorDFS(cat, new HashSet<string>(), 0))
                {{
                    if (ancestor == ROOT) allHops.Add(h + 1);
                }}
            }}
            if (allHops.Count > 0)
            {{
                results.Add((ComputeDeff(allHops), art));
            }}
        }}

        results.Sort((a, b) =>
        {{
            int c = a.deff.CompareTo(b.deff);
            return c != 0 ? c : string.Compare(a.article, b.article, StringComparison.Ordinal);
        }});

        Console.WriteLine("article\\troot_category\\teffective_distance");
        foreach (var (deff, art) in results)
        {{
            Console.WriteLine($"{{art}}\\t{{ROOT}}\\t{{deff:F6}}");
        }}
    }}
}}
'''


GENERATORS = {
    'awk': generate_awk,
    'python': generate_python,
    'go': generate_go,
    'rust': generate_rust,
    'csharp': generate_csharp,
}


def main():
    parser = argparse.ArgumentParser(
        description="Generate self-contained benchmark pipeline per target"
    )
    parser.add_argument("--facts", required=True, help="Prolog facts file")
    parser.add_argument("--root", default="Physics", help="Root category")
    parser.add_argument("--target", required=True, choices=list(GENERATORS.keys()))
    parser.add_argument("--output", required=True, help="Output file path")
    parser.add_argument("--n", type=float, default=5, help="Dimensionality parameter")
    parser.add_argument("--max-depth", type=int, default=10,
                        help="Max DFS depth (paths beyond this are trimmed; "
                             "d^(-5) at depth 10 is negligible)")

    args = parser.parse_args()

    article_cats, category_parents, root_cats = load_facts(args.facts)
    if args.root:
        root_cats = {args.root}

    generator = GENERATORS[args.target]
    code = generator(article_cats, category_parents, root_cats,
                     n=int(args.n), max_depth=args.max_depth)

    Path(args.output).parent.mkdir(parents=True, exist_ok=True)
    with open(args.output, 'w') as f:
        f.write(code)

    print(f"Generated {args.target} pipeline: {args.output}")
    print(f"  Articles: {len(article_cats)}")
    print(f"  Category edges: {sum(len(v) for v in category_parents.values())}")
    print(f"  Root: {args.root}")


if __name__ == "__main__":
    main()
