#!/usr/bin/env bash
# Regenerate the (gitignored) cumulative scored set used for replay (--replay-pairs) and dedup
# (--dedup-against). It is the UNION of every round's committed scored set; duplicate pairs collapse
# downstream (dedup keys by sorted (a,b)). Kept out of git because it is 100% derived — run this after a
# fresh clone or after adding a new round's mu_pairs_scored_<round>_<yymmdd-HHMMSS>.tsv.
# Naming convention: each scored round is timestamped with its first-commit time in MOUNTAIN time
# (America/Denver), yymmdd-HHMMSS — so same-day rounds stay distinguishable and ordered.
cd "$(dirname "$0")"
ROUNDS=(mu_pairs_scored_eng_260621-174251.tsv mu_pairs_scored_enwiki_260622-085608.tsv mu_pairs_scored_mathfields_260622-123223.tsv
        mu_pairs_scored_alggeom_260622-162338.tsv mu_pairs_scored_boundary_260622-171513.tsv mu_pairs_scored_stats_260622-182510.tsv
        mu_pairs_scored_sysinfo_260622-184444.tsv mu_pairs_scored_cyber_260622-223014.tsv
        mu_pairs_scored_systheory_260622-231618.tsv
        mu_pairs_scored_pages_260623-002356.tsv
        mu_pairs_scored_pt_circuit_260623-061346.tsv)
{ echo "# cumulative scored set (regenerated by make_cumulative.sh) = union of all round scored sets"
  grep -hv '^#' "${ROUNDS[@]}"; } > mu_pairs_scored_cumulative.tsv
# "prior" = the pre-mathfields baseline (eng + enwiki), used as the mathfields round's replay baseline.
{ echo "# prior cumulative (regenerated) = mu_pairs_scored_eng_260621-174251.tsv + mu_pairs_scored_enwiki_260622-085608.tsv"
  grep -hv '^#' mu_pairs_scored_eng_260621-174251.tsv mu_pairs_scored_enwiki_260622-085608.tsv; } > mu_pairs_scored_prior.tsv
echo "cumulative: $(grep -vc '^#' mu_pairs_scored_cumulative.tsv) rows ; prior: $(grep -vc '^#' mu_pairs_scored_prior.tsv) rows"
